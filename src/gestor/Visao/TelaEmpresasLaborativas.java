    private void jTabelaEmpresasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaEmpresasMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String razaoSocial = "" + jTabelaEmpresas.getValueAt(jTabelaEmpresas.getSelectedRow(), 1);
            String idEmp = "" + jTabelaEmpresas.getValueAt(jTabelaEmpresas.getSelectedRow(), 0);
            jIDEmp.setText(idEmp);
            jPesqNomeEmpresa.setText(razaoSocial);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM EMPRESALAB "
                        + "WHERE RazaoSocial='" + jPesqNomeEmpresa.getText() + "' "
                        + "AND IdEmp='" + jIDEmp.getText() + "'");
                conecta.rs.first();
                jIDEmp.setText(String.valueOf(conecta.rs.getInt("IdEmp")));
                jComboBoxClass.setSelectedItem(conecta.rs.getString("StatusEmp"));
                jDataCadastro.setDate(conecta.rs.getDate("DataCad"));
                jRazaoSocial.setText(conecta.rs.getString("RazaoSocial"));
                jNomeFantasia.setText(conecta.rs.getString("NomeFantasia"));
                jCNPJ.setText(conecta.rs.getString("Cnpj"));
                jComboBoxTipoEmpresa.setSelectedItem(conecta.rs.getString("TipoEmpresa"));
                jInsEstadual.setText(conecta.rs.getString("InsEsta"));
                jEndereco.setText(conecta.rs.getString("Endereco"));
                jCidade.setText(conecta.rs.getString("Cidade"));
                jEstado.setText(conecta.rs.getString("Estado"));
                jCEP.setText(conecta.rs.getString("Cep"));
                jTelefone.setText(conecta.rs.getString("Telefone"));
                jTelefone1.setText(conecta.rs.getString("Telefone1"));
                jContato.setText(conecta.rs.getString("Contato"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            countHist = 0;
            HistoricoEmpresaInternos("SELECT * FROM PRONTUARIOSCRC "
                    + "INNER JOIN HISTORICOLABORINTERNO "
                    + "ON PRONTUARIOSCRC.IdInternoCrc=HISTORICOLABORINTERNO.IdInternoCrc "
                    + "WHERE IdEmp='" + jIDEmp.getText() + "'");
        }
    }//GEN-LAST:event_jTabelaEmpresasMouseClicked

