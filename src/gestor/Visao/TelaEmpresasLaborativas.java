    public void formatarCampos() {
        jPesqNomeEmpresa.setDocument(new LimiteDigitos(100));
        jPesqNomeInterno.setDocument(new LimiteDigitos(50));
        jRazaoSocial.setDocument(new LimiteDigitos(45));
        jNomeFantasia.setDocument(new LimiteDigitos(80));
        jEndereco.setDocument(new LimiteDigitosAlfa(180));
        jCidade.setDocument(new LimiteDigitos(80));
        jEstado.setDocument(new LimiteDigitos(50));
        jContato.setDocument(new LimiteDigitos(80));
        jInsEstadual.setDocument(new LimiteDigitosNum(25));
        try {
            MaskFormatter telefone = new MaskFormatter("(###)-####-####");
            jTelefone.setFormatterFactory(new DefaultFormatterFactory(telefone));
            MaskFormatter telefone1 = new MaskFormatter("(###)-####-####");
            jTelefone1.setFormatterFactory(new DefaultFormatterFactory(telefone1));
            MaskFormatter cep = new MaskFormatter("##.###-###");
            jCEP.setFormatterFactory(new DefaultFormatterFactory(cep));
            MaskFormatter cnpj = new MaskFormatter("##.###.###/####-##");
            jCNPJ.setFormatterFactory(new DefaultFormatterFactory(cnpj));
        } catch (ParseException ex) {
            Logger.getLogger(TelaEmpresasLaborativas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

