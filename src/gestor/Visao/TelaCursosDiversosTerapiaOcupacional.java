    public void preencherTabelaCursos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Nome do Curso"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdCurso"), conecta.rs.getString("StatusCurso"), conecta.rs.getString("DescricaoCurso")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaCursos.setModel(modelo);
        jTabelaCursos.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaCursos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaCursos.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaCursos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaCursos.getColumnModel().getColumn(2).setPreferredWidth(320);
        jTabelaCursos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaCursos.setAutoResizeMode(jTabelaCursos.AUTO_RESIZE_OFF);
        jTabelaCursos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaCursos();
        conecta.desconecta();
    }

