    public void preencherTabela() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição"};
        conecta.abrirConexao();
        conecta.executaSQL("SELECT * FROM CARGOS ORDER BY IdCargo");
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdCargo"), conecta.rs.getString("NomeCargo")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe dados a ser exibido!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaCargo.setModel(modelo);
        jTabelaCargo.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaCargo.getColumnModel().getColumn(0).setResizable(false);
        jTabelaCargo.getColumnModel().getColumn(1).setPreferredWidth(370);
        jTabelaCargo.getColumnModel().getColumn(1).setResizable(false);
        jTabelaCargo.getTableHeader().setReorderingAllowed(false);
        jTabelaCargo.setAutoResizeMode(jTabelaCargo.AUTO_RESIZE_OFF);
        jTabelaCargo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

