                conecta.executaSQL("SELECT * FROM FICHA_JURIDICA "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON FICHA_JURIDICA.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "WHERE IdFicha='" + IdLanc + "'");
                conecta.rs.first();
