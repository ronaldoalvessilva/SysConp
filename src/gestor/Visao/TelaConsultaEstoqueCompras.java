    public void preencherTabelaEstoqueProdutos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Código Barra", "Descrição", "Grupo Produtos", "Quant.", "Valor R$"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                qtdEstoque = conecta.rs.getFloat("SaldoAtual");
                DecimalFormat qtd = new DecimalFormat("#,##0.00");
                String estoqueAtual = qtd.format(qtdEstoque);
                saldoAtualEstoque = estoqueAtual;
                //
                valorCompra = conecta.rs.getFloat("ValorCompra");
                DecimalFormat vlc = new DecimalFormat("#,##0.00");
                String valorCompras = vlc.format(valorCompra);
                valorCompraProd = valorCompras;
                //
                count = count + 1;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getString("IdProd"), conecta.rs.getString("CodigoBarra"), conecta.rs.getString("DescricaoProd"), conecta.rs.getString("NomeGrupo"), saldoAtualEstoque, valorCompraProd});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem exibidos....");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaEstoqueProduto.setModel(modelo);
        jTabelaEstoqueProduto.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaEstoqueProduto.getColumnModel().getColumn(0).setResizable(false);
        jTabelaEstoqueProduto.getColumnModel().getColumn(1).setPreferredWidth(90);
        jTabelaEstoqueProduto.getColumnModel().getColumn(1).setResizable(false);
        jTabelaEstoqueProduto.getColumnModel().getColumn(2).setPreferredWidth(320);
        jTabelaEstoqueProduto.getColumnModel().getColumn(2).setResizable(false);
        jTabelaEstoqueProduto.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTabelaEstoqueProduto.getColumnModel().getColumn(3).setResizable(false);
        jTabelaEstoqueProduto.getColumnModel().getColumn(4).setPreferredWidth(60);
        jTabelaEstoqueProduto.getColumnModel().getColumn(4).setResizable(false);
        jTabelaEstoqueProduto.getColumnModel().getColumn(5).setPreferredWidth(80);
        jTabelaEstoqueProduto.getColumnModel().getColumn(5).setResizable(false);
        jTabelaEstoqueProduto.setAutoResizeMode(jTabelaEstoqueProduto.AUTO_RESIZE_OFF);
        jTabelaEstoqueProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinhaColunasTabelaEstoqueProdutos();
        conecta.desconecta();
    }

