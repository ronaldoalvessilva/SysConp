    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String nomeInterno = "" + jTabelaDepartamento.getValueAt(jTabelaDepartamento.getSelectedRow(), 1);
            jDescricaoDepto.setText(nomeInterno);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM DEPARTAMENTOS WHERE NomeDepartamento LIKE'" + jDescricaoDepto.getText() + "%'");
                conecta.rs.first();
                jQualSetor.setText(conecta.rs.getString("NomeDepartamento"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

