    public void buscarInternosLocal(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Nome do Interno", "Cela", "Pavilhão"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdInternoCrc"), conecta.rs.getString("NomeInternoCrc"), conecta.rs.getString("EndCelaPav"), conecta.rs.getString("DescricaoPav")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqCelasInternos.setModel(modelo);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(2).setPreferredWidth(140);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTabelaPesqCelasInternos.getColumnModel().getColumn(3).setResizable(false);
        jTabelaPesqCelasInternos.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqCelasInternos.setAutoResizeMode(jTabelaPesqCelasInternos.AUTO_RESIZE_OFF);
        jTabelaPesqCelasInternos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaInternos();
        conecta.desconecta();
    }

