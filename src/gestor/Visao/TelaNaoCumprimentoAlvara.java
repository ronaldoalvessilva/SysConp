    public void preencherTabelaCumprimentoAlvara(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Tipo Alvará", "Nome Completo do Interno"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Fortmatar data de Cadastro          
                dataCadastro = conecta.rs.getString("DataLanc");
                String day = dataCadastro.substring(8, 10);
                String mesc = dataCadastro.substring(5, 7);
                String anoc = dataCadastro.substring(0, 4);
                dataCadastro = day + "/" + mesc + "/" + anoc;
                //
                tipoAlvara = conecta.rs.getInt("TipoCumprimento");
                if (tipoAlvara == 0) {
                    descricaoAlvara = "Alvará Não Cumprido";
                } else if (tipoAlvara == 1) {
                    descricaoAlvara = "Alvará Cumprido";
                }
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdLanc"), dataCadastro, conecta.rs.getString("StatusLanc"), descricaoAlvara, conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaCumprimentoAlvara.setModel(modelo);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(0).setResizable(false);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(1).setResizable(false);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(2).setResizable(false);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(3).setPreferredWidth(120);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(3).setResizable(false);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(4).setPreferredWidth(280);
        jTabelaCumprimentoAlvara.getColumnModel().getColumn(4).setResizable(false);
        jTabelaCumprimentoAlvara.getTableHeader().setReorderingAllowed(false);
        jTabelaCumprimentoAlvara.setAutoResizeMode(jTabelaCumprimentoAlvara.AUTO_RESIZE_OFF);
        jTabelaCumprimentoAlvara.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

