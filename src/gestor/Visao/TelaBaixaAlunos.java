    public void preencherTabelaBaixaInternos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Data", "Observação "};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataCadastro = conecta.rs.getString("DataLanc");
                String dia = dataCadastro.substring(8, 10);
                String mes = dataCadastro.substring(5, 7);
                String ano = dataCadastro.substring(0, 4);
                dataCadastro = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdLanc"), conecta.rs.getString("StatusLanc"), dataCadastro, conecta.rs.getString("Observacao")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesquisaInternosBaixa.setModel(modelo);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(2).setResizable(false);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(3).setPreferredWidth(420);
        jTabelaPesquisaInternosBaixa.getColumnModel().getColumn(3).setResizable(false);
        jTabelaPesquisaInternosBaixa.getTableHeader().setReorderingAllowed(false);
        jTabelaPesquisaInternosBaixa.setAutoResizeMode(jTabelaPesquisaInternosBaixa.AUTO_RESIZE_OFF);
        jTabelaPesquisaInternosBaixa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

