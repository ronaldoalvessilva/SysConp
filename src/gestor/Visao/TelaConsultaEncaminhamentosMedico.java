    private void jTabelaEncaminhamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaEncaminhamentoMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String idRegistro = "" + jTabelaEncaminhamento.getValueAt(jTabelaEncaminhamento.getSelectedRow(), 0);
            jIdRegistro.setText(idRegistro);
            //            
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ITENS_ENCAMINHAMENTO_CIRURGIAS_ESPECIALISTAS "
                        + "INNER JOIN ENCAMINHAMENTO_CIRURGIAS_ESPECIALISTAS "
                        + "ON ITENS_ENCAMINHAMENTO_CIRURGIAS_ESPECIALISTAS.IdEnca=ENCAMINHAMENTO_CIRURGIAS_ESPECIALISTAS.IdEnca "
                        + "WHERE ITENS_ENCAMINHAMENTO_CIRURGIAS_ESPECIALISTAS.IdItemEnca='" + idRegistro + "'");
                conecta.rs.first();
                tipoEncaminhaCE = conecta.rs.getInt("TipoEnca");
                if (tipoEncaminhaCE == 0) {
                    jRadioBtCirurgia.setSelected(true);
                } else if (tipoEncaminhaCE == 1) {
                    jRadioBtEspecialista.setSelected(true);
                }
                jIdRegistro.setText(conecta.rs.getString("IdItemEnca"));
                jDescricaoEncaminhamento.setText(conecta.rs.getString("DescricaoEncaminhamento"));
                jDataSaidaEnca.setDate(conecta.rs.getDate("DataSaida"));
                jHoraSaidaEnca.setText(conecta.rs.getString("HoraSaida"));
                jDataEntradaEnca.setDate(conecta.rs.getDate("DataRetorno"));
                jHoraRetornoEnca.setText(conecta.rs.getString("HoraRetorno"));
                jDestino.setText(conecta.rs.getString("Destino"));
                jMotivoEnca.setText(conecta.rs.getString("Motivo"));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível exibir registro.\nERRO: " + e);
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaEncaminhamentoMouseClicked

