    public void preencherTabelaEncaminhamentos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Registro", "Descrição do Encaminhamento", "Data Saída", "Hora Saída", "Data Retorno", "Hora Retorno"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            count = 0;
            do {
                count = count + 1;
                // Fortmatar data de Cadastro          
                dataSaida = conecta.rs.getString("DataSaida");
                String day = dataSaida.substring(8, 10);
                String mesc = dataSaida.substring(5, 7);
                String anoc = dataSaida.substring(0, 4);
                dataSaida = day + "/" + mesc + "/" + anoc;
                //
                dataRetorno = conecta.rs.getString("DataRetorno");
                String dia = dataRetorno.substring(8, 10);
                String mes = dataRetorno.substring(5, 7);
                String ano = dataRetorno.substring(0, 4);
                dataRetorno = dia + "/" + mes + "/" + ano;
                //
                dados.add(new Object[]{conecta.rs.getInt("IdItemEnca"), conecta.rs.getString("DescricaoEncaminhamento"), dataSaida, conecta.rs.getString("HoraSaida"), dataRetorno, conecta.rs.getString("HoraRetorno")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaEncaminhamento.setModel(modelo);
        jTabelaEncaminhamento.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaEncaminhamento.getColumnModel().getColumn(0).setResizable(false);
        jTabelaEncaminhamento.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTabelaEncaminhamento.getColumnModel().getColumn(1).setResizable(false);
        jTabelaEncaminhamento.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTabelaEncaminhamento.getColumnModel().getColumn(2).setResizable(false);
        jTabelaEncaminhamento.getColumnModel().getColumn(3).setPreferredWidth(70);
        jTabelaEncaminhamento.getColumnModel().getColumn(3).setResizable(false);
        jTabelaEncaminhamento.getColumnModel().getColumn(4).setPreferredWidth(80);
        jTabelaEncaminhamento.getColumnModel().getColumn(4).setResizable(false);
        jTabelaEncaminhamento.getColumnModel().getColumn(5).setPreferredWidth(80);
        jTabelaEncaminhamento.getColumnModel().getColumn(5).setResizable(false);
        jTabelaEncaminhamento.getTableHeader().setReorderingAllowed(false);
        jTabelaEncaminhamento.setAutoResizeMode(jTabelaEncaminhamento.AUTO_RESIZE_OFF);
        jTabelaEncaminhamento.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaPatologias();
        conecta.desconecta();
    }

