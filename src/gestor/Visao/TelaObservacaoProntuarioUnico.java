    public void buscarRegistros(String sql) {
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            jObservacoes.setText(conecta.rs.getString("Observacao"));
        } catch (SQLException ex) {
        }
    }   
