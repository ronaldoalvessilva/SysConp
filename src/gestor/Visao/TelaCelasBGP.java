    private void jTabelaCelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaCelasMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomePavilhao = "" + jTabelaCelas.getValueAt(jTabelaCelas.getSelectedRow(), 2);
            jPesqDescricao.setText(nomePavilhao);
            String IdPav = "" + jTabelaCelas.getValueAt(jTabelaCelas.getSelectedRow(), 0);
            String endCela = "" + jTabelaCelas.getValueAt(jTabelaCelas.getSelectedRow(), 3);
            jIdCela.setText(IdPav);
            jLocal.setText(endCela);
            jLocal.setEnabled(!true);
            jPavilhao.setEnabled(!true);
            jBtNovo.setEnabled(!true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM CELAS "
                        + "INNER JOIN PAVILHAO "
                        + "ON CELAS.IdPav=PAVILHAO.IdPav "
                        + "WHERE EndCelaPav='" + endCela + " "
                        + "'AND idCela='" + IdPav + "'");
                conecta.rs.first();
                jIdCela.setText(String.valueOf(conecta.rs.getInt("IdCela")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusCela"));
                jLocal.setText(conecta.rs.getString("EndCelaPav"));
                jPavilhao.setText(conecta.rs.getString("DescricaoPav"));
                jCapacidade.setText(conecta.rs.getString("Capacidade"));
                jNumeroCela.setText(conecta.rs.getString("NrCela"));
                jMotivoInativacao.setText(conecta.rs.getString("Motivo"));
                conecta.desconecta();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + e);
            }
        }
    }//GEN-LAST:event_jTabelaCelasMouseClicked

