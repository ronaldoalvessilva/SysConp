    public void preencherTabelaFornecedores(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Razão Social do Fornecedor"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getString("IdForn"), conecta.rs.getString("RazaoSocial")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem exibidos....");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaFornecedores.setModel(modelo);
        jTabelaFornecedores.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelaFornecedores.getColumnModel().getColumn(0).setResizable(false);
        jTabelaFornecedores.getColumnModel().getColumn(1).setPreferredWidth(370);
        jTabelaFornecedores.getColumnModel().getColumn(1).setResizable(false);
        jTabelaFornecedores.setAutoResizeMode(jTabelaFornecedores.AUTO_RESIZE_OFF);
        jTabelaFornecedores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinhaColunasTabelaCotacoes();
        conecta.desconecta();
    }

