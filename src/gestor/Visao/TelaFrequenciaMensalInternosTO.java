    private void jBtImpressaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtImpressaoActionPerformed
        // TODO add your handling code here:
        Integer rows = jTabelaDemostracaoMensal.getModel().getRowCount();
        if (jIdInternoCrcDemo.getText().equals("") && rows == 0) {
            JOptionPane.showMessageDialog(rootPane, "Não é possível imprimir o relatório.");
        } else {
            try {
                conecta.abrirConexao();
                String path = "reports/RelatorioDiasTrabalhadoIndividual.jasper";
                conecta.executaSQL("SELECT * FROM FREQUENCIA_LABORATIVA_EXTERNA "
                        + "INNER JOIN ITENS_FREQUENCIA_LABORATIVA_EXTERNA "
                        + "ON FREQUENCIA_LABORATIVA_EXTERNA.IdFreqLab=ITENS_FREQUENCIA_LABORATIVA_EXTERNA.IdFreqLab "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ITENS_FREQUENCIA_LABORATIVA_EXTERNA.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "WHERE PRONTUARIOSCRC.IdInternoCrc='" + jIdInternoCrcDemo.getText() + "'");
                HashMap parametros = new HashMap();
                parametros.put("idInterno", jIdInternoCrcDemo.getText());
                parametros.put("nomeUsuario", nameUser);
                parametros.put("dataInicial", jDataPesqInicial.getDate());
                parametros.put("dataFinal", jDataPesqFinal.getDate());
                JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio                                   
                JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao          
                jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                jv.setTitle("Relatório de Dias Trabalhados por Interno");
                jv.setVisible(true); // Chama o relatorio para ser visualizado                                    
                jv.toFront(); // Traz o relatorio para frente da aplicação            
                conecta.desconecta();
            } catch (JRException e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório \n\nERRO :" + e);
            }
        }
    }//GEN-LAST:event_jBtImpressaoActionPerformed

