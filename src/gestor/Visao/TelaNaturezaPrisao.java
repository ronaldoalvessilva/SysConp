    public void preencherTabelaNatureza(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código ", "Data", "Status", "Descrição da Natureza"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataAmparo = conecta.rs.getString("DataNatp");
                String dia = dataAmparo.substring(8, 10);
                String mes = dataAmparo.substring(5, 7);
                String ano = dataAmparo.substring(0, 4);
                dataAmparo = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdNatp"), dataAmparo, conecta.rs.getString("StatusNatp"), conecta.rs.getString("DescricaoNatureza")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaNatureza.setModel(modelo);
        jTabelaNatureza.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaNatureza.getColumnModel().getColumn(0).setResizable(false);
        jTabelaNatureza.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaNatureza.getColumnModel().getColumn(1).setResizable(false);
        jTabelaNatureza.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaNatureza.getColumnModel().getColumn(2).setResizable(false);
        jTabelaNatureza.getColumnModel().getColumn(3).setPreferredWidth(250);
        jTabelaNatureza.getColumnModel().getColumn(3).setResizable(false);
        jTabelaNatureza.setAutoResizeMode(jTabelaNatureza.AUTO_RESIZE_OFF);
        jTabelaNatureza.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

