    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA")) {
            if (jDataLiberacao.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data de Liberação.");
            } else if (jCNPJ.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o CNPJ");
            } else if (jRazaoSocial.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe a razão social da empresa.");
            } else if (jDataValidade.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data de validade");
            } else {
                objChave.setDataLiberacao(jDataLiberacao.getDate());
                objChave.setRazaoSocial(jRazaoSocial.getText());
                objChave.setDataValidade(jDataValidade.getDate());
                pCHAVE_LIBERACAO_criptografada = jDataValidade.getDate().toString();
                objChave.setcHAVE01_liberacao(jCHAVE_liberacao.getText());
                if (acao == 1) {
                    GeraCpfCnpj gerador = new GeraCpfCnpj();
                    if (GeraCpfCnpj.isCNPJ(jCNPJ.getText()) == true) {
                        objChave.setUsuarioInsert(nameUser);
                        objChave.setDataInsert(dataModFinal);
                        objChave.setHorarioInsert(horaMov);
                        CONTROLE.incluirChaveValidacao(objChave);
                        CONTROLE.pBUSCAR_Chave(objChave);
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação    
                        bloquearCampos(!true);
                        bloquearBotoes(!true);
                        Salvar();
                        if (pRESPOSTA.equals("Sim")) {
                            JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                        } else if (pRESPOSTA.equals("Sim")) {
                            JOptionPane.showMessageDialog(rootPane, "Não foi possível gravar o registro, tente novamente.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "CNPJ invalido !!!");
                    }
                }
                if (acao == 2) {
                    objChave.setUsuarioUp(nameUser);
                    objChave.setDataUp(dataModFinal);
                    objChave.setHorarioUp(horaMov);
                    objChave.setiD_REGISTRO(Integer.valueOf(jID_REGISTRO.getText()));
                    CONTROLE.alterarChaveValidacao(objChave);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação  
                    bloquearCampos(!true);
                    bloquearBotoes(!true);
                    Salvar();
                    if (pRESPOSTA.equals("Sim")) {
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                    } else if (pRESPOSTA.equals("Sim")) {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível gravar o registro, tente novamente.");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem autorização para realiza essa operação.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

