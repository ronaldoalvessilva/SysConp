    public void LIMPAR_tabela() {
        // APAGAR DADOS DA TABELA
        while (jTabelaIntEvadidosSaidaTemporaria.getModel().getRowCount() > 0) {
            ((DefaultTableModel) jTabelaIntEvadidosSaidaTemporaria.getModel()).removeRow(0);
        }
    }

    public void PREENCHER_TABELA_todos() {
        DefaultTableModel dadosOrigem = (DefaultTableModel) jTabelaIntEvadidosSaidaTemporaria.getModel();
        try {
            for (CancelamentoEvasao dd : CONTROLE.LISTA_REGISTROS_EVADIDOS_ST_todos()) {
                if (dd.getDataSaida() != null) {
                    pDATA_saida = String.valueOf(dd.getDataSaida());
                    String dia = pDATA_saida.substring(8, 10);
                    String mes = pDATA_saida.substring(5, 7);
                    String ano = pDATA_saida.substring(0, 4);
                    pDATA_saida = dia + "/" + mes + "/" + ano;
                }
                //
                if (dd.getDataPrevRetorno() != null) {
                    pDATA_previsao = String.valueOf(dd.getDataPrevRetorno());
                    String pDia = pDATA_previsao.substring(8, 10);
                    String pMes = pDATA_previsao.substring(5, 7);
                    String pAno = pDATA_previsao.substring(0, 4);
                    pDATA_previsao = pDia + "/" + pMes + "/" + pAno;
                }
                jtotalRegistros.setText(Integer.toString(pTOTAL_evadidos));
                dadosOrigem.addRow(new Object[]{dd.getIdItem(), dd.getIdInternoCrc(), dd.getNomeInternoCrc(), dd.getIdSaida(), pDATA_saida, pDATA_previsao, dd.getNomeDestino()});                
                // BARRA DE ROLAGEM HORIZONTAL
                jTabelaIntEvadidosSaidaTemporaria.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                // ALINHAR TEXTO DA TABELA CENTRALIZADO
                DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
                centralizado.setHorizontalAlignment(SwingConstants.CENTER);
                //
                jTabelaIntEvadidosSaidaTemporaria.getColumnModel().getColumn(0).setCellRenderer(centralizado);
                jTabelaIntEvadidosSaidaTemporaria.getColumnModel().getColumn(1).setCellRenderer(centralizado);
                jTabelaIntEvadidosSaidaTemporaria.getColumnModel().getColumn(3).setCellRenderer(centralizado);
                jTabelaIntEvadidosSaidaTemporaria.getColumnModel().getColumn(4).setCellRenderer(centralizado);
                jTabelaIntEvadidosSaidaTemporaria.getColumnModel().getColumn(5).setCellRenderer(centralizado);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Algo deu errado: "+ ex);
            Logger.getLogger(TelaCancelamentoEvasao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

