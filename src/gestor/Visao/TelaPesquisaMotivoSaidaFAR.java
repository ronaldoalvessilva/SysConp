    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String tituloMotivo = "" + jTabelaMotivos.getValueAt(jTabelaMotivos.getSelectedRow(), 1);
            jPesqTituloMotivo.setText(tituloMotivo);
            String idMot = "" + jTabelaMotivos.getValueAt(jTabelaMotivos.getSelectedRow(), 0);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM MOTIVO_SAIDA_PRODUTOS_FAR WHERE TituloMotivo='" + tituloMotivo + "'");
                conecta.rs.first();
                jMotivoSaidaProdutos.setText(conecta.rs.getString("TituloMotivo"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome.\nERRO: " + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

