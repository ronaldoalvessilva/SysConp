    public void pesquisarEmpresasLaborativas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Titulo de Motivo"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdMot"), conecta.rs.getString("TituloMotivo")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaMotivos.setModel(modelo);
        jTabelaMotivos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaMotivos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaMotivos.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaMotivos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaMotivos.getTableHeader().setReorderingAllowed(false);
        jTabelaMotivos.setAutoResizeMode(jTabelaMotivos.AUTO_RESIZE_OFF);
        jTabelaMotivos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaMovitos();
        conecta.desconecta();
    }

    public void alinharCamposTabelaMovitos() {
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaMotivos.getColumnModel().getColumn(0).setCellRenderer(centralizado);
    }

    public void limparTabelaMotivos() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Titulo de Motivo"};
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaMotivos.setModel(modelo);
        jTabelaMotivos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaMotivos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaMotivos.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaMotivos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaMotivos.getTableHeader().setReorderingAllowed(false);
        jTabelaMotivos.setAutoResizeMode(jTabelaMotivos.AUTO_RESIZE_OFF);
        jTabelaMotivos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        modelo.getLinhas().clear();
    }
