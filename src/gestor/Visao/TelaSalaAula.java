    public void preencherTabelaSalas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Descrição"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            // Formatar a data no formato Brasil
            dataCadastro = conecta.rs.getString("DataCad");
            String dia = dataCadastro.substring(8, 10);
            String mes = dataCadastro.substring(5, 7);
            String ano = dataCadastro.substring(0, 4);
            dataCadastro = dia + "/" + mes + "/" + ano;
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdSala"), dataCadastro, conecta.rs.getString("StatusSala"), conecta.rs.getString("Descricao")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaSalas.setModel(modelo);
        jTabelaSalas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaSalas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaSalas.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaSalas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaSalas.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaSalas.getColumnModel().getColumn(2).setResizable(false);
        jTabelaSalas.getColumnModel().getColumn(3).setPreferredWidth(280);
        jTabelaSalas.getColumnModel().getColumn(3).setResizable(false);
        jTabelaSalas.getTableHeader().setReorderingAllowed(false);
        jTabelaSalas.setAutoResizeMode(jTabelaSalas.AUTO_RESIZE_OFF);
        jTabelaSalas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaSalas();
        conecta.desconecta();
    }

