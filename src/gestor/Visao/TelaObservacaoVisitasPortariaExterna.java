    public void buscarFotoVisita() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM ENTRADASVISITAS WHERE IdLanc='" + jIDlanc.getText() + "'");
            conecta.rs.first();
            // jObservacao.setText(conecta.rs.getString("ObsLanc"));
            jZoomObservacaoVisitasDiversas.setText(conecta.rs.getString("ObsLanc"));
            //
            jZoomObservacaoVisitasDiversas.setEnabled(!true);
            jZoomObservacaoVisitasDiversas.setLineWrap(true);
            jZoomObservacaoVisitasDiversas.setWrapStyleWord(true);
            //
        } catch (Exception e) {
        }
        conecta.desconecta();
    }
