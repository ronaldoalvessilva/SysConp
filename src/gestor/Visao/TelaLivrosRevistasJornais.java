    private void jTabelaAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAutorMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeAutor = "" + jTabelaAutor.getValueAt(jTabelaAutor.getSelectedRow(), 2);
            jNomeAutor.setText(nomeAutor);
            jBtNovoAutor.setEnabled(!true);
            jBtAlterarAutor.setEnabled(true);
            jBtExcluirAutor.setEnabled(true);
            jBtSalvarAutor.setEnabled(!true);
            jBtCancelarAutor.setEnabled(true);
            jBtAuditoriaAutor.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ITENS_AUTOR_LIVROS "
                        + "INNER JOIN LIVROS_REVISTAS_JORNAIS "
                        + "ON ITENS_AUTOR_LIVROS.IdLivro=ITENS_AUTOR_LIVROS.IdLivro "
                        + "INNER JOIN AUTORES_LIVROS "
                        + "ON ITENS_AUTOR_LIVROS.IdAutor=AUTORES_LIVROS.IdAutor "
                        + " WHERE NomeAutor='" + jNomeAutor.getText() + "'");
                conecta.rs.first();
                jIdAutor.setText(String.valueOf(conecta.rs.getInt("IdAutor")));
                idItem = conecta.rs.getInt("IdItem");
                jNomeAutor.setText(conecta.rs.getString("NomeAutor"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaAutorMouseClicked

