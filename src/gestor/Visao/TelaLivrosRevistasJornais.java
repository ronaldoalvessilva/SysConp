    private void jTabelaLivrosRevistasJornaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaLivrosRevistasJornaisMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String codLivro = "" + jTabelaLivrosRevistasJornais.getValueAt(jTabelaLivrosRevistasJornais.getSelectedRow(), 0);
            jPesqIdTitulo.setText(codLivro);
            String tituloLivro = "" + jTabelaLivrosRevistasJornais.getValueAt(jTabelaLivrosRevistasJornais.getSelectedRow(), 2);
            jPesqTitulo.setText(tituloLivro);
            //
            jBtNovo.setEnabled(!true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            jBtNovo1.setEnabled(!true);
            jBtAlterar1.setEnabled(true);
            jBtExcluir1.setEnabled(true);
            jBtSalvar1.setEnabled(!true);
            jBtCancelar1.setEnabled(true);
            //
            jBtNovoAutor.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM LIVROS_REVISTAS_JORNAIS "
                        + "INNER JOIN EDITORAS_INSTITUICAO "
                        + "ON LIVROS_REVISTAS_JORNAIS.IdForn=EDITORAS_INSTITUICAO.IdForn "
                        + "INNER JOIN CATEGORIA_LIVROS "
                        + "ON LIVROS_REVISTAS_JORNAIS.IdCat=CATEGORIA_LIVROS.IdCat "
                        + "INNER JOIN LOCAL_ACERVO "
                        + "ON LIVROS_REVISTAS_JORNAIS.IdLocal=LOCAL_ACERVO.IdLocal "
                        + "WHERE IdLivro='" + jPesqIdTitulo.getText() + "'");
                conecta.rs.first();
                jIdLivro.setText(String.valueOf(conecta.rs.getInt("IdLivro")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusLivro"));
                jComboBoxTipoLivro.setSelectedItem(conecta.rs.getString("TipoLivro"));
                jTituloLivro.setText(conecta.rs.getString("TituloLivro"));
                jEditora.setText(conecta.rs.getString("RazaoSocial"));
                jCodigoBarras.setText(conecta.rs.getString("CodigoBarra"));
                jPrazoEmprestimo.setValue(conecta.rs.getInt("PrazoEmp"));
                jComboBoxIdioma.setSelectedItem(conecta.rs.getString("Idioma"));
                jDataAquisicao.setDate(conecta.rs.getDate("DataAquisicao"));
                jCategoriaLivro.setText(conecta.rs.getString("DescricaoCategoria"));
                jISBN.setText(conecta.rs.getString("ISBN"));
                jVolume.setText(conecta.rs.getString("Volume"));
                jEdicao.setText(conecta.rs.getString("Edicao"));
                jPaginas.setText(conecta.rs.getString("Paginas"));
                jLocalArmazenamento.setText(conecta.rs.getString("DescricaoLocal"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                caminho = conecta.rs.getString("Foto1");
                if (caminho != null) {
                    javax.swing.ImageIcon b = new javax.swing.ImageIcon(caminho);
                    jFoto1.setIcon(b);
                    jFoto1.setIcon(new ImageIcon(b.getImage().getScaledInstance(jFoto1.getWidth(), jFoto1.getHeight(), Image.SCALE_DEFAULT)));
                }
                caminho1 = conecta.rs.getString("Foto2");
                if (caminho1 != null) {
                    javax.swing.ImageIcon c = new javax.swing.ImageIcon(caminho1);
                    jFoto2.setIcon(c);
                    jFoto2.setIcon(new ImageIcon(c.getImage().getScaledInstance(jFoto2.getWidth(), jFoto2.getHeight(), Image.SCALE_DEFAULT)));
                }
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
            preencherTabelaItensAutor("SELECT * FROM ITENS_AUTOR_LIVROS "
                    + "INNER JOIN AUTORES_LIVROS "
                    + "ON ITENS_AUTOR_LIVROS.IdAutor=AUTORES_LIVROS.IdAutor "
                    + "WHERE IdLivro='" + jIdLivro.getText() + "'");
        }
    }//GEN-LAST:event_jTabelaLivrosRevistasJornaisMouseClicked

