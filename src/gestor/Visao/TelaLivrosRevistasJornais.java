    public void buscarCod() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM LIVROS_REVISTAS_JORNAIS");
            conecta.rs.last();
            jIdLivro.setText(conecta.rs.getString("IdLivro"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possível obter o código do livro.");
        }
        conecta.desconecta();
    }

