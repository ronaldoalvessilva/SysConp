    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String descricaoPertence = "" + jTabelaPesqPertences.getValueAt(jTabelaPesqPertences.getSelectedRow(), 1);
            jPesqDescricaoPertence.setText(descricaoPertence);
            String idFunc = "" + jTabelaPesqPertences.getValueAt(jTabelaPesqPertences.getSelectedRow(), 0);
            jIDPertence.setText(idFunc);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM PERTENCES WHERE DescricaoPertence='" + descricaoPertence + "'");
                conecta.rs.first();               
                jIDPertence.setText(String.valueOf(conecta.rs.getInt("IdPertence")));
                jDescricaoPertence.setText(conecta.rs.getString("DescricaoPertence"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

