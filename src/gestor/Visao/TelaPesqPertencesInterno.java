//Preencher tabela com todos os COLABORADORES
    public void buscarVisitas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"ID", "    Nome do Pertence"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdPertence"), conecta.rs.getString("DescricaoPertence")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "NÃ£o existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqPertences.setModel(modelo);
        jTabelaPesqPertences.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesqPertences.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqPertences.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaPesqPertences.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqPertences.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqPertences.setAutoResizeMode(jTabelaPesqPertences.AUTO_RESIZE_OFF);
        jTabelaPesqPertences.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }
