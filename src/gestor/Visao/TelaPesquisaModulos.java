    private void jCheckBoxTodosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCheckBoxTodosItemStateChanged
        // TODO add your handling code here:
        flag = 1;
        if (evt.getStateChange() == evt.SELECTED) {
            this.preencherTabelaModulos("SELECT * FROM MODULOS "
                    + "INNER JOIN USUARIOS_MODULOS "
                    + "ON MODULOS.IdModulo=USUARIOS_MODULOS.IdModulo "
                    + "WHERE IdUsuario='" + IdUsuario.getText() + "'");
        } else {
            limparTabelaModulos();
        }
    }//GEN-LAST:event_jCheckBoxTodosItemStateChanged

    private void jTabelaModulosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaModulosMouseClicked
        // TODO add your handling code here:
        flag = 1;
        String idMod = "" + jTabelaModulos.getValueAt(jTabelaModulos.getSelectedRow(), 0);
        //        
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM MODULOS "
                    + "WHERE IdModulo='" + idMod + "'");
            conecta.rs.first();
            codigoModulo = conecta.rs.getInt("IdModulo");
            jPesqDescricaoModulo.setText(conecta.rs.getString("NomeModulo"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possível listar módulos do sistema.\nERRO: " + ex);
        }
    }//GEN-LAST:event_jTabelaModulosMouseClicked

    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        if (jPesqDescricaoModulo.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "É necessário selecionar o nome do módulo.");
        } else {
            flag = 1;            
            if (flag == 1) {
                String nomeModulo = "" + jTabelaModulos.getValueAt(jTabelaModulos.getSelectedRow(), 1);
                jPesqDescricaoModulo.setText(nomeModulo);
                String idModulo = "" + jTabelaModulos.getValueAt(jTabelaModulos.getSelectedRow(), 0);
                conecta.abrirConexao();
                try {
                    conecta.executaSQL("SELECT * FROM  MODULOS "
                            + "WHERE IdModulo='" + idModulo + "'");
                    conecta.rs.first();
                    jIdModulo.setText(conecta.rs.getString("IdModulo"));
                    jComboBoxModuloAcesso.setText(conecta.rs.getString("NomeModulo"));
                    conecta.desconecta();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados." + ex);
                }
                pesquisarTelaAcesso();
                dispose();
            }
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

