    private void jBtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtConfirmarActionPerformed
        // TODO add your handling code here:
        if (jComboBoxTipoEmpresa.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe um tipo de empresa para o relatório");
        } else {
            if (jComboBoxTipoEmpresa.getSelectedItem().equals("Interna") && jComboBoxStatusInterno.getSelectedItem().equals("Ativo")) {
                listaEmpresaInternaAtiva();
            } else {
                if (jComboBoxTipoEmpresa.getSelectedItem().equals("Interna") && jComboBoxStatusInterno.getSelectedItem().equals("Inativo")) {

                } else {
                    if (jComboBoxTipoEmpresa.getSelectedItem().equals("Externa") && jComboBoxStatusInterno.getSelectedItem().equals("Ativo")) {
                        listaEmpresaExterna();
                    } else {
                        if (jComboBoxTipoEmpresa.getSelectedItem().equals("Externa") && jComboBoxStatusInterno.getSelectedItem().equals("Inativa")) {
                            listaEmpresaExterna();
                        } else {
                            if (jComboBoxTipoEmpresa.getSelectedItem().equals("Ambas") && jComboBoxStatusInterno.getSelectedItem().equals("Ativo")) {
                                listaEmpresaAmbasInternoAtivo();
                            } else {
                                if (jComboBoxTipoEmpresa.getSelectedItem().equals("Ambas") && jComboBoxStatusInterno.getSelectedItem().equals("Inativo")) {
                                    listaEmpresaAmbasInternoInativo();
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_jBtConfirmarActionPerformed

