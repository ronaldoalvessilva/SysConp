    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        objCampos.setNomeUsuario(nameUser);
        objCampos.setNomeTelaAcesso(telaEquipamentosEPI);
        pPESQUISAR_acessos.pesquisarUsuario(objCampos);
        pPESQUISAR_acessos.pesquisarGrupoUsuario(objCampos);
        pPESQUISAR_acessos.pesquisarTelasAcesso(objCampos);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || objCampos.getNomeGrupo().equals("ADMINISTRADORES") || objCampos.getCodigoUsuario() == objCampos.getCodigoUsuarioAcesso() && objCampos.getNomeTelaAcesso().equals(telaEquipamentosEPI) && objCampos.getCodigoGravar() == 1) {
            DecimalFormat valorReal = new DecimalFormat("###,##00.0");
            valorReal.setCurrency(Currency.getInstance(new Locale("pt", "BR")));
            if (jComboBoxStatusEquipamento.getSelectedItem().equals("Selecione...")) {
                JOptionPane.showMessageDialog(rootPane, "Selecione o status do registro.");
            } else if (jDataCadastroEquipamento.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data do cadastro.");
            } else if (jDescricaoEquipamento.getText().equals("Selecione...")) {
                JOptionPane.showMessageDialog(rootPane, "Informe a descrição do grupo de armas.");
            } else {
                objEquipa.setFotoEquipamento(persona_imagem);
                objEquipa.setStatusEquipamento((String) jComboBoxStatusEquipamento.getSelectedItem());
                objEquipa.setDataCadastroEquipamento(jDataCadastroEquipamento.getDate());
                objEquipa.setNumeroCodigoBarras(jNumeroCodigoBarras.getText());
                objEquipa.setDescricaoEquipamento(jDescricaoEquipamento.getText());
                objEquipa.setMarcaEquipamento(jMarcaEquipamento.getText());
                objEquipa.setModeloEquipamento(jModeloEquipamento.getText());
                objEquipa.setComprimentoEquipamento(jComprimentoEquipamento.getText());
                objEquipa.setTipoMaterialEquipamento(jTipoMaterialEquipamento.getText());
                try {
                    objEquipa.setPesoEquipamento(valorReal.parse(jPesoEquipamento.getText()).floatValue());
                } catch (ParseException ex) {
                    Logger.getLogger(TelaEquipamentosEPI.class.getName()).log(Level.SEVERE, null, ex);
                }
                objEquipa.setCorEquipamento(jCorEquipamento.getText());
                objEquipa.setObservacao(jObservacaoEquipamento.getText());
                if (acao == 1) {
                    objEquipa.setUsuarioInsert(nameUser);
                    objEquipa.setDataInsert(dataModFinal);
                    objEquipa.setHorarioInsert(horaMov);
                    CONTROL.incluirEQUIPAMENTO_epi(objEquipa);
                    bloquearBotoes(!true);
                    bloquearCampos(!true);
                    Salvar();
                    pBUSCAR_codigo();
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    if (pRESPOSTA_epi.equals("Sim")) {
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível gravar o registro, tente novamente.");
                    }
                }
                if (acao == 2) {
                    objEquipa.setUsuarioUp(nameUser);
                    objEquipa.setDataUp(dataModFinal);
                    objEquipa.setHorarioUp(horaMov);
                    objEquipa.setIdEquipamento(Integer.valueOf(jCodigoEquipamento.getText()));
                    CONTROL.alterarEQUIPAMENTO_epi(objEquipa);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    bloquearBotoes(!true);
                    bloquearCampos(!true);
                    Salvar();
                    if (pRESPOSTA_epi.equals("Sim")) {
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível gravar o registro, tente novameente.");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem acesso ao registro.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

