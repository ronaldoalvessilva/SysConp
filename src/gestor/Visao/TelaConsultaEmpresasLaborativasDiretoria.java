    public void preencherTodosEmpresas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"ID", "    Nome Empresa", " Nome Fantasia", "  Data", " CNPJ da Empresa", "    Contato"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                // Formatar a data no formato Brasil
                dataCadastro = conecta.rs.getString("DataCad");
                String dia = dataCadastro.substring(8, 10);
                String mes = dataCadastro.substring(5, 7);
                String ano = dataCadastro.substring(0, 4);
                dataCadastro = dia + "/" + mes + "/" + ano;
                dados.add(new Object[]{conecta.rs.getInt("IdEmp"), conecta.rs.getString("RazaoSocial"), conecta.rs.getString("NomeFantasia"), dataCadastro, conecta.rs.getString("Cnpj"), conecta.rs.getString("Contato")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaEmpresas.setModel(modelo);
        jTabelaEmpresas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaEmpresas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaEmpresas.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaEmpresas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaEmpresas.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTabelaEmpresas.getColumnModel().getColumn(2).setResizable(false);
        jTabelaEmpresas.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTabelaEmpresas.getColumnModel().getColumn(3).setResizable(false);
        jTabelaEmpresas.getColumnModel().getColumn(4).setPreferredWidth(120);
        jTabelaEmpresas.getColumnModel().getColumn(4).setResizable(false);
        jTabelaEmpresas.getColumnModel().getColumn(5).setPreferredWidth(200);
        jTabelaEmpresas.getColumnModel().getColumn(5).setResizable(false);
        jTabelaEmpresas.getTableHeader().setReorderingAllowed(false);
        jTabelaEmpresas.setAutoResizeMode(jTabelaEmpresas.AUTO_RESIZE_OFF);
        jTabelaEmpresas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }

