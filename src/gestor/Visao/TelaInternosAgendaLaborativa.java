    private void jTabelaInternoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaInternoMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String idInterno = "" + jTabelaInterno.getValueAt(jTabelaInterno.getSelectedRow(), 0);
            jIdInternoAgenda.setText(idInterno);
            String nomeInterno = "" + jTabelaInterno.getValueAt(jTabelaInterno.getSelectedRow(), 2);
            jNomeInternoAgenda.setText(nomeInterno);
            //           
            jBtSalvar.setEnabled(true);
            try {
                conecta.abrirConexao();
                conecta.executaSQL("SELECT * FROM PRONTUARIOSCRC WHERE IdInternoCrc='" + idInterno + "'");
                conecta.rs.first();
                jIdInternoAgenda.setText(conecta.rs.getString("IdInternoCrc"));
                jNomeInternoAgenda.setText(conecta.rs.getString("NomeInternoCrc"));
            } catch (Exception e) {
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaInternoMouseClicked

