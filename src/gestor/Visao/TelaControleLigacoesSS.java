    public void preencherTodasLigacoes(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data ", "Nome do Interno", "Telefone"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataLigacao = conecta.rs.getString("DataControl");
                String dia = dataLigacao.substring(8, 10);
                String mes = dataLigacao.substring(5, 7);
                String ano = dataLigacao.substring(0, 4);
                dataLigacao = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdControl"), dataLigacao, conecta.rs.getString("NomeInternoCrc"), conecta.rs.getString("TelefoneControl")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaControle.setModel(modelo);
        jTabelaControle.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaControle.getColumnModel().getColumn(0).setResizable(false);
        jTabelaControle.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaControle.getColumnModel().getColumn(1).setResizable(false);
        jTabelaControle.getColumnModel().getColumn(2).setPreferredWidth(300);
        jTabelaControle.getColumnModel().getColumn(2).setResizable(false);
        jTabelaControle.getColumnModel().getColumn(3).setPreferredWidth(120);
        jTabelaControle.getColumnModel().getColumn(3).setResizable(false);
        jTabelaControle.getTableHeader().setReorderingAllowed(false);
        jTabelaControle.setAutoResizeMode(jTabelaControle.AUTO_RESIZE_OFF);
        jTabelaControle.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

