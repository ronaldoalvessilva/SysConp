    private void jTabelaAmparoLegalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAmparoLegalMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaAmparoLegal.getValueAt(jTabelaAmparoLegal.getSelectedRow(), 0);
            jPesqCodigo.setText(IdLanc);
            //
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM AMPARO_LEGAL "
                        + "WHERE IdLanc='" + IdLanc + "'");
                conecta.rs.first();
                jCodigoAmparo.setText(String.valueOf(conecta.rs.getInt("IdLanc")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusLanc"));
                jDataAmparoLegal.setDate(conecta.rs.getDate("DataLanc"));
                jDescricaoAmparoLegal.setText(conecta.rs.getString("DescricaoAmparoLegal"));
                jArtigo.setText(conecta.rs.getString("Artigo"));
                jParagrafo.setText(conecta.rs.getString("Paragrafo"));
                jInciso.setText(conecta.rs.getString("Inciso"));
                jAlinea.setText(conecta.rs.getString("Alinea"));
                jTextoObservacao.setText(conecta.rs.getString("Texto"));
                //
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
        }
    }//GEN-LAST:event_jTabelaAmparoLegalMouseClicked

