    private void jTabelaTodosRecadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaTodosRecadosMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeUsuario = "" + jTabelaTodosRecados.getValueAt(jTabelaTodosRecados.getSelectedRow(), 3);
            jPesqDestinatario.setText(nomeUsuario);
            String idLanc = "" + jTabelaTodosRecados.getValueAt(jTabelaTodosRecados.getSelectedRow(), 0);
            jIDLanc.setText(idLanc);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtResponder.setEnabled(true);
            jBtConfirmar.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM AGENDARECADOS INNER JOIN USUARIOS ON AGENDARECADOS.IdUsuario=USUARIOS.IdUsuario WHERE NomeUsuario='" + nameUser + "' AND IdLanc='" + jIDLanc.getText() + "'OR NomeUsuarioLogado='" + nameUser + "'AND IdLanc='" + jIDLanc.getText() + "'");
                conecta.rs.first();
                jIDLanc.setText(String.valueOf(conecta.rs.getInt("IdLanc")));
                jDataLanc.setDate(conecta.rs.getDate("DataLanc"));
                jHoraRecado.setText(conecta.rs.getString("Horario"));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusAgenda"));
                jNomeRementente.setText(conecta.rs.getString("NomeUsuarioLogado"));
                jNomeDestinatario.setText(conecta.rs.getString("NomeUsuario"));
                jRecado.setText(conecta.rs.getString("Recados"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaTodosRecadosMouseClicked

