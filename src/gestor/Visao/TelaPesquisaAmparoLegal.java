    public void preencherTabelaAmparo(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Amparo Legal", "Artigo", "Parágrafo", "Inciso", "Alinea"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdLanc"), conecta.rs.getString("StatusLanc"), conecta.rs.getString("DescricaoAmparoLegal"), conecta.rs.getString("Artigo"), conecta.rs.getString("Paragrafo"), conecta.rs.getString("Inciso"), conecta.rs.getString("Alinea")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAmparoLegal.setModel(modelo);
        jTabelaAmparoLegal.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelaAmparoLegal.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaAmparoLegal.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(2).setPreferredWidth(250);
        jTabelaAmparoLegal.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(3).setPreferredWidth(50);
        jTabelaAmparoLegal.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTabelaAmparoLegal.getColumnModel().getColumn(4).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(5).setPreferredWidth(50);
        jTabelaAmparoLegal.getColumnModel().getColumn(5).setResizable(false);
        jTabelaAmparoLegal.getColumnModel().getColumn(6).setPreferredWidth(50);
        jTabelaAmparoLegal.getColumnModel().getColumn(6).setResizable(false);
        jTabelaAmparoLegal.getTableHeader().setReorderingAllowed(false);
        jTabelaAmparoLegal.setAutoResizeMode(jTabelaAmparoLegal.AUTO_RESIZE_OFF);
        jTabelaAmparoLegal.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

