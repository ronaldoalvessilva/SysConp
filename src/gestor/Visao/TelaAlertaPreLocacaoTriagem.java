    public void preencherTabelaEntradasTriagem() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Data Entrada", "Código", "Nome do Interno", "Pavilhão"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM ITENS_EXPORTADO_LOCACAO_INTERNOS "
                    + "INNER JOIN PRE_LOCACAO_INTERNOS "
                    + "ON ITENS_EXPORTADO_LOCACAO_INTERNOS.CodigoReg=PRE_LOCACAO_INTERNOS.CodigoReg "
                    + "INNER JOIN PRONTUARIOSCRC "
                    + "ON ITENS_EXPORTADO_LOCACAO_INTERNOS.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                    + "INNER JOIN PAVILHAO "
                    + "ON ITENS_EXPORTADO_LOCACAO_INTERNOS.IdPav=PAVILHAO.IdPav "
                    + "WHERE ITENS_EXPORTADO_LOCACAO_INTERNOS.ConfirmaBase='" + confirmadoEntrada + "'");                    
            conecta.rs.first();
            count = 0;
            do {
                count = count + 1;
                // Formatar a data Entrada
                dataEntrada = conecta.rs.getString("DataReg");
                String diae = dataEntrada.substring(8, 10);
                String mese = dataEntrada.substring(5, 7);
                String anoe = dataEntrada.substring(0, 4);
                dataEntrada = diae + "/" + mese + "/" + anoe;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{dataEntrada, conecta.rs.getInt("IdInternoCrc"), conecta.rs.getString("NomeInternoCrc"), conecta.rs.getString("DescricaoPav")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaEntradaInternosTriagem.setModel(modelo);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(0).setResizable(false);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(1).setResizable(false);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(2).setPreferredWidth(300);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(2).setResizable(false);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(3).setResizable(false);
        jTabelaEntradaInternosTriagem.getTableHeader().setReorderingAllowed(false);
        jTabelaEntradaInternosTriagem.setAutoResizeMode(jTabelaEntradaInternosTriagem.AUTO_RESIZE_OFF);
        jTabelaEntradaInternosTriagem.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

    public void alinharCamposTabela() {
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        jTabelaEntradaInternosTriagem.getColumnModel().getColumn(1).setCellRenderer(centralizado);
    }
