        jTabelaInstituicao.getColumnModel().getColumn(0).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaInstituicao.getColumnModel().getColumn(1).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaInstituicao.getColumnModel().getColumn(2).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(3).setPreferredWidth(320);
        jTabelaInstituicao.getColumnModel().getColumn(3).setResizable(false);
        jTabelaInstituicao.getTableHeader().setReorderingAllowed(false);
        jTabelaInstituicao.setAutoResizeMode(jTabelaInstituicao.AUTO_RESIZE_OFF);
        jTabelaInstituicao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaMatriculas();
        conecta.desconecta();
