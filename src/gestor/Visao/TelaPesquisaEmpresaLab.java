    public void pesquisarEmpresasLaborativas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"ID", "Razao Social"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdEmp"), conecta.rs.getString("RazaoSocial")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqEmpresas.setModel(modelo);
        jTabelaPesqEmpresas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesqEmpresas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqEmpresas.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaPesqEmpresas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqEmpresas.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqEmpresas.setAutoResizeMode(jTabelaPesqEmpresas.AUTO_RESIZE_OFF);
        jTabelaPesqEmpresas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }
