    public void buscarCodFunc() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM FUNCIONARIOS");
            conecta.rs.first();
            codFunc = conecta.rs.getInt("IdFunc");
            objCola.setIdFunc(codFunc);
            conecta.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possivel encontrar COLABORADOR \nERRO: " + ex);
        }
    }

