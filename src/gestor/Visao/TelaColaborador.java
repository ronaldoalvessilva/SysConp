    private void jBtSalvarDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarDepActionPerformed
        // TODO add your handling code here:
        if (jNomeDepen1.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nome do dependene não pode ser em branco");
            jNomeDepen1.requestFocus();
        } else {
            if (jDataNascd1.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Data de nascimento do dependente não pode ser em branco");
                jDataNascd1.requestFocus();
            } else {
                if (jComboBoxpa1.getSelectedItem() == null) {
                    JOptionPane.showMessageDialog(rootPane, "Parenesco do dependene não pode ser em banco.");
                    jComboBoxpa1.requestFocus();
                } else {
                    objDepen.setNomeDep(jNomeDepen1.getText());
                    objDepen.setDataNascDep(jDataNascd1.getDate());
                    objDepen.setParenteDep((String) jComboBoxpa1.getSelectedItem());
                    objCola.setIdFunc(Integer.parseInt(jIdFunc.getText()));
                    buscarCodFunc();
                    // Se a ação for 1 (incluir)
                    if (acao == 1) {
                        conecta.abrirConexao();
                        try {
                            PreparedStatement pst = conecta.con.prepareStatement("INSERT INTO DEPENDENTES (NomeDep,DataNascDep,ParenteDep,IdFunc)"
                                    + " VALUES(?,?,?,?)");
                            pst.setString(1, objDepen.getNomeDep());
                            pst.setTimestamp(2, new java.sql.Timestamp(objDepen.getDataNascDep().getTime()));
                            pst.setString(3, objDepen.getParenteDep());
                            buscarFunc(objCola.getIdFunc());
                            pst.setInt(4, codFunc);
                            pst.execute();
                            JOptionPane.showMessageDialog(rootPane, "DEPENDENTE Incluido com sucesso!!!");
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, "Não Foi possivel INSERIR os Dados dos DEPENDENTES\n\nERRO" + ex);
                        }
                        // TRAZENDO ERRADO O DEPENDENTE NA CONSULTA
                        preencherTabelaDepende("SELECT * FROM DEPENDENTES WHERE IdFunc='" + codFunc + "'");
                        SalvarDependente();
                    }
                    // Se a acao for 2 (Alterar)
                    if (acao == 2) {
                        objCola.setIdFunc(Integer.parseInt(jIdFunc.getText()));
                        objDepen.setNomeDep(jNomeDepen1.getText());
                        buscarCodFunc();
                        conecta.abrirConexao();
                        try {
                            PreparedStatement pst = conecta.con.prepareStatement("UPDATE DEPENDENTES SET NomeDep=?,DataNascDep=?,ParenteDep=?,IdFunc=? WHERE IdFunc='" + jIdFunc.getText() + "' AND NomeDep='" + jNomeDepen1.getText() + "'");
                            pst.setString(1, objDepen.getNomeDep());
                            pst.setTimestamp(2, new java.sql.Timestamp(objDepen.getDataNascDep().getTime()));
                            pst.setString(3, objDepen.getParenteDep());
                            buscarFunc(objCola.getIdFunc());
                            pst.setInt(4, codFunc);
                            pst.executeUpdate();
                            JOptionPane.showMessageDialog(rootPane, "DEPENDENTE Alterardo com sucesso!!!");
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, "Não Foi possivel ALTERAR os Dados dos DEPENDENTES\nERRO" + ex);
                        }
                        // TRAZENDO ERRADO O DEPENDENTE NA CONSULTA
                        preencherTabelaDepende("SELECT * FROM DEPENDENTES WHERE IdFunc='" + codFunc + "'");
                        SalvarDependente();
                    }
                }
            }
        }
    }//GEN-LAST:event_jBtSalvarDepActionPerformed

