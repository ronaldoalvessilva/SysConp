    public void teclaEnter() {
        // Substituir tecla TAB por ENTER no JInternalFrame
        Action doEnterKeyPress = new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                KeyboardFocusManager.getCurrentKeyboardFocusManager().getFocusOwner().transferFocus();
            }
        };
        this.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke("released ENTER"),
                "doEnter");
        this.getActionMap().put("doEnter", doEnterKeyPress);
    }

    public void excluirDepenIndividual() {
        try {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir DEPENDENTE selecionado?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                conecta.abrirConexao();
                conecta.rs.first();
                PreparedStatement pst;
                pst = conecta.con.prepareStatement("DELETE FROM DEPENDENTES WHERE NomeDep='" + objDepen.getNomeDep() + "'");
                //  pst.setString(1, objDepen.getNomeDep());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Exclusão do DEPENDENTE com sucesso!!");
                conecta.desconecta();
                jNomeDepen1.setText("");
                jDataNascd1.setDate(null);
                jComboBoxpa1.setSelectedItem(null);
                jBtNovoDep.setEnabled(true);
                jBtAlterarDep.setEnabled(!true);
                jBtExcluirDep.setEnabled(!true);
                jBtSalvarDep.setEnabled(!true);
                jBtSair.setEnabled(true);
                jBtCancelarDep.setEnabled(true);
                // desabilitar os campos                
                jNomeDepen1.setEnabled(!true);
                jDataNascd1.setEnabled(!true);
                jComboBoxpa1.setEnabled(!true);
                conecta.desconecta();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel excluir o DEPENDENTE!!\n" + ex);
            conecta.desconecta();
        }
    }
