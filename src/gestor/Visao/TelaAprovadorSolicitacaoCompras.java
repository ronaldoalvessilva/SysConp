    private void jTabelaAprovadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAprovadoresMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaAprovadores.getValueAt(jTabelaAprovadores.getSelectedRow(), 0);
            jIdPesqFunc.setText(IdLanc);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM APROVADOR_SOLICITACAO_COMPRAS_AC "
                        + "INNER JOIN COLABORADOR "
                        + "ON APROVADOR_SOLICITACAO_COMPRAS_AC.IdFunc=COLABORADOR.IdFunc "
                        + "INNER JOIN DEPARTAMENTOS "
                        + "ON COLABORADOR.IdDepartamento=DEPARTAMENTOS.IdDepartamento "
                        + "WHERE IdFuncAprova='" + IdLanc + "'");
                conecta.rs.first();
                jCodAprovador.setText(String.valueOf(conecta.rs.getInt("IdFuncAprova")));
                jComboBoxStatusFunc.setSelectedItem(conecta.rs.getString("StatusAprova"));
                jComboBoxTipoLiberacao.setSelectedItem(conecta.rs.getString("TipoAprova"));
                jCodigoFunc.setText(conecta.rs.getString("IdFunc"));
                jNomeColaborador.setText(conecta.rs.getString("NomeFunc"));
                jNomeDepartamento.setText(conecta.rs.getString("NomeDepartamento"));
                // Formata o valor para ser exibido na tela no formato BR
                valorSolicitacao = conecta.rs.getFloat("ValorSolicita");
                DecimalFormat vs = new DecimalFormat("#,##0.00");
                String vlSolicitacao = vs.format(valorSolicitacao);
                jValorSolicitacao.setText(vlSolicitacao);
                //
                valorSaldoSolicitacao = conecta.rs.getFloat("ValorSaldoSolicita");
                DecimalFormat vss = new DecimalFormat("#,##0.00");
                String vsaldoSolic = vss.format(valorSaldoSolicitacao);
                jValorSaldoSolicitacao.setText(vsaldoSolic);
                //
                valorPedido = conecta.rs.getFloat("ValorPedido");
                DecimalFormat vp = new DecimalFormat("#,##0.00");
                String vlPedido = vp.format(valorPedido);
                jValorPedido.setText(vlPedido);
                //
                valorSaldoPedido = conecta.rs.getFloat("ValorSaldoPedido");
                DecimalFormat vsp = new DecimalFormat("#,##0.00");
                String vsaldoPedido = vsp.format(valorSaldoPedido);
                jValorSaldoPedido.setText(vsaldoPedido);
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível exibir registro.\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaAprovadoresMouseClicked

