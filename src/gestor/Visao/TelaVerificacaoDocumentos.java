    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        verificarRegistro();
        if (jIDVisita.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "É necessário gravar o registro da visita primeiro.");
        } else if (jIdInternoVisita.getText().equals("") || jNomeInternoVisita.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe qual é o nome do interno para essa visita.");
        } else {
            if (jCheckBoxIdentifica.isSelected()) {
                iden = 1;
            } else if (!jCheckBoxIdentifica.isSelected()) {
                iden = 0;
            }
            objVerifica.setIdent(iden);
            if (jCheckBoxFotosRecentes.isSelected()) {
                duas = 1;
            } else if (!jCheckBoxFotosRecentes.isSelected()) {
                duas = 0;
            }
            objVerifica.setdFoto(duas);
            if (jCheckBoxAntecedentes.isSelected()) {
                ante = 1;
            } else if (!jCheckBoxAntecedentes.isSelected()) {
                ante = 0;
            }
            objVerifica.setAnt(ante);
            if (jCheckBoxComprovante.isSelected()) {
                comp = 1;
            } else if (!jCheckBoxComprovante.isSelected()) {
                comp = 0;
            }
            objVerifica.setComp(comp);
            if (jCheckBoxDeclaracao.isSelected()) {
                decla = 1;
            } else if (!jCheckBoxDeclaracao.isSelected()) {
                decla = 0;
            }
            objVerifica.setDecla(decla);
            if (jCheckBoxAutorizacao.isSelected()) {
                auto = 1;
            } else if (!jCheckBoxAutorizacao.isSelected()) {
                auto = 0;
            }
            objVerifica.setAutori(auto);
            objVerifica.setColaboradorResponsavel(jColaboradorResponsavel.getText());
            objVerifica.setJustificativa(jJustificativa.getText());
            objVerifica.setAprova((String) jComboBoxAprovacao.getSelectedItem());
            if (jCheckBoxAtestado.isSelected()) {
                atesta = 1;
            } else if (!jCheckBoxAtestado.isSelected()) {
                atesta = 0;
            }
            objVerifica.setAtes(atesta);
            if (jCheckBoxVisitaAprovada.isSelected()) {
                visapro = 1;
            } else if (!jCheckBoxVisitaAprovada.isSelected()) {
                visapro = 0;
            }
            objVerifica.setVisApr(visapro);
            if (jCheckBoxVisitaReprovada.isSelected()) {
                visarepro = 1;
            } else if (!jCheckBoxVisitaReprovada.isSelected()) {
                visarepro = 0;
            }
            objVerifica.setVisRep(visarepro);
            objVerifica.setIdVisita(Integer.valueOf(jIDVisita.getText()));
            objVerifica.setNomeVisita(jNomeVisita.getText());
            objVerifica.setIdinternoCrc(Integer.valueOf(jIdInternoVisita.getText()));
            objVerifica.setNomeInternooCrc(jNomeInternoVisita.getText());
            if (acao == 1) {
                if (jIDVisita.getText().equals(codigoVisita)) {
                    JOptionPane.showMessageDialog(rootPane, "Já foi inserido um registro para esse visitante.");
                } else {
                    objVerifica.setUsuarioInsert(nameUser);
                    objVerifica.setDataInsert(dataModFinal);
                    objVerifica.setHorarioInsert(horaMov);
                    control.incluirDocumentos(objVerifica);
                    buscarCodigo();
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    Salvar();
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
            }
            if (acao == 2) {
                objVerifica.setUsuarioUp(nameUser);
                objVerifica.setDataUp(dataModFinal);
                objVerifica.setHorarioUp(horaMov);
                objVerifica.setIdVerDoc(codigoVerifica);
                //               
                objVerifica.setIdVisita(Integer.valueOf(jIDVisita.getText()));
                control.alterarDocumentos(objVerifica);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Salvar();
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

