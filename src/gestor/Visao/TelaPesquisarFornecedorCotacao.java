    private void jCheckBoxTodosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCheckBoxTodosItemStateChanged
        // TODO add your handling code here:
        flag = 1;
        if (evt.getStateChange() == evt.SELECTED) {
            jTabelaFornecedores.setVisible(true);
            this.pesquisarTabelaDoencas("SELECT * FROM FORNECEDORES_COMPRAS ORDER BY RazaoSocial");
        } else {
            limparTabelaFornecedor();
        }
    }//GEN-LAST:event_jCheckBoxTodosItemStateChanged

    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaFornecedores.getValueAt(jTabelaFornecedores.getSelectedRow(), 0);
            jPesqDescricao.setText(IdLanc);            
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM FORNECEDORES_COMPRAS WHERE IdForn ='" + IdLanc + "'");
                conecta.rs.first();
                jCodFornecedor.setText(conecta.rs.getString("IdForn"));
                jDescricaoFornecedor.setText(conecta.rs.getString("RazaoSocial"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

