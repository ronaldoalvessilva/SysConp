    public void buscarCelasPaviloes(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição Cela", "Descrição Pavilhão"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdCela"), conecta.rs.getString("EndCelaPav"), conecta.rs.getString("DescricaoPav")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqCelasPavilhao.setModel(modelo);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(1).setPreferredWidth(170);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(2).setResizable(false);
        jTabelaPesqCelasPavilhao.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqCelasPavilhao.setAutoResizeMode(jTabelaPesqCelasPavilhao.AUTO_RESIZE_OFF);
        jTabelaPesqCelasPavilhao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaInternos();
        conecta.desconecta();
    }
     public void alinharCamposTabelaInternos() {
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaPesqCelasPavilhao.getColumnModel().getColumn(0).setCellRenderer(centralizado);
    }
