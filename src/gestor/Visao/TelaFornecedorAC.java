    private void jTabelaFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaFornecedorMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeForn = "" + jTabelaFornecedor.getValueAt(jTabelaFornecedor.getSelectedRow(), 1);
            jPesqNomeFornecedor.setText(nomeForn);
            jBtNovo.setEnabled(!true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM FORNECEDORES_AC WHERE RazaoSocial='" + jPesqNomeFornecedor.getText() + "'");
                conecta.rs.first();
                jIdFornecedor.setText(String.valueOf(conecta.rs.getInt("IdForn")));
                jRazaoSocial.setText(conecta.rs.getString("RazaoSocial"));
                jComboBoxClass.setSelectedItem(conecta.rs.getString("ClassFor"));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusFor"));
                jCNPJ.setText(conecta.rs.getString("Cnpj"));
                jInsEsta.setText(conecta.rs.getString("InsEstadual"));
                jTelefone.setText(conecta.rs.getString("Telefone"));
                jTelefone1.setText(conecta.rs.getString("Telefone1"));
                jCelular.setText(conecta.rs.getString("Celular"));
                jEmail.setText(conecta.rs.getString("Email"));
                jFax.setText(conecta.rs.getString("Fax"));
                jEndereco.setText(conecta.rs.getString("Endereco"));
                jCompl.setText(conecta.rs.getString("Compl"));
                jCep.setText(conecta.rs.getString("Cep"));
                jCidade.setText(conecta.rs.getString("Cidade"));
                jComboBoxEstado.setSelectedItem(conecta.rs.getString("Estado"));
                jEndCob.setText(conecta.rs.getString("EnderecoCob"));
                jComplCob.setText(conecta.rs.getString("ComplCob"));
                jCepCob.setText(conecta.rs.getString("CepCob"));
                jCidadeCob.setText(conecta.rs.getString("CidadeCob"));
                jComboBoxEstadoCob.setSelectedItem(conecta.rs.getString("EstadoCob"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaFornecedorMouseClicked

