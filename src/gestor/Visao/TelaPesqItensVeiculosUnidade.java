                conecta.executaSQL("SELECT * FROM COLABORADOR INNER JOIN DEPARTAMENTOS ON COLABORADOR.IdDepartamento=DEPARTAMENTOS.IdDepartamento INNER JOIN ITENSVEICULOSUNIDADE ON COLABORADOR.IdFunc=ITENSVEICULOSUNIDADE.IdFunc INNER JOIN VEICULOS ON ITENSVEICULOSUNIDADE.IdVeiculo=VEICULOS.IdVeiculo WHERE NomeFunc LIKE'" + jPesqNomeVisita.getText() + "%' AND IdLanc='" + jIdLanc.getText() + "'AND HorarioEntrada='" + jHorarioEntrada.getText() + "'");
