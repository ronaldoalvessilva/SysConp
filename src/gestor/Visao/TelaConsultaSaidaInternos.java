    private void jBtDataLanc1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtDataLanc1ActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (jDataPesqInicial.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data inicial para pesquisa.");
            jDataPesqInicial.requestFocus();
        } else {
            if (jDataPesFinal.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data final para pesquisa.");
                jDataPesFinal.requestFocus();
            } else {
                if (jDataPesqInicial.getDate().after(jDataPesFinal.getDate())) {
                    JOptionPane.showMessageDialog(rootPane, "Data Inicial não pode ser maior que data final");
                } else {
                    SimpleDateFormat formatoAmerica = new SimpleDateFormat("dd/MM/yyyy");
                    dataInicial = formatoAmerica.format(jDataPesqInicial.getDate().getTime());
                    dataFinal = formatoAmerica.format(jDataPesFinal.getDate().getTime());
                    jTabelaSaidaInterno.setVisible(true);
                    pesquisarLancCod("SELECT * FROM SAIDACRC INNER JOIN OPERACAO ON SAIDACRC.IdOp = OPERACAO.IdOp WHERE DataLancaMov BETWEEN'" + dataInicial + "'AND '" + dataFinal + "'");
                }
            }
        }
    }//GEN-LAST:event_jBtDataLanc1ActionPerformed

