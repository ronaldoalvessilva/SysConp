    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        Integer rows = jTabelaFornecedor.getModel().getRowCount();
        if (jDataCotacao.getDate() == null) {
            jDataCotacao.requestFocus();
            jDataCotacao.setBackground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Informe a data da Cotação.");
        } else if (jComboBoxDepartamento.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe o nome do departamento.");
        } else if (jComboBoxCentroCusto.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe o Centro de Custo.");
        } else if (rows == 0) {
            JOptionPane.showMessageDialog(rootPane, "Informe o(s) fornecedor(es) para cotação.");
        } else {
            objCotacao.setStatusCota(jStatusCotacao.getText());
            objCotacao.setDataCotacao(jDataCotacao.getDate());
            objCotacao.setDescricaoDepartamento((String) jComboBoxDepartamento.getSelectedItem());
            objCotacao.setDescricaoCentroCusto((String) jComboBoxCentroCusto.getSelectedItem());
            objCotacao.setTipoCotacao((String) jComboBoxTipoCotacao.getSelectedItem());
            objCotacao.setDescricaoFornecedor(jDescricaoFornecedor.getText());
            objCotacao.setFreteCotacao((String) jComboBoxFrete.getSelectedItem());
            if (acao == 1) {
                objCotacao.setUsuarioInsert(nameUser);
                objCotacao.setDataInsert(dataModFinal);
                objCotacao.setHorarioInsert(horaMov);
                //
                control.incluirCotacaoCompras(objCotacao);
                buscarCodigo();
                // ADICIONAR NA TABELA FORNECEDORES_COTACAO_COMPRAS OS FORNECEDORES DA COTAÇÃO
                incluirFornecedor();
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
            if (acao == 2) {
                objCotacao.setUsuarioUp(nameUser);
                objCotacao.setDataUp(dataModFinal);
                objCotacao.setHorarioUp(horaMov);
                //
                objCotacao.setIdCota(Integer.valueOf(jCodigoCota.getText()));
                control.alterarCotacaoCompras(objCotacao);
                // EXCLUI OS FORNECEDORES PARA SEREM ADICIONADOS LOGO EM SEGUIDA              
                objCotacao.setIdCota(Integer.valueOf(jCodigoCota.getText()));
                controle.excluirForncedorCotacao(objCotacao);
                // ADICIONANDO OS NOVOS FORNECEDORES
                incluirFornecedor();
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

