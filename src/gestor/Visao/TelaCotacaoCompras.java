        jTabelaItensCotacao.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTabelaItensCotacao.getColumnModel().getColumn(2).setResizable(false);
        jTabelaItensCotacao.getColumnModel().getColumn(3).setPreferredWidth(280);
        jTabelaItensCotacao.getColumnModel().getColumn(3).setResizable(false);
        jTabelaItensCotacao.getColumnModel().getColumn(4).setPreferredWidth(70);
        jTabelaItensCotacao.getColumnModel().getColumn(4).setResizable(false);
        jTabelaItensCotacao.getColumnModel().getColumn(5).setPreferredWidth(70);
