    private void jPrecoFornecedorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPrecoFornecedorFocusLost
        // TODO add your handling code here:
        DecimalFormat qtdReal = new DecimalFormat("#,##00.0");
        qtdReal.setCurrency(Currency.getInstance(new Locale("pt", "BR")));
        try {
            objProdutoCotacao.setQtdItem(qtdReal.parse(jQtdItem.getText()).floatValue());
            objProdutoCotacao.setValorUnitario(qtdReal.parse(jPrecoFornecedor.getText()).floatValue());
            objProdutoCotacao.setValorTotalProduto(qtdReal.parse(jValorTotalProduto.getText()).floatValue());
        } catch (ParseException ex) {
        }
        valorTotalItem = objProdutoCotacao.getQtdItem() * objProdutoCotacao.getValorUnitario();
        objProdutoCotacao.setValorTotalProduto(valorTotalItem);
        DecimalFormat vti = new DecimalFormat("#,##0.00");
        String vlTotalItem = vti.format(valorTotalItem);
        jValorTotalProduto.setText(vlTotalItem);
    }//GEN-LAST:event_jPrecoFornecedorFocusLost


