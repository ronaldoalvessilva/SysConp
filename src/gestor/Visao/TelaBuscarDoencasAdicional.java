        jTabelaDoencas.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaDoencas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaDoencas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaDoencas.getColumnModel().getColumn(2).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(3).setPreferredWidth(250);
