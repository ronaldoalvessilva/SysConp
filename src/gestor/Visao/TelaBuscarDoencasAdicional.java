    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaDoencas.getValueAt(jTabelaDoencas.getSelectedRow(), 0);
            jPesqDescricao.setText(IdLanc);
            jBtAdicionarPatologia.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM DOENCAS WHERE IdDoenca ='" + IdLanc + "'");
                conecta.rs.first();
                jIdItem.setText(conecta.rs.getString("IdDoenca"));
                jDescricaoPatologia.setText(conecta.rs.getString("Descricao"));
                jCid.setText(conecta.rs.getString("Cid"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

