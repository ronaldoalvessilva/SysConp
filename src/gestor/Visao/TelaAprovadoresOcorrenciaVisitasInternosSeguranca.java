    public void preencherTabelaAprovadores(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Nome do Aprovador"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data Entrada
                dataEntrada = conecta.rs.getString("DataAprova");
                String diae = dataEntrada.substring(8, 10);
                String mese = dataEntrada.substring(5, 7);
                String anoe = dataEntrada.substring(0, 4);
                dataEntrada = diae + "/" + mese + "/" + anoe;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdAprova"), dataEntrada, conecta.rs.getString("StatusAprova"), conecta.rs.getString("NomeUsuario")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAprovadores.setModel(modelo);
        jTabelaAprovadores.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaAprovadores.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaAprovadores.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTabelaAprovadores.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(3).setPreferredWidth(250);
        jTabelaAprovadores.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAprovadores.getTableHeader().setReorderingAllowed(false);
        jTabelaAprovadores.setAutoResizeMode(jTabelaAprovadores.AUTO_RESIZE_OFF);
        jTabelaAprovadores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharTabelaAprovadoresOcorrenciaVisitas();
        conecta.desconecta();
    }

