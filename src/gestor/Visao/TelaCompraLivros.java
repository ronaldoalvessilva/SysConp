    private void jTabelaComprasLivrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaComprasLivrosMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaComprasLivros.getValueAt(jTabelaComprasLivros.getSelectedRow(), 0);
            jIdPesqDoc.setText(IdLanc);
            jDataDoc.setDate(jDataDoc.getDate());
            //
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtFinalizar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            jBtNovoItem.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM COMPRAS_ACERVO "
                        + "INNER JOIN FORNECEDORES_ACERVO "
                        + "ON COMPRAS_ACERVO.IdForn=COMPRAS_ACERVO.IdForn "
                        + "WHERE IdCompra ='" + IdLanc + "'");
                conecta.rs.first();
                jIdDoc.setText(String.valueOf(conecta.rs.getInt("IdCompra")));
                jStatusDoc.setText(conecta.rs.getString("StatusLanc"));
                jComboBoxClassificacao.setSelectedItem(conecta.rs.getString("ClasseCompra"));
                jDataDoc.setDate(conecta.rs.getDate("DataCompra"));
                jDescricaoEditora.setText(conecta.rs.getString("RazaoSocial"));
                jNumDoc.setText(conecta.rs.getString("NumeroDoc"));
                jSerieDoc.setText(conecta.rs.getString("SerieDoc"));
                jDataRecebimento.setDate(conecta.rs.getDate("DataRecebe"));
                jDataEmissao.setDate(conecta.rs.getDate("DataEmissao"));
                //
                statusTipoPagto = conecta.rs.getInt("FormaPagto");
                if (statusTipoPagto == 0) {
                    jRadBtAvista.setSelected(true);
                } else if (statusTipoPagto == 1) {
                    jRadBtPrazo.setSelected(true);
                }
                //
                valorProduto = conecta.rs.getFloat("ValorProdutos");
                DecimalFormat vp = new DecimalFormat(",###0.00");
                String vlProduto = vp.format(valorProduto);
                jValorProdutos.setText(vlProduto);
                //
                valorNFe = conecta.rs.getFloat("ValorNFE");
                DecimalFormat vnf = new DecimalFormat(",###0.00");
                String vlNotaFiscal = vnf.format(valorNFe);
                jValorNFE.setText(vlNotaFiscal);
                //
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisar dados... " + e);
            }
            count = 0;
            preencherTabelaItens("SELECT * FROM ITENS_COMPRA_ACERVO "
                    + "INNER JOIN LIVROS_REVISTAS_JORNAIS "
                    + "ON ITENS_COMPRA_ACERVO.IdLivro=LIVROS_REVISTAS_JORNAIS.IdLivro "
                    + "INNER JOIN COMPRAS_ACERVO "
                    + "ON ITENS_COMPRA_ACERVO.IdCompra=COMPRAS_ACERVO.IdCompra "
                    + "INNER JOIN LOCAL_ACERVO "
                    + "ON LIVROS_REVISTAS_JORNAIS.IdLocal=LOCAL_ACERVO.IdLocal "
                    + "WHERE ITENS_COMPRA_ACERVO.IdCompra='" + IdLanc + "'");
        }
    }//GEN-LAST:event_jTabelaComprasLivrosMouseClicked

