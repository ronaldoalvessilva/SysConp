    public void validarEstoque() {

        for (int i = 0; i < jTabelaProdutos.getRowCount(); i++) {
            objEstoqueComp.setDataLanc(jDataDoc.getDate());
            objEstoqueComp.setIdDocumento(Integer.valueOf(jIdDoc.getText()));
            objEstoqueComp.setTipoMov(tipoMov);
            objEstoqueComp.setIdLivro((int) jTabelaProdutos.getValueAt(i, 1));
            objEstoqueComp.setNomeLivro((String) jTabelaProdutos.getValueAt(i, 2));
            objEstoqueComp.setSaldoEstoque((int) jTabelaProdutos.getValueAt(i, 3));
            objEstoqueComp.setDescricaoLocal((String) jTabelaProdutos.getValueAt(i, 5));
            // SOMAR SALDO ANTES DE PASSAR PARA O BANCO DE DADOS. 
            pegarSaldoEstoque(objEstoqueComp.getIdLivro());
            if (objEstoqueComp.getIdLivro() == codLivro) {
                saldoEstoque = saldoEstoque + objEstoqueComp.getSaldoEstoque();
                objEstoqueComp.setSaldoEstoqueAtual((float) saldoEstoque);
            } else {
                saldoEstoque = objEstoqueComp.getSaldoEstoque();
                objEstoqueComp.setSaldoEstoqueAtual((float) saldoEstoque);
            }
            controleEstoque.incluirEstoqueAcervoCompras(objEstoqueComp); // alterar saldo de estoque dos produtos ESTQUE_ACERVO
        }
    }

