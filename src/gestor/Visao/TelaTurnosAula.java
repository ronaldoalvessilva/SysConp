    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaTurnosAulaManu_PEDA);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoPEDA.equals("ADMINISTRADORES") || codigoUserPEDA == codUserAcessoPEDA && nomeTelaPEDA.equals(telaTurnosAulaManu_PEDA) && codExcluirPEDA == 1) {
            verificarRegistros();
            statusMov = "Excluiu";
            horaMov = jHoraSistema.getText();
            dataModFinal = jDataSistema.getText();
            if (jIdTurno.getText().equals(codTurno)) {
                JOptionPane.showMessageDialog(rootPane, "Não é possível excluir esse registro, o mesmo está sendo utilizado por outro registro.");
            } else {
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o LANÇAMENTO selecionado?", "Confirmação",
                        JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    objTurma.setIdTurno(Integer.parseInt(jIdTurno.getText()));
                    control.excluirTurnosAula(objTurma);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    JOptionPane.showMessageDialog(rootPane, "Registro EXCLUIDO com sucesso !!!");
                    Excluir();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acesso não autorizado, solicite liberação ao administrador.");
        }
    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaTurnosAulaManu_PEDA);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoPEDA.equals("ADMINISTRADORES") || codigoUserPEDA == codUserAcessoPEDA && nomeTelaPEDA.equals(telaTurnosAulaManu_PEDA) && codGravarPEDA == 1) {
            if (jDataCadastro.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro.");
                jDataCadastro.requestFocus();
                jDataCadastro.setBackground(Color.red);
            } else {
                if (jDescricaoTurno.getText().equals("")) {
                    JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro.");
                    jDescricaoTurno.requestFocus();
                    jDescricaoTurno.setBackground(Color.red);
                } else {
                    objTurma.setStatusTurno((String) jComboBoxStatus.getSelectedItem());
                    objTurma.setDataCad(jDataCadastro.getDate());
                    objTurma.setDescricaoTurno(jDescricaoTurno.getText());
                    if (acao == 1) {
                        objTurma.setUsuarioInsert(nameUser);
                        objTurma.setDataInsert(dataModFinal);
                        objTurma.setHorarioInsert(horaMov);
                        control.incluirTurnosAula(objTurma);
                        buscarCod();
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                        Salvar();
                    }
                    if (acao == 2) {
                        objTurma.setUsuarioUp(nameUser);
                        objTurma.setDataUp(dataModFinal);
                        objTurma.setHorarioUp(horaMov);
                        objTurma.setIdTurno(Integer.valueOf(jIdTurno.getText()));
                        control.alterarTurnoAula(objTurma);
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                        Salvar();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acesso não autorizado, solicite liberação ao administrador.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

