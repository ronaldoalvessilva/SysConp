    private void jTabelaAprovadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAprovadoresMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaAprovadores.getValueAt(jTabelaAprovadores.getSelectedRow(), 0);
            jIdPesqFunc.setText(IdLanc);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM SOLICITANTE_REQUISICAO_MEDICAMENTOS_ENFAR "
                        + "INNER JOIN DEPARTAMENTOS "
                        + "ON SOLICITANTE_REQUISICAO_MEDICAMENTOS_ENFAR.IdDepartamento=DEPARTAMENTOS.IdDepartamento "
                        + "WHERE IdFuncSolici='" + IdLanc + "'");
                conecta.rs.first();
                jIdFuncSoli.setText(String.valueOf(conecta.rs.getInt("IdFuncSolici")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusSolici"));
                jNomeSolicitanteMedicamento.setText(conecta.rs.getString("NomeSolicitante"));
                jMatriculoaSolicitante.setText(conecta.rs.getString("Matricula"));
                jCargoSolicitante.setText(conecta.rs.getString("Cargo"));
                jIdDepto.setText(conecta.rs.getString("IdDepartamento"));
                jNomeDepartamento.setText(conecta.rs.getString("NomeDepartamento"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                caminho = conecta.rs.getString("FotoFuncSolici");
                if (caminho != null) {
                    javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                    jFotoColaborador.setIcon(i);
                    jFotoColaborador.setIcon(new ImageIcon(i.getImage().getScaledInstance(jFotoColaborador.getWidth(), jFotoColaborador.getHeight(), Image.SCALE_DEFAULT)));
                }
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível exibir registro.\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaAprovadoresMouseClicked

