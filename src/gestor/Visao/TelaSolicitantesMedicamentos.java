    public void verificarColaboradorAprovador() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM SOLICITACAO_PRODUTOS_ENFAR "
                    + "WHERE IdFuncAprova='" + objSolicitante.getIdSolicitante() + "'");
            conecta.rs.first();
            codAprovador = conecta.rs.getString("IdFuncAprova");
            if (jIdFuncSoli.getText().equals(codAprovador)) {
                JOptionPane.showMessageDialog(rootPane, "Esse registro não poderá ser excluído,\no mesmo está sendo utilizado em outro local.");
            }
        } catch (SQLException ex) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o LANÇAMENTO selecionado?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                objSolicitante.setIdSolicitante(Integer.parseInt(jIdFuncSoli.getText()));
                control.excluirSolicitanteENFAR(objSolicitante);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro EXCLUIDO com sucesso !!!");
                Excluir();
            }
        }
    }

    public void pesquisarColaboadorAprovador(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", " Solicitante", "       Observação"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                //
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdFuncSolici"), conecta.rs.getString("StatusSolici"), conecta.rs.getString("NomeSolicitante"), conecta.rs.getString("Observacao")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAprovadores.setModel(modelo);
        jTabelaAprovadores.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaAprovadores.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaAprovadores.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(2).setPreferredWidth(270);
        jTabelaAprovadores.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAprovadores.getColumnModel().getColumn(3).setPreferredWidth(280);
        jTabelaAprovadores.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAprovadores.getTableHeader().setReorderingAllowed(false);
        jTabelaAprovadores.setAutoResizeMode(jTabelaAprovadores.AUTO_RESIZE_OFF);
        jTabelaAprovadores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }

