    private void jBtPesquisarParenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarParenteActionPerformed
        // TODO add your handling code here:
        if (jGrauParentesco.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o parentesco do interno para pesquisa.");
        } else {
            try {
                conecta.abrirConexao();
                String path = "reports/ServicoSocial/RelatorioParentesInternos.jasper";
                conecta.executaSQL("SELECT * FROM ROLVISITAS "
                        + "INNER JOIN ITENSROL "
                        + "ON ROLVISITAS.IdRol=ITENSROL.IdRol "
                        + "INNER JOIN VISITASINTERNO "
                        + "ON ITENSROL.IdVisita=VISITASINTERNO.IdVisita "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ROLVISITAS.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "WHERE VISITASINTERNO.ParentescoVisita='" + jGrauParentesco.getText() + "' "
                        + "AND ROLVISITAS.StatusRol='" + statusRol + "' "
                        + "ORDER BY PRONTUARIOSCRC.NomeInternoCrc");
                HashMap parametros = new HashMap();
                parametros.put("statusRol", statusRol);
                parametros.put("parentesco", jGrauParentesco.getText());
                parametros.put("usuarioNome", nameUser);
                JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio                                   
                JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao          
                jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                jv.setTitle("Relatório de Parentes dos Internos"); // Titulo do relatório
                jv.setVisible(true); // Chama o relatorio para ser visualizado                                    
                jv.toFront(); // Traz o relatorio para frente da aplicação            
                conecta.desconecta();
            } catch (JRException e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório.\nERRO :" + e);
            }
        }
    }//GEN-LAST:event_jBtPesquisarParenteActionPerformed


