    // Preencher tabela pesquisando por nome
    public void preencherTabelaNome(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição", "Classificação"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdUnid"), conecta.rs.getString("DescricaoUnid"), conecta.rs.getString("ClassUnid")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaUnidade.setModel(modelo);
        jTabelaUnidade.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaUnidade.getColumnModel().getColumn(0).setResizable(false);
        jTabelaUnidade.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTabelaUnidade.getColumnModel().getColumn(1).setResizable(false);
        jTabelaUnidade.getColumnModel().getColumn(2).setPreferredWidth(125);
        jTabelaUnidade.getColumnModel().getColumn(2).setResizable(false);
        jTabelaUnidade.setAutoResizeMode(jTabelaUnidade.AUTO_RESIZE_OFF);
        jTabelaUnidade.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

