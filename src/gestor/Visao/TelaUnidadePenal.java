    // Relacionamento da UNIDADE PENAL com tabela de INTERNOS
    public void buscarUnidadeExcluir() {
        conecta.abrirConexao();
        conecta.executaSQL("SELECT * FROM DADOSFISICOSINTERNOS WHERE IdUnid='" + jIdUnid.getText() + "'");
        try {
            conecta.rs.first();
            codUnid = conecta.rs.getInt("IdUnid");
            if (codUnid != 0) {
                JOptionPane.showMessageDialog(rootPane, "UNIDADE PENAL não poderá ser DELETADO, existe registro relacionado");
                conecta.desconecta();
            }
        } catch (SQLException ex) {
        }
    }

