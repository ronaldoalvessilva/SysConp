    public void formatarCampos() {
        jDescricao.setDocument(new LimiteDigitosAlfa(50));
        jEndereco.setDocument(new LimiteDigitosAlfa(60));
        jComplemento.setDocument(new LimiteDigitosAlfa(25));
        jBairro.setDocument(new LimiteDigitosAlfa(18));
        jCidade.setDocument(new LimiteDigitos(25));
        jEstado.setDocument(new LimiteDigitos(18));
        jObservacao.setDocument(new LimiteDigitosAlfa(60));
        // Mascara para CEP e TELEFONE
        try {
            MaskFormatter cep = new MaskFormatter("##.###-###");
            jCep.setFormatterFactory(new DefaultFormatterFactory(cep));
            MaskFormatter telefone = new MaskFormatter("(###)-####-####");
            jTelefone.setFormatterFactory(new DefaultFormatterFactory(telefone));
            MaskFormatter fone = new MaskFormatter("(###)-####-####");
            jFone.setFormatterFactory(new DefaultFormatterFactory(fone));
            MaskFormatter fax = new MaskFormatter("(###)-####-####");
            jFax.setFormatterFactory(new DefaultFormatterFactory(fax));
        } catch (ParseException ex) {
            Logger.getLogger(TelaUnidadePenal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

