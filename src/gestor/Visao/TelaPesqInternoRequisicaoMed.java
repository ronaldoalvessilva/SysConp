    private void jBtPesqNomeInternoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesqNomeInternoActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (jPesqNomeInterno.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o nome do interno para pesquisar.");
        } else {
            jTabelaLocacao.setVisible(true);
            preencherTabelaInternos("SELECT * FROM ITENSLOCACAOINTERNO "
                    + "INNER JOIN PRONTUARIOSCRC "
                    + "ON ITENSLOCACAOINTERNO.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                    + "INNER JOIN CELAS "
                    + "ON ITENSLOCACAOINTERNO.IdCela=CELAS.IdCela "
                    + "INNER JOIN PAVILHAO "
                    + "ON CELAS.IdPav=PAVILHAO.IdPav "
                    + "WHERE NomeInternoCrc LIKE'%" + jPesqNomeInterno.getText() + "%'");
        }
    }//GEN-LAST:event_jBtPesqNomeInternoActionPerformed

    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String nomeInterno = "" + jTabelaLocacao.getValueAt(jTabelaLocacao.getSelectedRow(), 1);
            jPesqNomeInterno.setText(nomeInterno);
            String idInterno = "" + jTabelaLocacao.getValueAt(jTabelaLocacao.getSelectedRow(), 0);
            jCodInterno.setText(idInterno);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ITENSLOCACAOINTERNO "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ITENSLOCACAOINTERNO.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN CELAS "
                        + "ON ITENSLOCACAOINTERNO.IdCela=CELAS.IdCela "
                        + "INNER JOIN PAVILHAO "
                        + "ON CELAS.IdPav=PAVILHAO.IdPav WHERE NomeInternoCrc='" + jPesqNomeInterno.getText() + "'");
                conecta.rs.first();
                jCodInterno.setText(conecta.rs.getString("IdInternoCrc")); //Coluna 0
                jMatriculaPenal.setText(conecta.rs.getString("MatriculaCrc"));
                jNomeInternoReq.setText(conecta.rs.getString("NomeInternoCrc")); // Coluna 1 
                //
                caminho = conecta.rs.getString("FotoInternoCrc");
                javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                jFotoInternoReq.setIcon(i);
                jFotoInternoReq.setIcon(new ImageIcon(i.getImage().getScaledInstance(jFotoInternoReq.getWidth(), jFotoInternoReq.getHeight(), Image.SCALE_DEFAULT)));
                //
                jPavilhaoReq.setText(conecta.rs.getString("DescricaoPav"));
                jCelaReq.setText(conecta.rs.getString("EndCelaPav"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

