//Preencher tabela com todos os VISITANTES
    public void buscarVisitas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Nome da Visita", "Data Entrada", "Horário", "Data Saída", "Horário"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            // Formatar a data Entrada
            dataEntrada = conecta.rs.getString("DataEntrada");
            String dia = dataEntrada.substring(8, 10);
            String mes = dataEntrada.substring(5, 7);
            String ano = dataEntrada.substring(0, 4);
            dataEntrada = dia + "/" + mes + "/" + ano;
            //
            dataSaida = conecta.rs.getString("DataSaida");
            String dias = dataSaida.substring(8, 10);
            String mess = dataSaida.substring(5, 7);
            String anos = dataSaida.substring(0, 4);
            dataSaida = dias + "/" + mess + "/" + anos;
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdVisitaRel"), conecta.rs.getString("NomeVisitaRel"), dataEntrada, conecta.rs.getString("HorarioEntrada"), dataSaida, conecta.rs.getString("HorarioSaida")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqVisitante.setModel(modelo);
        jTabelaPesqVisitante.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaPesqVisitante.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqVisitante.getColumnModel().getColumn(1).setPreferredWidth(300);
        jTabelaPesqVisitante.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqVisitante.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaPesqVisitante.getColumnModel().getColumn(2).setResizable(false);
        jTabelaPesqVisitante.getColumnModel().getColumn(3).setPreferredWidth(70);
        jTabelaPesqVisitante.getColumnModel().getColumn(3).setResizable(false);
        jTabelaPesqVisitante.getColumnModel().getColumn(4).setPreferredWidth(80);
        jTabelaPesqVisitante.getColumnModel().getColumn(4).setResizable(false);
        jTabelaPesqVisitante.getColumnModel().getColumn(5).setPreferredWidth(70);
        jTabelaPesqVisitante.getColumnModel().getColumn(5).setResizable(false);
        jTabelaPesqVisitante.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqVisitante.setAutoResizeMode(jTabelaPesqVisitante.AUTO_RESIZE_OFF);
        jTabelaPesqVisitante.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharTabelaItens();
        conecta.desconecta();
    }
