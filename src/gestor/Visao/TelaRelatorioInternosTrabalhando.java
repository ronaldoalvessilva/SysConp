    private void jBtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtConfirmarActionPerformed
        // TODO add your handling code here:
        if (jComboBoxSituacaoTrabalho.getSelectedItem().equals("Selecione...")) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um opção para imprimir o relatório.");
        } else {
            if (jComboBoxSituacaoTrabalho.getSelectedItem().equals("Trabalhando")) {
                pAtivoInativo = "Ativo";
            } else if (jComboBoxSituacaoTrabalho.getSelectedItem().equals("Trabalhou")) {
                pAtivoInativo = "Inativo";
            }
            try {
                conecta.abrirConexao();
                String path = "reports/TerapiaOcupacional/RelatorioInternosAtividadeLaborativa.jasper";
                conecta.executaSQL("SELECT PRONTUARIOSCRC.IdInternoCrc, "
                        + "PRONTUARIOSCRC.NomeInternoCrc, RazaoSocial,StatusInterno "
                        + "FROM ITENSAGENDALABORATIVA "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ITENSAGENDALABORATIVA.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN EMPRESALAB "
                        + "ON ITENSAGENDALABORATIVA.IdEmp=EMPRESALAB.IdEmp "
                        + "WHERE StatusInterno='" + pAtivoInativo + "' "
                        + "AND SituacaoCrc='" + entradaUnidade + "' "
                        + "OR StatusInterno='" + pAtivoInativo + "' "
                        + "AND SituacaoCrc='" + retornoUnidade + "' "
                        + "ORDER BY RazaoSocial,NomeInternoCrc");
                HashMap parametros = new HashMap();
                parametros.put("pUsuario", nameUser);
                parametros.put("pUnidade", descricaoUnidade);
                parametros.put("ativoInativo", pAtivoInativo);
                JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio
                JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao
                jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                jv.setTitle("Relatório de Atividade Laborativa");
                jv.setVisible(true); // Chama o relatorio para ser visualizado
                jv.toFront(); // Traz o relatorio para frente da aplicação
                conecta.desconecta();
            } catch (JRException e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório \n\nERRO :" + e);
            }
        }
    }//GEN-LAST:event_jBtConfirmarActionPerformed

