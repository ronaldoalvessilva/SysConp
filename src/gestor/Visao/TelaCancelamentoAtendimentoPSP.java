    private void jTabelaRegistroCancelamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaRegistroCancelamentoMouseClicked
        // TODO add your handling code here:
        count = 0;
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaRegistroCancelamento.getValueAt(jTabelaRegistroCancelamento.getSelectedRow(), 0);
            jIDPesqAtend.setText(IdLanc);
            //
            bloquearCampos();
            bloquearBotoes();
            limparCampos();
            jBtNovo.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ELIMIAR_ASSINATURA_INTERNO_PSP "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ELIMIAR_ASSINATURA_INTERNO_PSP.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN DEPARTAMENTOS "
                        + "ON ELIMIAR_ASSINATURA_INTERNO_PSP.IdDepartamento=DEPARTAMENTOS.IdDepartamento "
                        + "WHERE IdCancel='" + IdLanc + "'");
                conecta.rs.first();
                jCodigoRegistro.setText(String.valueOf(conecta.rs.getInt("IdCancel")));
                jStatusRegistro.setText(conecta.rs.getString("StatusCancelamento"));
                jDataRegistro.setDate(conecta.rs.getDate("DataCancelamento"));
                jDepartamento.setText(conecta.rs.getString("NomeDepartamento"));
                jUsuarioAtendente.setText(conecta.rs.getString("UsuarioAtendente"));
                jIdAssinatura.setText(String.valueOf(conecta.rs.getInt("IdRegistro")));
                jDataAssinatura.setDate(conecta.rs.getDate("DataRegistro"));
                jHorarioAssinatura.setText(conecta.rs.getString("Horario"));
                jTipoAtendimento.setText(conecta.rs.getString("TipoAtendimento"));
                jIdInterno.setText(conecta.rs.getString("IdInternoCrc"));
                jNomeInterno.setText(conecta.rs.getString("NomeInternoCrc"));
                jMotivoCancelamento.setText(conecta.rs.getString("Motivo"));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            conecta.abrirConexao();
        }
    }//GEN-LAST:event_jTabelaRegistroCancelamentoMouseClicked

