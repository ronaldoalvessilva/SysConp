    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String nomeAdvogado = "" + jTabelaPesqOficialJustica.getValueAt(jTabelaPesqOficialJustica.getSelectedRow(), 1);
            jPesqNomeAdvogado.setText(nomeAdvogado);
            String idFunc = "" + jTabelaPesqOficialJustica.getValueAt(jTabelaPesqOficialJustica.getSelectedRow(), 0);
            jIdTransiente.setText(idFunc);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM OFICIAL_JUSTICA "
                        + "WHERE NomeOficial='" + nomeAdvogado + "'");
                conecta.rs.first();
                jIdTransiente.setText(String.valueOf(conecta.rs.getInt("IdOficial")));
                jNomeTransiente.setText(conecta.rs.getString("NomeOficial"));
                // Capturando foto
                caminhoOficial = conecta.rs.getString("FotoOficial");
                if (caminhoOficial != null) {
                    javax.swing.ImageIcon a = new javax.swing.ImageIcon(caminhoOficial);
                    jFotoTransiente.setIcon(a);
                    jFotoTransiente.setIcon(new ImageIcon(a.getImage().getScaledInstance(jFotoTransiente.getWidth(), jFotoTransiente.getHeight(), Image.SCALE_DEFAULT)));
                }
                // BUSCAR A FOTO DO ADVOGADO NO BANCO DE DADOS
                byte[] imgBytes = ((byte[]) conecta.rs.getBytes("ImagemFrenteOF"));
                if (imgBytes != null) {
                    ImageIcon pic = null;
                    pic = new ImageIcon(imgBytes);
                    Image scaled = pic.getImage().getScaledInstance(jFotoTransiente.getWidth(), jFotoTransiente.getHeight(), Image.SCALE_DEFAULT);
                    ImageIcon icon = new ImageIcon(scaled);
                    jFotoTransiente.setIcon(icon);
                }
                jBtZoom.setEnabled(true);
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome." + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

