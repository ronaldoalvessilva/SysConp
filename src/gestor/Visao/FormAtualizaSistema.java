    public void copyDirectory(File srcPath, File dstPath) throws IOException {
        
        if (srcPath.isDirectory()) {
            if (!dstPath.exists()) {
                dstPath.mkdir();
            }
            String files[] = srcPath.list();
            for (int i = 0; i < files.length; i++) {
                copyDirectory(new File(srcPath, files[i]),
                        new File(dstPath, files[i]));
            }
        } else {
            if (!srcPath.exists()) {
                System.out.println("File or directory does not exist." + srcPath);
                System.exit(0);
            } else {
                InputStream in = new FileInputStream(srcPath);
                OutputStream out = new FileOutputStream(dstPath);
                // Transfer bytes from in to out
                byte[] buf = new byte[1024];
                int len;
                while ((len = in.read(buf)) > 0) {
                    out.write(buf, 0, len);                    
                }                
                in.close();
                out.close();
            }            
        }
        System.out.println("Directory copied." + srcPath);         
//        jLabel5.setText("Copiando Arquivos..." + srcPath);
        jLabel2.setText("Sistema Atualizado com sucesso, clique no botão sair.");
        jBtConfirmar.setEnabled(!true);
        jBtSair.setEnabled(true);
    }   
