    public void buscarTextoAtestado() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM EMISSAO_ATESTADO_RECLUSAO_CRC "
                    + "WHERE IdAtestado='" + jIdAtestado.getText() + "'");
            conecta.rs.first();
            jEditorTextoAtestado.setText(conecta.rs.getString("TextoAtestado"));
        } catch (SQLException ex) {
            Logger.getLogger(TelaTextoAtestadoReclusaoCRC.class.getName()).log(Level.SEVERE, null, ex);
        }
        conecta.desconecta();
        if (acao == 1 || acao == 2) {
            jEditorTextoAtestado.setEnabled(true);
        }
    }
