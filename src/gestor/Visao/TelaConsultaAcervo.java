    public void preencherTabelaAcervo(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data Compra", "Descrição do Acervo", "Tipo Acervo", "Código Barras"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataDocumeto = conecta.rs.getString("DataAquisicao");
                String dia = dataDocumeto.substring(8, 10);
                String mes = dataDocumeto.substring(5, 7);
                String ano = dataDocumeto.substring(0, 4);
                dataDocumeto = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdLivro"), dataDocumeto, conecta.rs.getString("TituloLivro"), conecta.rs.getString("TipoLivro"), conecta.rs.getString("CodigoBarra")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAcervo.setModel(modelo);
        jTabelaAcervo.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaAcervo.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAcervo.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaAcervo.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAcervo.getColumnModel().getColumn(2).setPreferredWidth(340);
        jTabelaAcervo.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAcervo.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTabelaAcervo.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAcervo.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTabelaAcervo.getColumnModel().getColumn(4).setResizable(false);
        jTabelaAcervo.getTableHeader().setReorderingAllowed(false);
        jTabelaAcervo.setAutoResizeMode(jTabelaAcervo.AUTO_RESIZE_OFF);
        jTabelaAcervo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaAcervo();
        conecta.desconecta();
    }

