    private void jTabelaCatReceitasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaCatReceitasMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeCat = "" + jTabelaCatReceitas.getValueAt(jTabelaCatReceitas.getSelectedRow(), 3);
            jPesqDescricaoCatReceita.setText(nomeCat);
            jBtNovo.setEnabled(!true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM CATEGORIA_RECEITAS_NUTRI WHERE DescricaoCategoria='" + jPesqDescricaoCatReceita.getText() + "'");
                conecta.rs.first();
                jCodigoCatReceita.setText(String.valueOf(conecta.rs.getInt("IdCat")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusCat"));
                jComboBoxTipoReceita.setSelectedItem(conecta.rs.getString("TipoReceita"));
                jDataCategoriaReceita.setDate(conecta.rs.getDate("DataCat"));
                jDecricaoCatReceita.setText(conecta.rs.getString("DescricaoCategoria"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaCatReceitasMouseClicked

