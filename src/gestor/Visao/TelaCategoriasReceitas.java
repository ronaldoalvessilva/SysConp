    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jDataCategoriaReceita.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data de Cadastro.");
        } else if (jDecricaoCatReceita.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a descrição da categoria.");
        } else {
            objCat.setStatusCat((String) jComboBoxStatus.getSelectedItem());
            objCat.setTipoReceita((String) jComboBoxTipoReceita.getSelectedItem());
            objCat.setDataCat(jDataCategoriaReceita.getDate());
            objCat.setDescricaoCategoria(jDecricaoCatReceita.getText());
            if (acao == 1) {
                objCat.setUsuarioInsert(nameUser);
                objCat.setDataInsert(dataModFinal);
                objCat.setHorarioInsert(horaMov);
                //
                control.incluirCategoriaReceitas(objCat);
                buscarCodigo();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                desabilitarCampos();
                botaoSalvar();
            }
            if (acao == 2) {
                objCat.setUsuarioUp(nameUser);
                objCat.setDataUp(dataModFinal);
                objCat.setHorarioUp(horaMov);
                //
                objCat.setIdCat(Integer.valueOf(jCodigoCatReceita.getText()));
                control.alterarCategoriaReceitas(objCat);
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                desabilitarCampos();
                botaoSalvar();
            }
        }
        desabilitarCampos();
    }//GEN-LAST:event_jBtSalvarActionPerformed

