    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String nomeInterno = "" + jTabelaPesqInternos.getValueAt(jTabelaPesqInternos.getSelectedRow(), 1);
            jPesqNomeInterno.setText(nomeInterno);
            String idInterno = "" + jTabelaPesqInternos.getValueAt(jTabelaPesqInternos.getSelectedRow(), 0);
            jIdInternoCrcFreq.setText(idInterno);
            //
            jBtNovoInterno.setEnabled(true);
            jBtAlterarInterno.setEnabled(true);
            jBtExcluirInterno.setEnabled(true);
            jBtSalvarInterno.setEnabled(true);
            jBtCancelarInterno.setEnabled(true);
            jBtBuscarInterno.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ITENS_FREQUENCIA_LABORATIVA_EXTERNA "
                        + "INNER JOIN FREQUENCIA_LABORATIVA_EXTERNA "
                        + "ON ITENS_FREQUENCIA_LABORATIVA_EXTERNA.IdFreqLab=FREQUENCIA_LABORATIVA_EXTERNA.IdFreqLab "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON ITENS_FREQUENCIA_LABORATIVA_EXTERNA.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "WHERE PRONTUARIOSCRC.NomeInternoCrc='" + jPesqNomeInterno.getText() + "' "
                        + "AND FREQUENCIA_LABORATIVA_EXTERNA.IdFreqLab='" + jIdFrequencia.getText() + "'");
                conecta.rs.first();
                jIdInternoCrcFreq.setText(conecta.rs.getString("IdInternoCrc")); //Coluna 0
                jNomeInternoFreqTerapia.setText(conecta.rs.getString("NomeInternoCrc")); // Coluna 1  
                jDiasTrabalhado.setText(conecta.rs.getString("TotalDias"));
                //
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

