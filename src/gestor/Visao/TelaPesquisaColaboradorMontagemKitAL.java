    public void preencherTabelaColaborador(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Nome do Colaborador", "Nome Departamento", "Cargo"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdFunc"), conecta.rs.getString("NomeFunc"), conecta.rs.getString("NomeDepartamento"), conecta.rs.getString("NomeCargo")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        TabelaColaborador.setModel(modelo);
        TabelaColaborador.getColumnModel().getColumn(0).setPreferredWidth(70);
        TabelaColaborador.getColumnModel().getColumn(0).setResizable(false);
        TabelaColaborador.getColumnModel().getColumn(1).setPreferredWidth(300);
        TabelaColaborador.getColumnModel().getColumn(1).setResizable(false);
        TabelaColaborador.getColumnModel().getColumn(2).setPreferredWidth(200);
        TabelaColaborador.getColumnModel().getColumn(2).setResizable(false);
        TabelaColaborador.getColumnModel().getColumn(3).setPreferredWidth(200);
        TabelaColaborador.getColumnModel().getColumn(3).setResizable(false);
        TabelaColaborador.getTableHeader().setReorderingAllowed(false);
        TabelaColaborador.setAutoResizeMode(TabelaColaborador.AUTO_RESIZE_OFF);
        TabelaColaborador.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }

