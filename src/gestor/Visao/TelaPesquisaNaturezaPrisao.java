    public void preencherTabelaAmparo(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Natureza da Prisão"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdNatp"), conecta.rs.getString("StatusNatp"), conecta.rs.getString("DescricaoNatureza")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaNaturezaPrisao.setModel(modelo);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(0).setResizable(false);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(1).setResizable(false);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(2).setPreferredWidth(300);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(2).setResizable(false);        
        jTabelaNaturezaPrisao.getTableHeader().setReorderingAllowed(false);
        jTabelaNaturezaPrisao.setAutoResizeMode(jTabelaNaturezaPrisao.AUTO_RESIZE_OFF);
        jTabelaNaturezaPrisao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

    public void limparTabela() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Natureza da Prisão"};
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaNaturezaPrisao.setModel(modelo);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(0).setResizable(false);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(1).setResizable(false);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(2).setPreferredWidth(300);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(2).setResizable(false);       
        jTabelaNaturezaPrisao.getTableHeader().setReorderingAllowed(false);
        jTabelaNaturezaPrisao.setAutoResizeMode(jTabelaNaturezaPrisao.AUTO_RESIZE_OFF);
        jTabelaNaturezaPrisao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        modelo.getLinhas().clear();
    }

    public void alinharCamposTabela() {
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaNaturezaPrisao.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        jTabelaNaturezaPrisao.getColumnModel().getColumn(1).setCellRenderer(centralizado);      
    }
