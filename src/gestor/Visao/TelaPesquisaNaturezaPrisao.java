    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaNaturezaPrisao.getValueAt(jTabelaNaturezaPrisao.getSelectedRow(), 0);
            jDescricaoNatureza.setText(IdLanc);
            //            
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM NATUREZA_PRISAO "
                        + "WHERE IdNatp='" + IdLanc + "'");
                conecta.rs.first();
                jIdNatp.setText(String.valueOf(conecta.rs.getInt("IdNatp")));
                jDescricaoNaturezaPrisao.setText(conecta.rs.getString("DescricaoNatureza"));               
                //
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa..." + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

