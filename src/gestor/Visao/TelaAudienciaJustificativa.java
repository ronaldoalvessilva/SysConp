    public void preencherTabelaAudiencias(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Nome do Interno"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataLanc = conecta.rs.getString("DataLanc");
                String dia = dataLanc.substring(8, 10);
                String mes = dataLanc.substring(5, 7);
                String ano = dataLanc.substring(0, 4);
                dataLanc = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdLanc"), dataLanc, conecta.rs.getString("StatusLanc"), conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAudiencias.setModel(modelo);
        jTabelaAudiencias.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaAudiencias.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAudiencias.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaAudiencias.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAudiencias.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaAudiencias.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAudiencias.getColumnModel().getColumn(3).setPreferredWidth(420);
        jTabelaAudiencias.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAudiencias.getTableHeader().setReorderingAllowed(false);
        jTabelaAudiencias.setAutoResizeMode(jTabelaAudiencias.AUTO_RESIZE_OFF);
        jTabelaAudiencias.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharTabela();
        conecta.desconecta();
    }

