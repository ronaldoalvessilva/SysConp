    public void iniciarProcessos() {
        DecimalFormat qtdReal = new DecimalFormat("##,##0.00");
        qtdReal.setCurrency(Currency.getInstance(new Locale("pt", "BR")));
        try {
            objBusInt.setCapacidade(qtdReal.parse(jCapacidadeUnidade.getText()).floatValue());
            objBusInt.setPopulacaoAtual(qtdReal.parse(jPopulacaoAtual.getText()).floatValue());
        } catch (ParseException ex) {
        }
        resultado = ((objBusInt.getPopulacaoAtual() * 100) / objBusInt.getCapacidade()) - 100;
        objBusInt.setPercentual(resultado);
        DecimalFormat vti = new DecimalFormat("#,##0.00");
        String vlTotalItem = vti.format(resultado);
        jPercentualExcessoPop.setText(vlTotalItem);
        //
        qtdExcesso = objBusInt.getPopulacaoAtual() - objBusInt.getCapacidade();
        DecimalFormat qtd = new DecimalFormat("#0");
        String vlLiquido = qtd.format(qtdExcesso);
        jQtdExcesso.setText(vlLiquido);
    }

