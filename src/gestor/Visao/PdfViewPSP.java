    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:    
        if (acao == 1) {
            String nome = txtDocumento.getText();
            objPron.setIdInterno(Integer.valueOf(jIdInternoConPSP.getText()));
            Sql s = new Sql();
            int codigo = s.auto_icrement("SELECT MAX(IdPdf) FROM PDF_INTERNOS;");
            File rota = new File(rota_documento);
            if (nome.trim().length() != 0 && rota_documento.trim().length() != 0) {
                salvarPdf(codigo, objPron.getIdInterno(), nome, rota);
                tpdf.visualizar_PdfBean(tabela);
                rota_documento = "";
               habilitaBotao(!true, !true, !true, !true, !true, !true);
                txtDocumento.setEnabled(false);
                txtPesquisa.setEnabled(true);
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Preencher o campo \"Descrição\"!");
            }
        }
        if (acao == 2) {
            if (txtDocumento.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencher o campo \"Descrição\"!");
            } else {
                String nome = txtDocumento.getText();
                File rota = new File(rota_documento);
                if (nome.trim().length() != 0 && rota_documento.trim().length() != 0) {
                    alterarPdf(id, nome, rota);
                    tpdf.visualizar_PdfBean(tabela);
                } else if (rota_documento.trim().length() == 0) {
                    alterarPdf(id, nome);
                    tpdf.visualizar_PdfBean(tabela);
                }
                rota_documento = "";
                habilitaBotao(!true, !true, !true, !true, !true, !true);
                txtDocumento.setEnabled(!true);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

