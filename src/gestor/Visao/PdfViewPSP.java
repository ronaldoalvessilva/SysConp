    public void excluirPdf(int codigo) {
        PdfDAO pa = new PdfDAO();
        PdfVO po = new PdfVO();
        if (JOptionPane.showConfirmDialog(null, "Deseja realemente excluir o registro selecionado?", "Excluir", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_NO_OPTION) {
            po.setId(codigo);
            pa.excluir_PdfVO(po);
        }
    }

    public void selecionarPdf() {
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fi = new FileNameExtensionFilter("pdf", "PDF");
        j.setFileFilter(fi);
        int se = j.showOpenDialog(this);
        if (se == 0) {
            this.txtDocumento.setText("" + j.getSelectedFile().getName());//optar por btnSelecionar ou txtDocumento
            rota_documento = j.getSelectedFile().getAbsolutePath();
        } else {
        }
    }

