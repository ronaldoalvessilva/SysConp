    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        if (jPesqNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Selecione o nome do interno e clique no botão ENVIAR");
        } else {
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM PRONTUARIOSCRC WHERE NomeInternoCrc LIKE'" + jPesqNome.getText() + "%' AND SituacaoCrc='" + situacao + "' OR NomeInternoCrc LIKE'" + jPesqNome.getText() + "' AND SituacaoCrc='" + situacaoRet + "'");
                conecta.rs.first();
                jIdInternoCrc.setText(String.valueOf(conecta.rs.getInt("IdInternoCrc")));
                jNomeInternoCrc.setText(conecta.rs.getString("NomeInternoCrc"));
                caminho = conecta.rs.getString("FotoInternoCrc");
                javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                FotoInternoAudiencia.setIcon(i);
                FotoInternoAudiencia.setIcon(new ImageIcon(i.getImage().getScaledInstance(FotoInternoAudiencia.getWidth(), FotoInternoAudiencia.getHeight(), Image.SCALE_DEFAULT)));               
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa INTERNO" + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

