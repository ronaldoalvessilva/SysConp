    private void jBtSalvarCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarCompActionPerformed
        // TODO add your handling code here:
        if (jDataEvento.getDate() == null) {
            jDataEvento.setBackground(Color.red);
            jDataEvento.requestFocus();
            JOptionPane.showMessageDialog(null, "Informe a data da agenda.");
        } else if (jDataLembrete.getDate() == null) {
            jDataLembrete.setBackground(Color.red);
            jDataLembrete.requestFocus();
            JOptionPane.showMessageDialog(null, "Informe a data do Lembrete.");
        } else if (jHoraLembrete.getText().equals("")) {
            jHoraLembrete.setBackground(Color.red);
            jHoraLembrete.requestFocus();
            JOptionPane.showMessageDialog(null, "Informe a hora do Lembrete.");
        } else {
            objAgeComp.setStatusAgenda((String) jComboBoxStatusComp.getSelectedItem());
            objAgeComp.setTipoEvento((String) jComboBoxTipoEvento.getSelectedItem());
            objAgeComp.setDataAgenda(jDataEvento.getDate());
            objAgeComp.setAssunto(jAssunto.getText());
            objAgeComp.setPrioridade((String) jComboBoxPrioridade.getSelectedItem());
            objAgeComp.setConclusao((String) jComboBoxConclusao.getSelectedItem());
            objAgeComp.setDataInicio(jDataInicio.getDate());
            objAgeComp.setDataTermino(jDataTermino.getDate());
            objAgeComp.setHoraInicio(jHoraInicio.getText());
            objAgeComp.setHoraTermino(jHoraTermino.getText());
            objAgeComp.setDataLembrete(jDataLembrete.getDate());
            objAgeComp.setHoraLembrete(jHoraLembrete.getText());
            objAgeComp.setTexto(jTextoEvento.getText());
            objAgeComp.setUsuarioAgenda(jNomeUsuarioAgenda.getText());
            if (acao == 1) {
                objAgeComp.setUsuarioInsert(nameUser);
                objAgeComp.setDataInsert(dataModFinal);
                objAgeComp.setHorarioInsert(horaMov);
                control.incluirCompromisso(objAgeComp);
                buscarCodigo();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Salvar();
                JOptionPane.showMessageDialog(null, "Registro gravado com sucesso.");
            }
            if (acao == 2) {
                objAgeComp.setUsuarioUp(nameUser);
                objAgeComp.setDataUp(dataModFinal);
                objAgeComp.setHorarioUp(horaMov);
                objAgeComp.setIdAgenda(Integer.valueOf(jCodigoAgendaComp.getText()));
                control.alterarCompromisso(objAgeComp);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Salvar();
                JOptionPane.showMessageDialog(null, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarCompActionPerformed

