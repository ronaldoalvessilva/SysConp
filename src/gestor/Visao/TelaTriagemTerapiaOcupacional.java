    private void jTabelaTriagemOcupacionalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaTriagemOcupacionalMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String idAtend = "" + jTabelaTriagemOcupacional.getValueAt(jTabelaTriagemOcupacional.getSelectedRow(), 0);
            jIDPesqLanc.setText(idAtend);
            //
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(!true);
            jBtAuditoria.setEnabled(true);
            jBtFinalizar.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM TRIAGEM_OCUPACIONAL "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON PRONTUARIOSCRC.IdInternoCrc=TRIAGEM_OCUPACIONAL.IdInternoCrc "
                        + "INNER JOIN DADOSPENAISINTERNOS "
                        + "ON PRONTUARIOSCRC.IdInternoCrc=DADOSPENAISINTERNOS.IdInternoCrc "
                        + "INNER JOIN UNIDADE "
                        + "ON DADOSPENAISINTERNOS.IdUnid=UNIDADE.IdUnid "
                        + "WHERE IdTriagem='" + idAtend + "'");
                conecta.rs.first();
                jIdTriagem.setText(String.valueOf(conecta.rs.getInt("IdTriagem")));
                jStatusLanc.setText(conecta.rs.getString("StatusLanc"));
                jDataCadastro.setDate(conecta.rs.getDate("DataLanc"));
                jIdInternoCrc.setText(conecta.rs.getString("IdInternoCrc"));
                jNomeInternoTriagemOcupacional.setText(conecta.rs.getString("NomeInternoCrc"));
                // Capturando foto
                caminho = conecta.rs.getString("FotoInternoCrc");
                javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                jFotoInternoTriagemOcupacional.setIcon(i);
                jFotoInternoTriagemOcupacional.setIcon(new ImageIcon(i.getImage().getScaledInstance(jFotoInternoTriagemOcupacional.getWidth(), jFotoInternoTriagemOcupacional.getHeight(), Image.SCALE_DEFAULT)));
                //
                jMatriculaPenal.setText(conecta.rs.getString("MatriculaCrc"));
                jProfissao.setText(conecta.rs.getString("ProfissaoCrc"));
                jProcedenciaInterno.setText(conecta.rs.getString("DescricaoUnid"));
                jComboBoxJaTrabalho.setSelectedItem(conecta.rs.getString("JaTrabalho"));
                jOndeTrabalhou.setText(conecta.rs.getString("OndeTrabalhou"));
                jComboBoxInteresseUnidade.setSelectedItem(conecta.rs.getString("InteresseUnidade"));
                jQualTipoAtividade.setText(conecta.rs.getString("QualTipoAtividade"));
                jComboBoxVisitasFinaisSemana.setSelectedItem(conecta.rs.getString("VisitasFinaisSemana"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados." + e);
            }
        }
    }//GEN-LAST:event_jTabelaTriagemOcupacionalMouseClicked

