    public void preencherTabelaTriagemOcupacional(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Data Cadastro", "Nome do Interno"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data Entrada
                dataEntrada = conecta.rs.getString("DataLanc");
                String dia = dataEntrada.substring(8, 10);
                String mes = dataEntrada.substring(5, 7);
                String ano = dataEntrada.substring(0, 4);
                dataEntrada = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdTriagem"), conecta.rs.getString("StatusLanc"), dataEntrada, conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaTriagemOcupacional.setModel(modelo);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(0).setResizable(false);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(1).setResizable(false);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(2).setResizable(false);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(3).setPreferredWidth(300);
        jTabelaTriagemOcupacional.getColumnModel().getColumn(3).setResizable(false);
        jTabelaTriagemOcupacional.getTableHeader().setReorderingAllowed(false);
        jTabelaTriagemOcupacional.setAutoResizeMode(jTabelaTriagemOcupacional.AUTO_RESIZE_OFF);
        jTabelaTriagemOcupacional.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

