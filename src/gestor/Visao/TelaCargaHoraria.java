    public void preencherTabelaCargaHoraria(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Descrição"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                // Formatar a data no formato Brasil
                dataCadastro = conecta.rs.getString("DataCad");
                String dia = dataCadastro.substring(8, 10);
                String mes = dataCadastro.substring(5, 7);
                String ano = dataCadastro.substring(0, 4);
                dataCadastro = dia + "/" + mes + "/" + ano;
                dados.add(new Object[]{conecta.rs.getInt("IdCarga"), dataCadastro, conecta.rs.getString("StatusCarga"), conecta.rs.getString("DescricaoCarga")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaCargaHoraria.setModel(modelo);
        jTabelaCargaHoraria.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaCargaHoraria.getColumnModel().getColumn(0).setResizable(false);
        jTabelaCargaHoraria.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaCargaHoraria.getColumnModel().getColumn(1).setResizable(false);
        jTabelaCargaHoraria.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaCargaHoraria.getColumnModel().getColumn(2).setResizable(false);
        jTabelaCargaHoraria.getColumnModel().getColumn(3).setPreferredWidth(280);
        jTabelaCargaHoraria.getColumnModel().getColumn(3).setResizable(false);
        jTabelaCargaHoraria.getTableHeader().setReorderingAllowed(false);
        jTabelaCargaHoraria.setAutoResizeMode(jTabelaCargaHoraria.AUTO_RESIZE_OFF);
        jTabelaCargaHoraria.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposCargaHoraria();
        conecta.desconecta();
    }

