    private void jTabelaCargaHorariaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaCargaHorariaMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String descricao = "" + jTabelaCargaHoraria.getValueAt(jTabelaCargaHoraria.getSelectedRow(), 3);
            jPesqDescricao.setText(descricao);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM CARGAHORARIA WHERE DescricaoCarga='" + descricao + "'");
                conecta.rs.first();
                jIdCarga.setText(String.valueOf(conecta.rs.getInt("IdCarga")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusCarga"));
                jDataCadastro.setDate(conecta.rs.getDate("DataCad"));
                jDescricao.setText(conecta.rs.getString("DescricaoCarga"));
                jQtdDias.setText(conecta.rs.getString("QtdDias"));
                jQtdHoras.setText(conecta.rs.getString("QtdHoras"));
                jComboBoxMesReferencia.setSelectedItem(conecta.rs.getString("MesReferencia"));
                jAnoReferencia.setText(conecta.rs.getString("AnoReferencia"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa... " + e);
            }
        }
    }//GEN-LAST:event_jTabelaCargaHorariaMouseClicked

