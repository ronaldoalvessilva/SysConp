    public void preencherTabelaTarefas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Nome da Tarefa"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdTarefa"), conecta.rs.getString("StatusTarefa"), conecta.rs.getString("DescricaoTarefa")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaTarefas.setModel(modelo);
        jTabelaTarefas.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaTarefas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaTarefas.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaTarefas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaTarefas.getColumnModel().getColumn(2).setPreferredWidth(320);
        jTabelaTarefas.getColumnModel().getColumn(2).setResizable(false);
        jTabelaTarefas.setAutoResizeMode(jTabelaTarefas.AUTO_RESIZE_OFF);
        jTabelaTarefas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaCursos();
        conecta.desconecta();
    }

