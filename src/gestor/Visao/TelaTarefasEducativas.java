    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaTarefasEducativasManu_PEDA);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoPEDA.equals("ADMINISTRADORES") || codigoUserPEDA == codUserAcessoPEDA && nomeTelaPEDA.equals(telaTarefasEducativasManu_PEDA) && codGravarPEDA == 1) {
            if (jDataCadastro.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data do cadastro.");
                jDataCadastro.requestFocus();
                jDataCadastro.setBackground(Color.red);
            } else {
                if (jDescricaoTarefa.getText().equals("")) {
                    JOptionPane.showMessageDialog(rootPane, "Informe a descrição da tarefa.");
                } else {
                    objTarefa.setStatusTarefa((String) jComboBoxStatus.getSelectedItem());
                    objTarefa.setDataTarefa(jDataCadastro.getDate());
                    objTarefa.setDescricaoTarefa(jDescricaoTarefa.getText());
                    if (acao == 1) {
                        objTarefa.setUsuarioInsert(nameUser);
                        objTarefa.setDataInsert(dataModFinal);
                        objTarefa.setHorarioInsert(horaMov);
                        control.incluirTarefa(objTarefa);
                        buscarCod();
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                        Salvar();
                    }
                    if (acao == 2) {
                        objTarefa.setUsuarioUp(nameUser);
                        objTarefa.setDataUp(dataModFinal);
                        objTarefa.setHorarioUp(horaMov);
                        objTarefa.setIdTarefa(Integer.valueOf(jIdTarefa.getText()));
                        control.alterarTarefa(objTarefa);
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                        Salvar();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acesso não autorizado, solicite liberação ao administrador.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

