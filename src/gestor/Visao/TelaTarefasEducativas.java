    private void jTabelaTarefasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaTarefasMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String descricaoCurso = "" + jTabelaTarefas.getValueAt(jTabelaTarefas.getSelectedRow(), 2);
            jPesqDescricao.setText(descricaoCurso);
            String idCurso = "" + jTabelaTarefas.getValueAt(jTabelaTarefas.getSelectedRow(), 0);
            // Habilitar os botões
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM TAREFASDIVERSAS WHERE IdTarefa='" + idCurso + "'");
                conecta.rs.first();
                jIdTarefa.setText(conecta.rs.getString("IdTarefa"));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusTarefa"));
                jDataCadastro.setDate(conecta.rs.getDate("DataTarefa"));
                jDescricaoTarefa.setText(conecta.rs.getString("DescricaoTarefa"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro na pesquisa dos dados!!!" + ex);
            }
        }
    }//GEN-LAST:event_jTabelaTarefasMouseClicked


