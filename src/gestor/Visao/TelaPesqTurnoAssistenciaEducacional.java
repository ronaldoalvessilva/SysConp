    public void preencherTabelaInstituicao(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"CÃ³digo", "Nome do Turno de Aula"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdTurno"), conecta.rs.getString("DescricaoTurno")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados nÃ£o encontrado, use o botÃ£o TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaTurnosAulas.setModel(modelo);
        jTabelaTurnosAulas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaTurnosAulas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaTurnosAulas.getColumnModel().getColumn(1).setPreferredWidth(370);
        jTabelaTurnosAulas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaTurnosAulas.getTableHeader().setReorderingAllowed(false);
        jTabelaTurnosAulas.setAutoResizeMode(jTabelaTurnosAulas.AUTO_RESIZE_OFF);
        jTabelaTurnosAulas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaTurnos();
        conecta.desconecta();
    }

    public void limparTabela() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"CÃ³digo", "Nome do Turno de Aula"};
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaTurnosAulas.setModel(modelo);
        jTabelaTurnosAulas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaTurnosAulas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaTurnosAulas.getColumnModel().getColumn(1).setPreferredWidth(370);
        jTabelaTurnosAulas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaTurnosAulas.getTableHeader().setReorderingAllowed(false);
        jTabelaTurnosAulas.setAutoResizeMode(jTabelaTurnosAulas.AUTO_RESIZE_OFF);
        jTabelaTurnosAulas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        modelo.getLinhas().clear();
    }

    public void alinharCamposTabelaTurnos() {
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaTurnosAulas.getColumnModel().getColumn(0).setCellRenderer(centralizado);
    }
