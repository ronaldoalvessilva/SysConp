    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeInstituicao = "" + jTabelaTurnosAulas.getValueAt(jTabelaTurnosAulas.getSelectedRow(), 1);
            jPesqDescricaoTurno.setText(nomeInstituicao);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM TURNOSAULA WHERE DescricaoTurno='" + nomeInstituicao + "'");
                conecta.rs.first();
                jIdTurno.setText(conecta.rs.getString("IdTurno"));
                jDescricaoTurno.setText(conecta.rs.getString("DescricaoTurno"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados." + e);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

