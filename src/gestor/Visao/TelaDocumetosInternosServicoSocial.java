            String nomeInterno = "" + jTabelaInternosDocumentos.getValueAt(jTabelaInternosDocumentos.getSelectedRow(), 4);
            jPesqNomeInterno.setText(nomeInterno);
            String idInterno = "" + jTabelaInternosDocumentos.getValueAt(jTabelaInternosDocumentos.getSelectedRow(), 3);
