    public void preencherTabelaDocumentos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Data Doc.", "Código", "Nome do Interno"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);;
        try {
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data Entrada
                dataEntrada = conecta.rs.getString("DataDoc");
                String dia = dataEntrada.substring(8, 10);
                String mes = dataEntrada.substring(5, 7);
                String ano = dataEntrada.substring(0, 4);
                dataEntrada = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdDoc"), conecta.rs.getString("StatusDoc"), dataEntrada, conecta.rs.getInt("IdInternoCrc"), conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe dados a ser exibido!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaInternosDocumentos.setModel(modelo);
        jTabelaInternosDocumentos.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaInternosDocumentos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaInternosDocumentos.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaInternosDocumentos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaInternosDocumentos.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaInternosDocumentos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaInternosDocumentos.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTabelaInternosDocumentos.getColumnModel().getColumn(3).setResizable(false);
        jTabelaInternosDocumentos.getColumnModel().getColumn(4).setPreferredWidth(320);
        jTabelaInternosDocumentos.getColumnModel().getColumn(4).setResizable(false);
        jTabelaInternosDocumentos.getTableHeader().setReorderingAllowed(false);
        jTabelaInternosDocumentos.getTableHeader().setReorderingAllowed(false);
        jTabelaInternosDocumentos.setAutoResizeMode(jTabelaInternosDocumentos.AUTO_RESIZE_OFF);
        jTabelaInternosDocumentos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

