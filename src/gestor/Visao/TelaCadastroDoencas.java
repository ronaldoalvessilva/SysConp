    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (codigoUserENF == codUserAcessoENF && nomeTelaENF.equals(telaAcessoCadastroDoencasENF) && codGravarENF == 1 || nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoENF.equals("ADMINISTRADORES")) {
            if (jDataLanc.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro");
            } else {
                if (jDescricaoDoenca.getText().equals("")) {
                    JOptionPane.showMessageDialog(rootPane, "É necessário informar a descrição da doença.");
                    jDescricaoDoenca.requestFocus();
                    jDescricaoDoenca.setBackground(Color.red);
                } else {
                    objDoen.setDataLanc(jDataLanc.getDate());
                    objDoen.setDescricao(jDescricaoDoenca.getText());
                    objDoen.setCid(jCID.getText());
                    objDoen.setDescricaoDetalhada(jDescricaoDetalhada.getText());
                    if (acao == 1) {
                        //
                        objDoen.setUsuarioInsert(nameUser);
                        objDoen.setDataInsert(jDataSistema.getText());
                        objDoen.setHorarioInsert(jHoraSistema.getText());
                        //
                        control.incluirDoencas(objDoen);
                        buscarId();
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        Salvar();
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                    }
                    if (acao == 2) {
                        //
                        objDoen.setUsuarioUp(nameUser);
                        objDoen.setDataUp(jDataSistema.getText());
                        objDoen.setHorarioUp(jHoraSistema.getText());
                        //
                        objDoen.setIdLanc(Integer.valueOf(jIdLanc.getText()));
                        control.alterarDoencas(objDoen);
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        Salvar();
                        JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem acesso a gravar registro.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

