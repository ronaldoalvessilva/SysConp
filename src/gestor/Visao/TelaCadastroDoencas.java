    public void pesquisarTabelaDoencas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data ", "CID", "Descrição da Doença"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataBrasil = conecta.rs.getString("DataLanc");
                String dia = dataBrasil.substring(8, 10);
                String mes = dataBrasil.substring(5, 7);
                String ano = dataBrasil.substring(0, 4);
                dataBrasil = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdDoenca"), dataBrasil, conecta.rs.getString("Cid"), conecta.rs.getString("Descricao")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaDoencas.setModel(modelo);
        jTabelaDoencas.setRowSorter(new TableRowSorter(modelo)); //FAZER ORDENAMENTO NA TABLEA
        jTabelaDoencas.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaDoencas.getColumnModel().getColumn(0).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaDoencas.getColumnModel().getColumn(1).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTabelaDoencas.getColumnModel().getColumn(2).setResizable(false);
        jTabelaDoencas.getColumnModel().getColumn(3).setPreferredWidth(300);
        jTabelaDoencas.getColumnModel().getColumn(3).setResizable(false);
        jTabelaDoencas.setAutoResizeMode(jTabelaDoencas.AUTO_RESIZE_OFF);
        jTabelaDoencas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alimharCamposTabela();
        conecta.desconecta();
    }

