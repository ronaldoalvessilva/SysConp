    private void jBtImpressaoItensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtImpressaoItensActionPerformed
        // TODO add your handling code here:
        if (jIdLanc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Não é possível listar o relatório, pois,\no código do inventário está em banco.");
        } else {
            try {
                conecta.abrirConexao();
                String path = "reports/ListagemInventarioProdutos.jasper";
                conecta.executaSQL("SELECT *,"
                        + "INVENTARIO_FAR.IdLanc AS INVENTARIO_FAR_IdLanc, "
                        + "INVENTARIO_FAR.IdLocal AS INVENTARIO_FAR_IdLocal, "
                        + "INVENTARIO_FAR.Responsavel AS INVENTARIO_FAR_Responsavel, "
                        + "INVENTARIO_FAR.DataInicio AS INVENTARIO_FAR_DataInicio, "
                        + "LOCAL_ARMAZENAMENTO_AC.DescricaoLocal AS "
                        + "LOCAL_ARMAZENAMENTO_AC_DescricaoLocal, "
                        + "ITENS_INVENTARIO_FAR.IdProd AS ITENS_INVENTARIO_FAR_IdProd, "
                        + "PRODUTOS_AC.CodigoBarra AS PRODUTOS_AC_CodigoBarra, "
                        + "PRODUTOS_AC.DescricaoProd AS PRODUTOS_AC_DescricaoProd, "
                        + "PRODUTOS_AC.UnidadeProd AS PRODUTOS_AC_UnidadeProd, "
                        + "ITENS_INVENTARIO_FAR.QtdItem AS ITENS_INVENTARIO_FAR_QtdItem, "
                        + "ITENS_INVENTARIO_FAR.ValorCusto AS ITENS_INVENTARIO_FAR_ValorCusto "
                        + "FROM INVENTARIO_FAR "
                        + "INNER JOIN LOCAL_ARMAZENAMENTO_AC "
                        + "ON INVENTARIO_FAR.IdLocal=LOCAL_ARMAZENAMENTO_AC.IdLocal "
                        + "INNER JOIN ITENS_INVENTARIO_FAR ITENS_INVENTARIO_FAR "
                        + "ON INVENTARIO_FAR.IdLanc=ITENS_INVENTARIO_FAR.IdLanc "
                        + "INNER JOIN PRODUTOS_AC "
                        + "ON ITENS_INVENTARIO_FAR.IdProd=PRODUTOS_AC.IdProd "
                        + "WHERE ITENS_INVENTARIO_FAR.IdLanc='" + jIdLanc.getText() + "'");
                HashMap parametros = new HashMap();
                parametros.put("Idlanc", jIdLanc.getText());
                parametros.put("nomeUsuario", nameUser);
                JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio                                   
                JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao          
                jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                jv.setTitle("Listagem para Contagem de Estoque");
                jv.setVisible(true); // Chama o relatorio para ser visualizado                                    
                jv.toFront(); // Traz o relatorio para frente da aplicação            
                conecta.desconecta();
            } catch (JRException e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório \n\nERRO :" + e);
            }
        }
    }//GEN-LAST:event_jBtImpressaoItensActionPerformed

