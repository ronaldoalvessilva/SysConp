    public void verificarItens() {
        statusMov = "Excluiu";
        horaMov = jHoraSistema.getText();
        dataModFinal = jDataSistema.getText();
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM ITENSROL WHERE IdRol='" + jIDRol.getText() + "'");
            conecta.rs.first();
            codRol = conecta.rs.getString("IdRol");
            if (jIDRol.getText().equals(codRol)) {
                JOptionPane.showMessageDialog(rootPane, "Antes de excluir esse Rol, será necessário\nexcluir primeiro as Visitas relacionados a esse registro.");
            }
            conecta.desconecta();
        } catch (SQLException ex) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o ROL selecionado?", "Confirmação",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                objRol.setIdRol(Integer.parseInt(jIDRol.getText()));
                control.exclusaoRolVisitas(objRol);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                Excluir();
                JOptionPane.showMessageDialog(rootPane, "Registro EXCLUIDO com sucesso !!!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não foi possivel EXCLUIR Lançamento...\nERRO :" + ex);
            }
        }
    }

