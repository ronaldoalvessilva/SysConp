    public void pesquisarRequisicaoMateriais(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Titulo do Motivo"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data de emissão
                dataEmissao = conecta.rs.getString("DataMot");
                String dia = dataEmissao.substring(8, 10);
                String mes = dataEmissao.substring(5, 7);
                String ano = dataEmissao.substring(0, 4);
                dataEmissao = dia + "/" + mes + "/" + ano;
                //
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdMot"), dataEmissao, conecta.rs.getString("StatusMot"), conecta.rs.getString("TituloMotivo")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaMotivos.setModel(modelo);
        jTabelaMotivos.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaMotivos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaMotivos.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaMotivos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaMotivos.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTabelaMotivos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaMotivos.getColumnModel().getColumn(3).setPreferredWidth(300);
        jTabelaMotivos.getColumnModel().getColumn(3).setResizable(false);
        jTabelaMotivos.getTableHeader().setReorderingAllowed(false);
        jTabelaMotivos.setAutoResizeMode(jTabelaMotivos.AUTO_RESIZE_OFF);
        jTabelaMotivos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaMotivos();
        conecta.desconecta();
    }

