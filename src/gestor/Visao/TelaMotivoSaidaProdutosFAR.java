    private void jTabelaMotivosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaMotivosMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaMotivos.getValueAt(jTabelaMotivos.getSelectedRow(), 0);
            jCodigoMotivo.setText(IdLanc);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM MOTIVO_SAIDA_PRODUTOS_FAR "
                        + "WHERE IdMot='" + IdLanc + "'");
                conecta.rs.first();
                jIdMotivo.setText(String.valueOf(conecta.rs.getInt("IdMot")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusMot"));
                jDataMotivo.setDate(conecta.rs.getDate("DataMot"));
                jTituloMotivo.setText(conecta.rs.getString("TituloMotivo"));
                jTextoMotivo.setText(conecta.rs.getString("Motivo"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível exibir registro.\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaMotivosMouseClicked

