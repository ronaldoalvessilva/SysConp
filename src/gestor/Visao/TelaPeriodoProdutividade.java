    private void jBtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtConfirmarActionPerformed
        // TODO add your handling code here:
        if (jDataInicial.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data incial.");
            jDataInicial.requestFocus();
            jDataInicial.setBackground(Color.red);
        } else if (jDataFinal.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data final.");
            jDataFinal.requestFocus();
            jDataFinal.setBackground(Color.red);
        } else if (jDataInicial.getDate().after(jDataFinal.getDate())) {
            JOptionPane.showMessageDialog(rootPane, "Data Final n√£o pode ser menor que a data inicial.");
            jDataFinal.requestFocus();
            jDataFinal.setBackground(Color.red);
        } else {
            Produtividade objPrd = new Produtividade();
            objPrd.setVisible(true);
        }
    }//GEN-LAST:event_jBtConfirmarActionPerformed

