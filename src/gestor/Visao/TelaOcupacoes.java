    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdCodigoOcupa"), conecta.rs.getString("DescricaoOcupa")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe dados a ser exibido!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaOcupacoes.setModel(modelo);
        jTabelaOcupacoes.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaOcupacoes.getColumnModel().getColumn(0).setResizable(false);
        jTabelaOcupacoes.getColumnModel().getColumn(1).setPreferredWidth(320);
        jTabelaOcupacoes.getColumnModel().getColumn(1).setResizable(false);
        jTabelaOcupacoes.getTableHeader().setReorderingAllowed(false);
        jTabelaOcupacoes.setAutoResizeMode(jTabelaOcupacoes.AUTO_RESIZE_OFF);
        jTabelaOcupacoes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

