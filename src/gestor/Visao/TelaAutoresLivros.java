    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaAutoresManu_PEDA);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoPEDA.equals("ADMINISTRADORES") || codigoUserPEDA == codUserAcessoPEDA && nomeTelaPEDA.equals(telaAutoresManu_PEDA) && codGravarPEDA == 1) {
            if (jDataAutor.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro.");
            } else if (jNomeAutor.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o nome do autor.");
            } else {
                objAutor.setStatusAutor((String) jComboBoxStatusAutor.getSelectedItem());
                objAutor.setDataLanc(jDataAutor.getDate());
                objAutor.setNomeAutor(jNomeAutor.getText());
                objAutor.setNacionalidadeAutor(jNacionalidadeAutor.getText());
                objAutor.setPaisAutor(jPaisAutor.getText());
                objAutor.setObservacao(jObservacaoAutor.getText());
                if (acao == 1) {
                    objAutor.setUsuarioInsert(nameUser);
                    objAutor.setDataInsert(dataModFinal);
                    objAutor.setHorarioInsert(horaMov);
                    //
                    control.incluirAutorLivros(objAutor);
                    buscarCod();
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    Salvar();
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
                if (acao == 2) {
                    objAutor.setUsuarioUp(nameUser);
                    objAutor.setDataUp(dataModFinal);
                    objAutor.setHorarioUp(horaMov);
                    //
                    objAutor.setIdAutor(Integer.valueOf(jIdAutor.getText()));
                    control.alterarAutorLivros(objAutor);
                    Salvar();
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acesso não autorizado, solicite liberação ao administrador.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

