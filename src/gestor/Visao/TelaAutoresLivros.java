    private void jTabelaAutoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAutoresMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeAutor = "" + jTabelaAutores.getValueAt(jTabelaAutores.getSelectedRow(), 2);
            jPesqNomeAutor.setText(nomeAutor);
            jBtNovo.setEnabled(!true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM AUTORES_LIVROS WHERE NomeAutor='" + jPesqNomeAutor.getText() + "'");
                conecta.rs.first();
                jIdAutor.setText(String.valueOf(conecta.rs.getInt("IdAutor")));
                jComboBoxStatusAutor.setSelectedItem(conecta.rs.getString("StatusAutor"));
                jDataAutor.setDate(conecta.rs.getDate("DataLanc"));
                jNomeAutor.setText(conecta.rs.getString("NomeAutor"));
                jNacionalidadeAutor.setText(conecta.rs.getString("NacionalidadeAutor"));
                jPaisAutor.setText(conecta.rs.getString("PaisAutor"));
                jObservacaoAutor.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados...\nERRO: " + e);
            }
        }
    }//GEN-LAST:event_jTabelaAutoresMouseClicked


