    public void preencherTabelaAutores(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Nome do Autor"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                count = count + 1;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdAutor"), conecta.rs.getString("StatusAutor"), conecta.rs.getString("NomeAutor")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem exibidos....");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAutores.setModel(modelo);
        jTabelaAutores.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaAutores.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAutores.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaAutores.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAutores.getColumnModel().getColumn(2).setPreferredWidth(330);
        jTabelaAutores.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAutores.getTableHeader().setReorderingAllowed(false);
        jTabelaAutores.setAutoResizeMode(jTabelaAutores.AUTO_RESIZE_OFF);
        jTabelaAutores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaAutores();
        conecta.desconecta();
    }

