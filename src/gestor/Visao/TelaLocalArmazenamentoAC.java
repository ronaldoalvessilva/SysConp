    private void jTabelaLocalArmazenamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaLocalArmazenamentoMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String DescricaoLocal = "" + jTabelaLocalArmazenamento.getValueAt(jTabelaLocalArmazenamento.getSelectedRow(), 1);
            jPesqDescricaoLocal.setText(DescricaoLocal);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM LOCAL_ARMAZENAMENTO_AC "
                        + "WHERE DescricaoLocal='" + jPesqDescricaoLocal.getText() + "'");
                conecta.rs.first();
                jIDLocal.setText(String.valueOf(conecta.rs.getInt("IdLocal")));
                jComboBoxStatusLocal.setSelectedItem(conecta.rs.getString("StatusLocal"));
                jDescricaoLocal.setText(conecta.rs.getString("descricaoLocal"));
                nivelLocalFar = conecta.rs.getInt("NivelLocal");
                conecta.desconecta();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + e);
            }
            if (nivelLocalFar == 1) {
                jBtNovo.setEnabled(true);
                jBtAlterar.setEnabled(true);
                jBtExcluir.setEnabled(true);
                jBtSalvar.setEnabled(true);
                jBtCancelar.setEnabled(true);
                jBtAuditoria.setEnabled(true);
            } else if (nivelLocalFar == 2) {
                jBtNovo.setEnabled(true);
                jBtAlterar.setEnabled(!true);
                jBtExcluir.setEnabled(!true);
                jBtSalvar.setEnabled(!true);
                jBtCancelar.setEnabled(true);
                jBtAuditoria.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTabelaLocalArmazenamentoMouseClicked

