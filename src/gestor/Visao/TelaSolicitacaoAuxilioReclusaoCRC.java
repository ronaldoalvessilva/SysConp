    public void buscarCodigo() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM SOLICITACAO_ATESTADO_RECLUSAO_CRC");
            conecta.rs.last();
            jCodRegAux.setText(String.valueOf(conecta.rs.getInt("CodRegAux")));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel encontrar registro. \nERRO: " + ex);
        }
        conecta.desconecta();
    }

