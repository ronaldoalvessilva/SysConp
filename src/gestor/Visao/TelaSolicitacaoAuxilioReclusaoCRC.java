    private void jBtImpressaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtImpressaoActionPerformed
        // TODO add your handling code here:
        if (pDigitalCapturadaVisita == null || pDigitalCapturadaInterno == null) {
            assinaturaRel = "Não";
        } else {
            assinaturaRel = "Sim";
        }
        if (jCodRegAux.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Não existe registro a ser impresso.");
        } else {
            try {
                conecta.abrirConexao();
                String path = "reports/CRC/RelatorioSolicitacaoAtestadoReclusao.jasper";
                conecta.executaSQL("SELECT * FROM SOLICITACAO_ATESTADO_RECLUSAO_CRC "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON SOLICITACAO_ATESTADO_RECLUSAO_CRC.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN DADOSPENAISINTERNOS "
                        + "ON DADOSPENAISINTERNOS.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN VISITASINTERNO "
                        + "ON SOLICITACAO_ATESTADO_RECLUSAO_CRC.IdVisita=VISITASINTERNO.IdVisita "
                        + "WHERE CodRegAux='" + jCodRegAux.getText() + "'");
                HashMap parametros = new HashMap();
                parametros.put("idRegistro", jCodRegAux.getText());
                parametros.put("nomeUsuario", nameUser);
                parametros.put("nomeUnidade", descricaoUnidade);
                parametros.put("assinaturaBiometria", assinaturaRel);
                JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio                                   
                JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao
                jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                jv.setTitle("Relatório de Solicitação de Atestado de Reclusão");
                jv.setVisible(true); // Chama o relatorio para ser visualizado             
                jv.toFront(); // Traz o relatorio para frente da aplicação            
                conecta.desconecta();
            } catch (JRException e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório \n\nERRO :" + e);
            }
        }
    }//GEN-LAST:event_jBtImpressaoActionPerformed

