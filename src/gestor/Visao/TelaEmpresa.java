    public void formatarCampos() {
        jRazao.setDocument(new LimiteDigitosAlfa(250));
        jEndereco.setDocument(new LimiteDigitosAlfa(200));
        jCompl.setDocument(new LimiteDigitosAlfa(200));
        jCidade.setDocument(new LimiteDigitosAlfa(200));
        jEndCob.setDocument(new LimiteDigitosAlfa(200));
        jComplCob.setDocument(new LimiteDigitosAlfa(200));
        jCidadeCob.setDocument(new LimiteDigitosAlfa(200));
        try {
            //Formatação para números de telefones
            MaskFormatter telefone = new MaskFormatter("(###)-####-####");
            jTelefone.setFormatterFactory(new DefaultFormatterFactory(telefone));
            MaskFormatter telefone1 = new MaskFormatter("(###)-####-####");
            jTelefone1.setFormatterFactory(new DefaultFormatterFactory(telefone1));
            MaskFormatter celular = new MaskFormatter("(###)-####-####");
            jCelular.setFormatterFactory(new DefaultFormatterFactory(celular));
            MaskFormatter fax = new MaskFormatter("(###)-####-####");
            jFax.setFormatterFactory(new DefaultFormatterFactory(fax));
            MaskFormatter cnpj = new MaskFormatter("##.###.###/####-##");
            jCNPJ.setFormatterFactory(new DefaultFormatterFactory(cnpj));
            //Formataçõa par CEP
            MaskFormatter cep = new MaskFormatter("##.###-###");
            jCep.setFormatterFactory(new DefaultFormatterFactory(cep));
            MaskFormatter cepCob = new MaskFormatter("##.###-###");
            jCepCob.setFormatterFactory(new DefaultFormatterFactory(cepCob));
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possivel FORMATAR CAMPOS!!!\n\n\n" + ex);
        }
    }

