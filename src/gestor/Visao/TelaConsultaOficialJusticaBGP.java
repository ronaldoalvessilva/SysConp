    private void jTabelaOficialJusticaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaOficialJusticaMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeAdvogado = "" + jTabelaOficialJustica.getValueAt(jTabelaOficialJustica.getSelectedRow(), 1);
            jPesqNome.setText(nomeAdvogado);
            String idAdv = "" + jTabelaOficialJustica.getValueAt(jTabelaOficialJustica.getSelectedRow(), 0);
            jIdOficial.setText(idAdv);
            jPesqNomeInternoAdv.setEnabled(true);
            jBtPesqNomeInternoAdv.setEnabled(true);           
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM OFICIAL_JUSTICA "
                        + "WHERE NomeOficial='" + nomeAdvogado + "' "
                        + "AND IdOficial='" + idAdv + "'");
                conecta.rs.first();
                jIdOficial.setText(String.valueOf(conecta.rs.getInt("IdOficial")));
                jDataCadastro.setDate(conecta.rs.getDate("DataCadastro"));
                jNomeOficialJustica.setText(conecta.rs.getString("NomeOficial"));
                caminho = conecta.rs.getString("FotoOficial");
                javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                FotoOficialJustica.setIcon(i);
                FotoOficialJustica.setIcon(new ImageIcon(i.getImage().getScaledInstance(FotoOficialJustica.getWidth(), FotoOficialJustica.getHeight(), Image.SCALE_DEFAULT)));
                jRG.setText(conecta.rs.getString("RgOficial"));
                jCPF.setText(conecta.rs.getString("CpfOficial"));
                jREGOficial.setText(conecta.rs.getString("REGOficial"));
                jObsOficial.setText(conecta.rs.getString("ObsOficial"));
                conecta.desconecta();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + e);
            }
            preencherTabelaItens("SELECT * FROM VISITAS_OFICIAL_JUSTICA "
                    + "INNER JOIN PRONTUARIOSCRC "
                    + "ON VISITAS_OFICIAL_JUSTICA.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                    + "WHERE IdOficial='" + jIdOficial.getText() + "'");
        }
    }//GEN-LAST:event_jTabelaOficialJusticaMouseClicked

