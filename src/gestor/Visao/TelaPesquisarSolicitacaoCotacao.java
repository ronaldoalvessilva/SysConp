    public void preencherTabelaCotacoes(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código Sol.", "Data", "Código", "Descrição do Produto", "Unid.", "Qtd.", "Departamento"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                dataEntrada = conecta.rs.getString("DataSol");
                String diae = dataEntrada.substring(8, 10);
                String mese = dataEntrada.substring(5, 7);
                String anoe = dataEntrada.substring(0, 4);
                dataEntrada = diae + "/" + mese + "/" + anoe;
                // VALOR DEFINIDO PARA COMPRAS DO GRUPO.
                qtdItem = conecta.rs.getFloat("QtdItem");
                DecimalFormat vs = new DecimalFormat("#,##0.00");
                String vlSolicitacao = vs.format(qtdItem);
                qtdItem1 = vlSolicitacao;
                //                
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getString("IdSol"), dataEntrada, conecta.rs.getString("IdProd"), conecta.rs.getString("DescricaoProd"), conecta.rs.getString("UnidadeProd"), qtdItem1, conecta.rs.getString("NomeDepartamento")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem exibidos....");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaSolicitacoes.setModel(modelo);
        jTabelaSolicitacoes.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaSolicitacoes.getColumnModel().getColumn(0).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaSolicitacoes.getColumnModel().getColumn(1).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(2).setPreferredWidth(50);
        jTabelaSolicitacoes.getColumnModel().getColumn(2).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(3).setPreferredWidth(320);
        jTabelaSolicitacoes.getColumnModel().getColumn(3).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(4).setPreferredWidth(50);
        jTabelaSolicitacoes.getColumnModel().getColumn(4).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(5).setPreferredWidth(80);
        jTabelaSolicitacoes.getColumnModel().getColumn(5).setResizable(false);
        jTabelaSolicitacoes.getColumnModel().getColumn(6).setPreferredWidth(200);
        jTabelaSolicitacoes.getColumnModel().getColumn(6).setResizable(false);
        jTabelaSolicitacoes.setAutoResizeMode(jTabelaSolicitacoes.AUTO_RESIZE_OFF);
        jTabelaSolicitacoes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinhaColunasTabelaCotacoes();
        conecta.desconecta();
    }

