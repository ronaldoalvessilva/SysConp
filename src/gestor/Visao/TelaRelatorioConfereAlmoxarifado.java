    private void jBtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtConfirmarActionPerformed
        // TODO add your handling code here:
        if (jComboBoxPavilhao.getSelectedItem().equals("Selecione...")) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma galeria.");
        } else {
            final ViewAguardeProcessando carregando = new ViewAguardeProcessando(); //Teste tela aguarde
            carregando.setVisible(true);//Teste tela aguarde
            Thread t = new Thread() { //Teste tela aguarde
                public void run() { //Teste
                    try {
                        conecta.abrirConexao();
                        String path = "reports/Almoxarifado/Confere_ALMOXARIFADO.jasper";
                        conecta.executaSQL("SELECT * "
                                + "FROM ITENSLOCACAOINTERNO "
                                + "INNER JOIN PRONTUARIOSCRC "
                                + "ON  ITENSLOCACAOINTERNO.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                                + "INNER JOIN CELAS "
                                + "ON ITENSLOCACAOINTERNO.IdCela=CELAS.IdCela "
                                + "INNER JOIN PAVILHAO "
                                + "ON CELAS.IdPav=PAVILHAO.IdPav "
                                + "INNER JOIN DADOSPENAISINTERNOS "
                                + "ON PRONTUARIOSCRC.IdInternoCrc=DADOSPENAISINTERNOS.IdInternoCrc "
                                + "INNER JOIN DADOSFISICOSINTERNOS "
                                + "ON PRONTUARIOSCRC.IdInternoCrc=DADOSFISICOSINTERNOS.IdInternoCrc "
                                + "WHERE DescricaoPav='" + jComboBoxPavilhao.getSelectedItem() + "' "
                                + "ORDER BY CELAS.EndCelaPav,PRONTUARIOSCRC.NomeInternoCrc");
                        HashMap parametros = new HashMap();
                        parametros.put("nomeUsuario", nameUser);
                        parametros.put("descricaoUnidade", descricaoUnidade);
                        JRResultSetDataSource relatResul = new JRResultSetDataSource(conecta.rs); // Passa o resulSet Preenchido para o relatorio                                   
                        JasperPrint jpPrint = JasperFillManager.fillReport(path, parametros, relatResul); // indica o caminmhodo relatório
                        JasperViewer jv = new JasperViewer(jpPrint, false); // Cria instancia para impressao          
                        jv.setExtendedState(JasperViewer.MAXIMIZED_BOTH); // Maximizar o relatório
                        jv.setTitle("Relatório de Confere Por tamanho de calçado/Bermuda");
                        jv.setVisible(true); // Chama o relatorio para ser visualizado                                    
                        jv.toFront(); // Traz o relatorio para frente da aplicação            
                        conecta.desconecta();
                    } catch (JRException e) {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao chamar o Relatório \n\nERRO :" + e);
                    }
                    carregando.dispose(); //Teste tela aguarde
                }
            }; //Teste tela aguarde
            t.start(); //Teste tela aguarde  
        }
    }//GEN-LAST:event_jBtConfirmarActionPerformed

