    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        if (jPesqNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Selecione o nome do interno e clique no botão ENVIAR");
        } else {
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM PRONTUARIOSCRC "
                        + "INNER JOIN ITENS_LISTA_ROL "
                        + "ON PRONTUARIOSCRC.IdInternoCrc=ITENS_LISTA_ROL.IdInternocrc "
                        + "WHERE PRONTUARIOSCRC.NomeInternoCrc='" + nomeVisitaInterna + "' "
                        + "AND ITENS_LISTA_ROL.StatusInterna='" + statusInterna + "'");
                conecta.rs.first();
                jIDVisitaInterna.setText(String.valueOf(conecta.rs.getInt("IdInternoCrc")));
                jNomeVisitanteInterna.setText(conecta.rs.getString("NomeInternoCrc"));
                // Capturando foto
                caminhoVisita = conecta.rs.getString("FotoInternoCrc");
                javax.swing.ImageIcon v = new javax.swing.ImageIcon(caminhoVisita);
                jFotoVisitaInterno.setIcon(v);
                jFotoVisitaInterno.setIcon(new ImageIcon(v.getImage().getScaledInstance(jFotoVisitaInterno.getWidth(), jFotoVisitaInterno.getHeight(), Image.SCALE_DEFAULT)));
                jBtZoonInterna.setEnabled(true);
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "Existe restrição para essa visita, consulte o Rol de visitas\nou entre em contato com o serviço social.");
            }
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

