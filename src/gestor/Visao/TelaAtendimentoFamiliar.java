    public void preencherTodosAtendf(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Nome do Familiar", "Nome do Interno"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data Entrada
                dataAtendimento = conecta.rs.getString("DataAtendf");
                String dia = dataAtendimento.substring(8, 10);
                String mes = dataAtendimento.substring(5, 7);
                String ano = dataAtendimento.substring(0, 4);
                dataAtendimento = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdAtendf"), dataAtendimento, conecta.rs.getString("NomeVisita"), conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAtendimento.setModel(modelo);
        jTabelaAtendimento.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaAtendimento.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAtendimento.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaAtendimento.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAtendimento.getColumnModel().getColumn(2).setPreferredWidth(300);
        jTabelaAtendimento.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAtendimento.getColumnModel().getColumn(3).setPreferredWidth(300);
        jTabelaAtendimento.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAtendimento.getTableHeader().setReorderingAllowed(false);
        jTabelaAtendimento.setAutoResizeMode(jTabelaAtendimento.AUTO_RESIZE_OFF);
        jTabelaAtendimento.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinhaCelulasTabelaAtendimento();
        conecta.desconecta();
    }

