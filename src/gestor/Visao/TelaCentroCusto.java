    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jCodigoCentro.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o códig do centro de custo.");
        } else if (jDataCadastro.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro.");
        } else if (jComboBoxTipoCusto.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o tipo de centro de custo.");
        } else if (jDescricaoCentroCusto.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a descrição do centro de custo.");
        } else if (jComboBoxDepartamento.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe  qual é o departamento.");
        } else {
            objCentroCus.setCodigo(jCodigoCentro.getText());
            objCentroCus.setDataLanc(jDataCadastro.getDate());
            objCentroCus.setStatusCentro((String) jComboBoxStatus.getSelectedItem());
            objCentroCus.setTipoCusto((String) jComboBoxTipoCusto.getSelectedItem());
            objCentroCus.setDescricaoCentro(jDescricaoCentroCusto.getText());
            objCentroCus.setDescricaoDepartamento((String) jComboBoxDepartamento.getSelectedItem());
            objCentroCus.setObservacao(jObservacao.getText());
            if (acao == 1) {
                objCentroCus.setUsuarioInsert(nameUser);
                objCentroCus.setDataInsert(dataModFinal);
                objCentroCus.setHorarioInsert(horaMov);
                control.incluirCentroCusto(objCentroCus);
                buscarCodigo();
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
            if (acao == 2) {
                objCentroCus.setUsuarioUp(nameUser);
                objCentroCus.setDataUp(dataModFinal);
                objCentroCus.setHorarioUp(horaMov);
                objCentroCus.setIdCusto(Integer.valueOf(jIdCentro.getText()));
                control.alterarCentroCusto(objCentroCus);
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

