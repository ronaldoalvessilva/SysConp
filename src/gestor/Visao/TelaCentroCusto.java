    private void jTabelaCentroCustoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaCentroCustoMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeCentro = "" + jTabelaCentroCusto.getValueAt(jTabelaCentroCusto.getSelectedRow(), 1);
            jPesqDescricaoCentroCusto.setText(nomeCentro);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(!true);
            jBtAuditoria.setEnabled(true);
            jComboBoxDepartamento.removeAllItems();
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM CENTRO_CUSTO "
                        + "INNER JOIN DEPARTAMENTOS "
                        + "ON CENTRO_CUSTO.IdDepartamento=DEPARTAMENTOS.IdDepartamento "
                        + "WHERE DescricaoCentro='" + nomeCentro + "'");
                conecta.rs.first();
                jIdCentro.setText(String.valueOf(conecta.rs.getInt("IdCusto")));
                jCodigoCentro.setText(conecta.rs.getString("Codigo"));
                jDataCadastro.setDate(conecta.rs.getDate("DataLanc"));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusCentro"));
                jComboBoxTipoCusto.setSelectedItem(conecta.rs.getString("TipoCusto"));
                jDescricaoCentroCusto.setText(conecta.rs.getString("DescricaoCentro"));
                jComboBoxDepartamento.addItem(conecta.rs.getString("NomeDepartamento"));
                jObservacao.setText(conecta.rs.getString("Observacao"));               
            } catch (SQLException e) {
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaCentroCustoMouseClicked


