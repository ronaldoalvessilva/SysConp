    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jDescricaoFormaPagto.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a descrição para forma de pagamento.");
        } else if (jDataPagto.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data de cadastro.");
        } else if (jComboBoxStatus.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe se a forma de pagamento está ativa ou inativa.");
        } else {
            objForma.setDataForma(jDataPagto.getDate());
            objForma.setStatusForma((String) jComboBoxStatus.getSelectedItem());
            objForma.setDescricaoForma(jDescricaoFormaPagto.getText());
            if (acao == 1) {
                objForma.setUsuarioInsert(nameUser);
                objForma.setDataInsert(dataModFinal);
                objForma.setHorarioInsert(horaMov);
                control.incluirFormaPagamento(objForma);
                buscarCodigo();
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
            if (acao == 2) {
                objForma.setUsuarioUp(nameUser);
                objForma.setDataUp(dataModFinal);
                objForma.setHorarioUp(horaMov);
                objForma.setIdForma(Integer.valueOf(jCodigoPagto.getText()));
                control.alterarFormaPagamento(objForma);
                Salvar();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

