    private void jTabelaVisitasDiversasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaVisitasDiversasMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeColaborador = "" + jTabelaVisitasDiversas.getValueAt(jTabelaVisitasDiversas.getSelectedRow(), 1);
            jNomeVisitante.setText(nomeColaborador);
            idItem = "" + jTabelaVisitasDiversas.getValueAt(jTabelaVisitasDiversas.getSelectedRow(), 2);
            String horarioEntrada = "" + jTabelaVisitasDiversas.getValueAt(jTabelaVisitasDiversas.getSelectedRow(), 3);
            jHorarioEntrada.setText(horarioEntrada);
            String horarioSaida = "" + jTabelaVisitasDiversas.getValueAt(jTabelaVisitasDiversas.getSelectedRow(), 5);
            jHorarioSaida.setText(horarioSaida);
            // Habilitar os botões
            jBtZoonVisitaDiversas.setEnabled(true);
            jBtNovaVisita.setEnabled(!true);
            jBtAlterarVisita.setEnabled(true);
            jBtExcluirVisita.setEnabled(true);
            jBtSalvarVisita.setEnabled(!true);
            jBtCancelarVisita.setEnabled(true);
            jBtBuscarVisita.setEnabled(true);
            jBtAuditoriaVisitasDiversas.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ITENSVISITASDIVERSAS "
                        + "INNER JOIN VISITASDIVERSAS "
                        + "ON ITENSVISITASDIVERSAS.IdVisita=VISITASDIVERSAS.IdVisita "
                        + "INNER JOIN DEPARTAMENTOS "
                        + "ON ITENSVISITASDIVERSAS.IdDepartamento=DEPARTAMENTOS.IdDepartamento "
                        + "WHERE NomeVisita='" + jNomeVisitante.getText() + "' "
                        + "AND IdLanc='" + jIDlanc.getText() + "' "
                        + "AND HorarioEntrada='" + jHorarioEntrada.getText() + "' "
                        + "AND HorarioSaida='" + jHorarioSaida.getText() + "'");
                conecta.rs.first();
                jIDVisita.setText(conecta.rs.getString("IdVisita")); //Coluna 0
                jNomeVisitante.setText(conecta.rs.getString("NomeVisita")); // Coluna 1
                // Capturando foto
                caminho = conecta.rs.getString("FotoVisita");
                if (caminho != null) {
                    javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminho);
                    FotoVisitasDiversas.setIcon(i);
                    FotoVisitasDiversas.setIcon(new ImageIcon(i.getImage().getScaledInstance(FotoVisitasDiversas.getWidth(), FotoVisitasDiversas.getHeight(), Image.SCALE_DEFAULT)));
                }
                // BUSCAR A FOTO DO ADVOGADO NO BANCO DE DADOS
                byte[] img2Bytes = ((byte[]) conecta.rs.getBytes("ImagemFrenteVI"));
                if (img2Bytes != null) {
                    ImageIcon pic2 = null;
                    pic2 = new ImageIcon(img2Bytes);
                    Image scaled2 = pic2.getImage().getScaledInstance(FotoVisitasDiversas.getWidth(), FotoVisitasDiversas.getHeight(), Image.SCALE_DEFAULT);
                    ImageIcon icon2 = new ImageIcon(scaled2);
                    FotoVisitasDiversas.setIcon(icon2);
                }
                idItem = conecta.rs.getString("IdItem"); // Coluna 2
                jIDDepto.setText(conecta.rs.getString("IdDepartamento"));
                jDepartamento.setText(conecta.rs.getString("NomeDepartamento"));
                jMotivoVisita.setText(conecta.rs.getString("MotivoVisita"));
                jDataEntrada.setDate(conecta.rs.getDate("DataEntrada"));
                jDataSaida.setDate(conecta.rs.getDate("DataSaida"));
                jHorarioEntrada.setText(conecta.rs.getString("HorarioEntrada"));
                jHorarioSaida.setText(conecta.rs.getString("HorarioSaida"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não existe dados a serem alterado!!!" + ex);
            }
        }
    }//GEN-LAST:event_jTabelaVisitasDiversasMouseClicked

