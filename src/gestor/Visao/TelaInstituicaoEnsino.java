    private void jTabelaInstituicaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaInstituicaoMouseClicked
        // TODO add your handling code here:
        String codInst = "";  // para limpar a tabela de 
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaInstituicao.getValueAt(jTabelaInstituicao.getSelectedRow(), 0);
            jIdPesq.setText(IdLanc);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM INSTITUICAOESCOLAR WHERE IdCod ='" + IdLanc + "'");
                conecta.rs.first();
                jCodigo.setText(String.valueOf(conecta.rs.getInt("IdCod")));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusInst"));
                jDataCadastro.setDate(conecta.rs.getDate("Datacad"));
                jNomeInstituicao.setText(conecta.rs.getString("NomeInstituicao"));
                jEnderecoInstituicao.setText(conecta.rs.getString("Endereco"));
                jCidadeInst.setText(conecta.rs.getString("Cidade"));
                jComboBoxEstado.setSelectedItem(conecta.rs.getString("Estado"));
                jTelefone.setText(conecta.rs.getString("Telefone"));
                jTelefone1.setText(conecta.rs.getString("Telefone1"));
                jCelular.setText(conecta.rs.getString("Celular"));
                jCelular1.setText(conecta.rs.getString("Celular1"));
                jEmail.setText(conecta.rs.getString("Email"));
                jContato.setText(conecta.rs.getString("Contato"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa... " + e);
            }
            HistoricoEscolarExterno("SELECT * FROM PRONTUARIOSCRC "
                    + "INNER JOIN HISTORICO_EXTERNO_EDUCACIONAL "
                    + "ON PRONTUARIOSCRC.IdInternoCrc=HISTORICO_EXTERNO_EDUCACIONAL.IdInternoCrc "
                    + "WHERE IdCod='" + codInst + "'");
            HistoricoEscolarInterno("SELECT * FROM PRONTUARIOSCRC "
                    + "INNER JOIN HISTORICO_INTERNO_EDUCACIONAL "
                    + "ON PRONTUARIOSCRC.IdInternoCrc=HISTORICO_INTERNO_EDUCACIONAL.IdInternoCrc "
                    + "WHERE IdCod='" + codInst + "'");
        }
    }//GEN-LAST:event_jTabelaInstituicaoMouseClicked

