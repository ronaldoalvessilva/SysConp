    public void preencherTodasEntradas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Nome da Instituição"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataCadastro = conecta.rs.getString("DataCad");
                String dia = dataCadastro.substring(8, 10);
                String mes = dataCadastro.substring(5, 7);
                String ano = dataCadastro.substring(0, 4);
                dataCadastro = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdCod"), dataCadastro, conecta.rs.getString("StatusInst"), conecta.rs.getString("NomeInstituicao")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaInstituicao.setModel(modelo);
        jTabelaInstituicao.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaInstituicao.getColumnModel().getColumn(0).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaInstituicao.getColumnModel().getColumn(1).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaInstituicao.getColumnModel().getColumn(2).setResizable(false);
        jTabelaInstituicao.getColumnModel().getColumn(3).setPreferredWidth(320);
        jTabelaInstituicao.getColumnModel().getColumn(3).setResizable(false);
        jTabelaInstituicao.getTableHeader().setReorderingAllowed(false);
        jTabelaInstituicao.setAutoResizeMode(jTabelaInstituicao.AUTO_RESIZE_OFF);
        jTabelaInstituicao.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabelaMatriculas();
        conecta.desconecta();
    }

