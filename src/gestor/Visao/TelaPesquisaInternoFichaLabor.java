    public void pesquisarInternosLaborativas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"ID", "   Nome Completo do Interno"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdInternoCrc"), conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaPesqInternosLabor.setModel(modelo);
        jTabelaPesqInternosLabor.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaPesqInternosLabor.getColumnModel().getColumn(0).setResizable(false);
        jTabelaPesqInternosLabor.getColumnModel().getColumn(1).setPreferredWidth(300);
        jTabelaPesqInternosLabor.getColumnModel().getColumn(1).setResizable(false);
        jTabelaPesqInternosLabor.getTableHeader().setReorderingAllowed(false);
        jTabelaPesqInternosLabor.setAutoResizeMode(jTabelaPesqInternosLabor.AUTO_RESIZE_OFF);
        jTabelaPesqInternosLabor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.desconecta();
    }
