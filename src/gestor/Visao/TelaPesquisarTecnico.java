    private void jBtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtEnviarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String nomeUser = "" + jTabelaUsuarios.getValueAt(jTabelaUsuarios.getSelectedRow(), 1);
            jNomeUsuario.setText(nomeUser);
            //                
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM USUARIOS "
                        + "WHERE NomeUsuario='" + nomeUser + "'");
                conecta.rs.first();
                if (jRBtAssistente.isSelected()) {
                    jTecnicoServicoSocial.setText(conecta.rs.getString("NomeUsuario"));
                } else if (jRBtPsicologo.isSelected()) {
                    jTecnicoPsicologico.setText(conecta.rs.getString("NomeUsuario"));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível listar usuario.\nERRO: " + ex);
            }
            conecta.desconecta();
            dispose();
        }
    }//GEN-LAST:event_jBtEnviarActionPerformed

