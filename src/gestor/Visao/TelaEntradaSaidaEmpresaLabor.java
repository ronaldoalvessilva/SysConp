    private void jTabelaAtividadeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAtividadeMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String nomeEmpresa = "" + jTabelaAtividade.getValueAt(jTabelaAtividade.getSelectedRow(), 1);
            jPesqDescricaoEmpresa.setText(nomeEmpresa);
            String idLanc = "" + jTabelaAtividade.getValueAt(jTabelaAtividade.getSelectedRow(), 0);
            jIDLanc.setText(idLanc);
            //
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtNovoInterno.setEnabled(true);
            jBtBuscarRegistro.setEnabled(true);
            jBtAuditoriaManu.setEnabled(true);
            //
            limparCampos();
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM FICHALABORATIVA "
                        + "INNER JOIN EMPRESALAB "
                        + "ON FICHALABORATIVA.IdEmp=EMPRESALAB.IdEmp "
                        + "WHERE RazaoSocial LIKE'" + jPesqDescricaoEmpresa.getText() + "%'");
                conecta.rs.first();
                jIDLanc.setText(String.valueOf(conecta.rs.getInt("IdLanc")));
                jDataLanc.setDate(conecta.rs.getDate("DataLanc"));
                jIdEmpresa.setText(conecta.rs.getString("IdEmp"));
                jRazaoSocial.setText(conecta.rs.getString("RazaoSocial"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + e);
            }
            preencherTabelaInternosLab("SELECT * FROM ITENSFICHALAB "
                    + "INNER JOIN PRONTUARIOSCRC "
                    + "ON ITENSFICHALAB.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                    + "WHERE IdLanc='" + idLanc + "'");
        }
    }//GEN-LAST:event_jTabelaAtividadeMouseClicked

