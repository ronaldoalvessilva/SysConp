    private void jBtFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtFinalizarActionPerformed
        // TODO add your handling code here:
        Integer rows = jTabelaVisita.getModel().getRowCount();
        if (rows == 0) {
            JOptionPane.showMessageDialog(rootPane, "Não é possível finalizar registro. A tabela de visitas encontra-se vazia.");
        } else {
            statusMov = "Finalizou";
            horaMov = jHoraSistema.getText();
            dataModFinal = jDataSistema.getText();
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ROL_VISITAS_RELIGIOSA WHERE IdRol='" + jIDRol.getText() + "'");
                conecta.rs.first();
                jStatusRol.setText(conecta.rs.getString("StatusRol"));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não foi possível verificar se lançamento foi finalizado\nERRO: " + ex);
            }
            if (jStatusRol.getText().equals("FINALIZADO")) {
                JOptionPane.showMessageDialog(rootPane, "Lançamento já foi finalizado");
            } else {
                String statusRol = "FINALIZADO";
                JOptionPane.showMessageDialog(rootPane, "Se esse Rol for finalizado,você não\n poderá mais excluir ou alterar.");
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente FINALIZAR o ROL selecionado?", "Confirmação",
                        JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    objRol.setStatusRol(statusRol);
                    objRol.setIdRol(Integer.parseInt(jIDRol.getText()));
                    control.finalizarRolVisitasReligiosas(objRol);
                    jStatusRol.setText(statusRol);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    JOptionPane.showMessageDialog(rootPane, "Registro FINALIZADO com sucesso !!!");
                    jDataRol.setEnabled(!true);
                    jBtNovo.setEnabled(true);
                    jBtAlterar.setEnabled(!true);
                    jBtExcluir.setEnabled(!true);
                    jBtSalvar.setEnabled(!true);
                    jBtCancelar.setEnabled(!true);
                    jBtFinalizar.setEnabled(!true);
                }
            }
        }
        conecta.desconecta();
    }//GEN-LAST:event_jBtFinalizarActionPerformed

