                jStatusRol.setText(conecta.rs.getString("StatusRol"));
                jDataRol.setDate(conecta.rs.getDate("DataRol"));
                jIDInstituicaoReligiosa.setText(conecta.rs.getString("IdCod"));
                jNomeInstituicaoReligiosa.setText(conecta.rs.getString("NomeInstituicao"));
                domingo = conecta.rs.getInt("Domingo");
                if (domingo == 1) {
                    jDomingoInstituicao.setSelected(true);
                } else if (domingo == 0) {
                    jDomingoInstituicao.setSelected(!true);
                }
                segunda = conecta.rs.getInt("Segunda");
                if (segunda == 1) {
                    jSegundaInstituicao.setSelected(true);
                } else if (segunda == 0) {
                    jSegundaInstituicao.setSelected(!true);
                }
                terca = conecta.rs.getInt("Terca");
                if (terca == 1) {
                    jTercaInstituicao.setSelected(true);
                } else if (terca == 0) {
                    jTercaInstituicao.setSelected(!true);
                }
                quarta = conecta.rs.getInt("Quarta");
