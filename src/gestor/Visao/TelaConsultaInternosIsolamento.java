    private void jTabelaInternoAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaInternoAutorMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String idReg = "" + jTabelaInternoAutor.getValueAt(jTabelaInternoAutor.getSelectedRow(), 0);
            jRegistro.setText(idReg);
            String nomeInterno = "" + jTabelaInternoAutor.getValueAt(jTabelaInternoAutor.getSelectedRow(), 1);
            jNomeInterno.setText(nomeInterno);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM AUTORES_REGIMENTO_DISCIPLINAR "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON AUTORES_REGIMENTO_DISCIPLINAR.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "INNER JOIN REGIMENTO_DISCIPLINAR_INTERNOS "
                        + "ON AUTORES_REGIMENTO_DISCIPLINAR.IdReg=REGIMENTO_DISCIPLINAR_INTERNOS.IdReg "
                        + "INNER JOIN CELAS "
                        + "ON AUTORES_REGIMENTO_DISCIPLINAR.IdCela=CELAS.IdCela "
                        + "INNER JOIN PAVILHAO ON CELAS.IdPav=PAVILHAO.IdPav "
                        + "INNER JOIN OCORRENCIA_AUTORES "
                        + "ON AUTORES_REGIMENTO_DISCIPLINAR.IdInternoCrc=OCORRENCIA_AUTORES.IdInternoCrc "
                        + "WHERE PRONTUARIOSCRC.NomeInternoCrc='" + jNomeInterno.getText() + "' "
                        + "AND AUTORES_REGIMENTO_DISCIPLINAR.IdReg='" + idReg + "'");
                conecta.rs.first();
                jRegistro.setText(conecta.rs.getString("IdReg"));
                jIdInterno.setText(conecta.rs.getString("IdInternoCrc"));
                jNomeInterno.setText(conecta.rs.getString("NomeInternoCrc"));
                jAlcunha.setText(conecta.rs.getString("AlcunhaCrc"));
                jNomeMaeInterno.setText(conecta.rs.getString("MaeInternoCrc"));
                jPavilhao.setText(conecta.rs.getString("DescricaoPav"));
                jCela.setText(conecta.rs.getString("EndCelaPav"));
                jQtdDias.setText(String.valueOf(conecta.rs.getInt("QtdeDias")));
                jDataInicio.setDate(conecta.rs.getDate("DataInicio"));
                jDataTermino.setDate(conecta.rs.getDate("DataTermino"));
                jOcorrencia.setText(conecta.rs.getString("IdOcr"));
                //
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Não existe dados a serem exibidos!!!" + ex);
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaInternoAutorMouseClicked

