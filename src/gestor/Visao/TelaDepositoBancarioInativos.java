                            objLog();
                            controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                            objDeposito();
                            totalGeralAnterior = saldoAnterior - valorAnterior;
                            totalGeral = objSaldo.getSaldo() + totalGeralAnterior;
