    public void pesquisarData(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "PA","Evolução Psiquiatrica"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                dataMov = conecta.rs.getString("DataEvol");
                String dia = dataMov.substring(8, 10);
                String mes = dataMov.substring(5, 7);
                String ano = dataMov.substring(0, 4);
                dataMov = dia + "/" + mes + "/" + ano;
                dados.add(new Object[]{conecta.rs.getInt("IdItem"), dataMov, conecta.rs.getString("Patologia"),conecta.rs.getString("EvolucaoPsiquiatrica")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaDatasEvolPsiquiatrica.setModel(modelo);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(0).setResizable(false);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(1).setResizable(false);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(2).setResizable(false);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(3).setPreferredWidth(350);
        jTabelaDatasEvolPsiquiatrica.getColumnModel().getColumn(3).setResizable(false);
        jTabelaDatasEvolPsiquiatrica.getTableHeader().setReorderingAllowed(false);
        jTabelaDatasEvolPsiquiatrica.setAutoResizeMode(jTabelaDatasEvolPsiquiatrica.AUTO_RESIZE_OFF);
        jTabelaDatasEvolPsiquiatrica.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }
