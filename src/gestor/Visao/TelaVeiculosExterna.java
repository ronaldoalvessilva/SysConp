    public void VerificarEntVisitas() {
        statusMov = "Excluiu";
        horaMov = jHoraSistema.getText();
        dataModFinal = jDataSistema.getText();
        objVeic.setIdVeiculo(Integer.valueOf(jIDVeiculo.getText()));
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM ITENSVEICULOSUNIDADE WHERE IdVeiculo='" + jIDVeiculo.getText() + "'");
            conecta.rs.first();
            codVeiculo = conecta.rs.getString("IdVeiculo");
            conecta.desconecta();
        } catch (SQLException ex) {
        }
        if (jIDVeiculo.getText().equals(codVeiculo)) {
            JOptionPane.showMessageDialog(rootPane, "Esse registro não poderá ser excluido, o mesmo está relacionado a ENTRADA DE VEICULOS DA UNIDADE!!!");
        } else {
            try {
                conecta.executaSQL("SELECT * FROM ENTRADAVEICULOCARGA WHERE IdVeiculo='" + jIDVeiculo.getText() + "'");
                conecta.rs.first();
                codVeiculo = conecta.rs.getString("IdVeiculo");
                conecta.desconecta();
            } catch (SQLException ex) {
            }
            if (jIDVeiculo.getText().equals(codVeiculo)) {
                JOptionPane.showMessageDialog(rootPane, "Esse registro não poderá ser excluido, o mesmo está relacionado ao documento de ENTRADA DE VEICULOS DE CARGAS!!!");
            } else {
                try {
                    conecta.executaSQL("SELECT * FROM ITENSVEICULOSTERCEIRO WHERE IdVeiculo='" + jIDVeiculo.getText() + "'");
                    conecta.rs.first();
                    codVeiculo = conecta.rs.getString("IdVeiculo");
                    conecta.desconecta();
                } catch (SQLException ex) {
                }
                if (jIDVeiculo.getText().equals(codVeiculo)) {
                    JOptionPane.showMessageDialog(rootPane, "Esse registro não poderá ser excluido, o mesmo está relacionado ao documento de ENTRADA DE VEICULOS DE TERCEIROS!!!");
                } else {
                    int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir VEICULO selecionado?", "Confirmação",
                            JOptionPane.YES_NO_OPTION);
                    if (resposta == JOptionPane.YES_OPTION) {
                        control.excluirVeiculos(objVeic);
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(rootPane, "Registro EXCLUIDO com sucesso !!!");
                        Excluir();
                    }
                    conecta.desconecta();
                }
            }
        }
    }
