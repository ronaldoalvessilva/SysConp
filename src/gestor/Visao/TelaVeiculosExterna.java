    private void jTabelaVeiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaVeiculosMouseClicked
        // TODO add your handling code here:
        if (flag == 1) {
            String modeloVeiculo = "" + jTabelaVeiculos.getValueAt(jTabelaVeiculos.getSelectedRow(), 1);
            jPesqDescricao.setText(modeloVeiculo);
            String idAdv = "" + jTabelaVeiculos.getValueAt(jTabelaVeiculos.getSelectedRow(), 0);
            jIDVeiculo.setText(idAdv);
            //
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM VEICULOS "
                        + "WHERE ModeloVeiculo='" + modeloVeiculo + "' "
                        + "AND IdVeiculo='" + idAdv + "'");
                conecta.rs.first();
                jIDVeiculo.setText(String.valueOf(conecta.rs.getInt("IdVeiculo")));
                jDataCadastro.setDate(conecta.rs.getDate("DataCadastro"));
                jComboBoxStatus.setSelectedItem(conecta.rs.getString("StatusVei"));
                jModeloVeiculo.setText(conecta.rs.getString("ModeloVeiculo"));
                jMarcaVeiculo.setText(conecta.rs.getString("MarcaVeiculo"));
                jPlacaVeiculo.setText(conecta.rs.getString("PlacaVeiculo"));
                jCidade.setText(conecta.rs.getString("CidadeVeiculo"));
                jEstado.setText(conecta.rs.getString("EstadoVeiculo"));
                // Capturando foto frente
                caminhoFotoFrente = conecta.rs.getString("FotoFrente");
                javax.swing.ImageIcon i = new javax.swing.ImageIcon(caminhoFotoFrente);
                jFotoVeiculoUniFrente.setIcon(i);
                jFotoVeiculoUniFrente.setIcon(new ImageIcon(i.getImage().getScaledInstance(jFotoVeiculoUniFrente.getWidth(), jFotoVeiculoUniFrente.getHeight(), Image.SCALE_DEFAULT)));
                // Capturando foto Lado direito
                caminhoFotoLadoDireito = conecta.rs.getString("FotoLadoDireito");
                if (caminhoFotoLadoDireito != null) {
                    javax.swing.ImageIcon d = new javax.swing.ImageIcon(caminhoFotoLadoDireito);
                    jFotoVeiculoUnidLadoDireito.setIcon(d);
                    jFotoVeiculoUnidLadoDireito.setIcon(new ImageIcon(d.getImage().getScaledInstance(jFotoVeiculoUnidLadoDireito.getWidth(), jFotoVeiculoUnidLadoDireito.getHeight(), Image.SCALE_DEFAULT)));
                }
                // Capturando foto Lado esquerdo
                CaminhoFotoLadoEsquerdo = conecta.rs.getString("FotoLadoEsquerdo");
                if (CaminhoFotoLadoEsquerdo != null) {
                    javax.swing.ImageIcon e = new javax.swing.ImageIcon(CaminhoFotoLadoEsquerdo);
                    jFotoVeiculoUnidLadoEsquerdo.setIcon(e);
                    jFotoVeiculoUnidLadoEsquerdo.setIcon(new ImageIcon(e.getImage().getScaledInstance(jFotoVeiculoUnidLadoEsquerdo.getWidth(), jFotoVeiculoUnidLadoEsquerdo.getHeight(), Image.SCALE_DEFAULT)));
                }
                // Capturando foto fundo
                caminhoFotoFundo = conecta.rs.getString("FotoFundo");
                if (caminhoFotoFundo != null) {
                    javax.swing.ImageIcon u = new javax.swing.ImageIcon(caminhoFotoFundo);
                    jFotoVeiculoUnidFundo.setIcon(u);
                    jFotoVeiculoUnidFundo.setIcon(new ImageIcon(u.getImage().getScaledInstance(jFotoVeiculoUnidFundo.getWidth(), jFotoVeiculoUnidFundo.getHeight(), Image.SCALE_DEFAULT)));
                }
                conecta.desconecta();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + e);
            }
        }
    }//GEN-LAST:event_jTabelaVeiculosMouseClicked

