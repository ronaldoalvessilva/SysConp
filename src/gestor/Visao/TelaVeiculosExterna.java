    public void pesquisarTodosPertences(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição", "Placa", "Data"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);;
        try {
            conecta.rs.first();
            do {
                count = count + 1;
                // Fortmatar data de Cadastro          
                dataCadastro = conecta.rs.getString("DataCadastro");
                String day = dataCadastro.substring(8, 10);
                String mesc = dataCadastro.substring(5, 7);
                String anoc = dataCadastro.substring(0, 4);
                dataCadastro = day + "/" + mesc + "/" + anoc;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdVeiculo"), conecta.rs.getString("ModeloVeiculo"), conecta.rs.getString("PlacaVeiculo"), dataCadastro});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe dados a ser exibido!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaVeiculos.setModel(modelo);
        jTabelaVeiculos.getColumnModel().getColumn(0).setPreferredWidth(52);
        jTabelaVeiculos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaVeiculos.getColumnModel().getColumn(1).setPreferredWidth(340);
        jTabelaVeiculos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaVeiculos.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaVeiculos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaVeiculos.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTabelaVeiculos.getColumnModel().getColumn(3).setResizable(false);
        jTabelaVeiculos.getTableHeader().setReorderingAllowed(false);
        jTabelaVeiculos.setAutoResizeMode(jTabelaVeiculos.AUTO_RESIZE_OFF);
        jTabelaVeiculos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

