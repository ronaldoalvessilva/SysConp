    public void preencherTabelaLocalEstoque(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Status", "Descrição da Falta"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdNatureza"), conecta.rs.getString("StatusNatureza"), conecta.rs.getString("DescricaoNatureza")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaNaturezaEventos.setModel(modelo);
        jTabelaNaturezaEventos.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaNaturezaEventos.getColumnModel().getColumn(0).setResizable(false);
        jTabelaNaturezaEventos.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaNaturezaEventos.getColumnModel().getColumn(1).setResizable(false);
        jTabelaNaturezaEventos.getColumnModel().getColumn(2).setPreferredWidth(380);
        jTabelaNaturezaEventos.getColumnModel().getColumn(2).setResizable(false);
        jTabelaNaturezaEventos.getTableHeader().setReorderingAllowed(false);
        jTabelaNaturezaEventos.setAutoResizeMode(jTabelaNaturezaEventos.AUTO_RESIZE_OFF);
        jTabelaNaturezaEventos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharTabela();
        conecta.desconecta();
    }

