    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jDescricaoSimplesFata.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a descrição do local de armazenamento.");
            jDescricaoSimplesFata.requestFocus();
            jDescricaoSimplesFata.setBackground(Color.red);
        } else {
            objNaturezaEven.setStatusNatureza((String) jComboBoxStatus.getSelectedItem());
            objNaturezaEven.setDescricaoNatureza(jDescricaoSimplesFata.getText());
            objNaturezaEven.setTipoFalta((String) jComboBoxTipoFalta.getSelectedItem());
            objNaturezaEven.setDescrucaoDetalhada(jTextoCompletoFalta.getText());
            if (acao == 1) {
                objNaturezaEven.setUsuarioInsert(nameUser);
                objNaturezaEven.setDataInsert(dataModFinal);
                objNaturezaEven.setHorarioInsert(horaMov);
                control.incluirNaturezaEvento(objNaturezaEven);
                buscarCodigo();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                Salvar();
            }
            if (acao == 2) {
                objNaturezaEven.setUsuarioUp(nameUser);
                objNaturezaEven.setDataUp(dataModFinal);
                objNaturezaEven.setHorarioUp(horaMov);
                objNaturezaEven.setIdNatureza(Integer.valueOf(jIdNatureza.getText()));
                control.alterarNaturezaEvento(objNaturezaEven);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                Salvar();
            }
        }

    }//GEN-LAST:event_jBtSalvarActionPerformed

