    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        if (jDataFicha.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Informe a data da ficha técnica.");
        } else if (jNomeFicha.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o nome da ficha técnica.");
        } else if (jDescricaoCategoria.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a categoria da ficha técnica.");
        } else {
            objFicha.setStatusFicha((String) jComboBoxStatus.getSelectedItem());
            objFicha.setDataFicha(jDataFicha.getDate());
            objFicha.setDescricaoFicha(jNomeFicha.getText());
            objFicha.setDescricaoCategoria(jDescricaoCategoria.getText());
            objFicha.setCaminhoFoto(caminhoFoto);
            objFicha.setPorcoes(Integer.valueOf(jPorcoes.getText()));
            objFicha.setModoFazer(jModoPreparar.getText());
            objFicha.setObservacao(jObservacao.getText());
            if (acao == 1) {
                objFicha.setUsuarioInsert(nameUser);
                objFicha.setDataInsert(dataModFinal);
                objFicha.setHorarioInsert(horaMov);
                //
                control.incluirFichaTecnicaNUTRI(objFicha);
                buscarCodigo();
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                Salvar();
            }
            if (acao == 2) {
                objFicha.setUsuarioUp(nameUser);
                objFicha.setDataUp(dataModFinal);
                objFicha.setHorarioUp(horaMov);
                objFicha.setIdFicha(Integer.valueOf(jIdFicha.getText()));
                control.alterarFichaTecnicaNUTRI(objFicha);
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                Salvar();
            }
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

