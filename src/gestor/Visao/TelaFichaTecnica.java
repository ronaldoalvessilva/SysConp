    public void buscarCodigo() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM FICHA_TECNICA_NUTRI");
            conecta.rs.last();
            jIdFicha.setText(conecta.rs.getString("IdFicha"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Não foi possível obter o código da ficha técnica.\nERRO: " + e);
        }
        conecta.desconecta();
    }

