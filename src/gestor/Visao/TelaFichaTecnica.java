    private void jQtdBrutaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jQtdBrutaFocusLost
        // TODO add your handling code here:               
        DecimalFormat qtdReal = new DecimalFormat(",###00.000");
        qtdReal.setCurrency(Currency.getInstance(new Locale("pt", "BR")));
        try {
            objIngredientes.setQtdItem(qtdReal.parse(jQtdBruta.getText()).floatValue());
            objIngredientes.setPerda(qtdReal.parse(jPerdaGrama.getText()).floatValue());
            objIngredientes.setQuantidadeFinal(qtdReal.parse(jQuantidadeFinal.getText()).floatValue());
            objIngredientes.setPesoBruto(qtdReal.parse(jPesoBruto.getText()).floatValue());
            objIngredientes.setPesoLiquido(qtdReal.parse(jPesoLiquido.getText()).floatValue());
            objIngredientes.setFatorCorrecao(qtdReal.parse(jFatorCorrecao.getText()).floatValue());
            objIngredientes.setFatorCoccao(qtdReal.parse(jFatorCoccao.getText()).floatValue());
        } catch (ParseException ex) {
        }
        // CALCULANDO O FATOR DE COCÇÃO DO ALIMENTO.
        if (fatorCoccao > 0 && fatorCorrecao == 0) {
            qtdPronta = (fatorCoccao * objIngredientes.getQtdItem());
            jQuantidadeFinal.setText(String.valueOf(Float.valueOf(qtdPronta)));
            //            
            DecimalFormat qtFinal = new DecimalFormat("###,###0.000");
            String qtdLiFinal = qtFinal.format(qtdPronta);
            jQuantidadeFinal.setText(qtdLiFinal);
            // CALCULA O FATOR DE CORREÇÃO DO ALIMENTO
        } else if (fatorCoccao == 0 && fatorCorrecao > 0) {
            qtdPronta = (objIngredientes.getQtdItem() / fatorCorrecao);
            //jQuantidadeFinal.setText(String.valueOf(Float.valueOf(qtdPronta).floatValue()));                                  
            DecimalFormat qtFinal = new DecimalFormat("###,###0.000");
            String qtdLiFinal = qtFinal.format(qtdPronta);
            jQuantidadeFinal.setText(qtdLiFinal);
            // CALCULA O FATOR DE CORREÇÃO E O FATOR DE COCÇÃO SOMANDO OS RESULTADOS.
        } else if (fatorCoccao > 0 && fatorCorrecao > 0) {
            qtdPronta = (objIngredientes.getQtdItem() / fatorCorrecao) + (fatorCoccao * objIngredientes.getQtdItem());
            jQuantidadeFinal.setText(String.valueOf(Float.valueOf(qtdPronta)));
            //            
            DecimalFormat qtFinal = new DecimalFormat("###,###0.000");
            String qtdLiFinal = qtFinal.format(qtdPronta);
            jQuantidadeFinal.setText(qtdLiFinal);
        } else if (fatorCorrecao == 0 && fatorCoccao == 0) {
            qtdPronta = objIngredientes.getQtdItem();
            DecimalFormat qtFinal = new DecimalFormat("###,###0.000");
            String qtdLiFinal = qtFinal.format(qtdPronta);
            jQuantidadeFinal.setText(qtdLiFinal);
        }
    }//GEN-LAST:event_jQtdBrutaFocusLost

