    public void buscarIDMov() {
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM MOVPOPULACAO");
            conecta.rs.last();
            jIdPop.setText(String.valueOf(conecta.rs.getInt("IdPopMov")));
            conecta.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel encontrar LANÇAMENTO DE ENTRADA \nERRO: " + ex);
        }
    }

    public void buscarDataPopulacao() {

        SimpleDateFormat formatoAmerica = new SimpleDateFormat("dd/MM/yyyy");
        dataPop = formatoAmerica.format(jDataPop.getDate().getTime());
        conecta.abrirConexao();
        try {
            conecta.executaSQL("SELECT * FROM MOVPOPULACAO WHERE DataPopMov='" + dataPop + "'");
            conecta.rs.first();
            dataPopBanco = conecta.rs.getString("DataPopMov");
            String dia = dataPopBanco.substring(8, 10);
            String mes = dataPopBanco.substring(5, 7);
            String ano = dataPopBanco.substring(0, 4);
            dataPopBanco = dia + "/" + mes + "/" + ano;
        } catch (Exception e) {
        }
    }

