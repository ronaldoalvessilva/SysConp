    private void jBtFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtFinalizarActionPerformed
        // TODO add your handling code here:
        String statusPop = "FINALIZADO";
        JOptionPane.showMessageDialog(rootPane, "Se esse registro for finalizado,\nvocê não poderá mais excluir ou alterar.");
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente FINALIZAR a POPULAÇÃO selecionado?", "Confirmação",
                JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            objPopMov.setStatusPop(statusPop);
            objPopMov.setIdPopMov(Integer.parseInt(jIdPop.getText()));
            control.finalizarMovPop(objPopMov);
            jStatusPop.setText(statusPop);
            JOptionPane.showMessageDialog(rootPane, "Registro FINALIZADO com sucesso !!!");
            jDataPop.setEnabled(!true);
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(!true);
            jBtExcluir.setEnabled(!true);
            jBtSalvar.setEnabled(!true);
            jBtCancelar.setEnabled(!true);
            jBtAuditoria.setEnabled(true);
        }
    }//GEN-LAST:event_jBtFinalizarActionPerformed

