    private void jTabelaInternosEvadidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaInternosEvadidoMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String IdLanc = "" + jTabelaInternosEvadido.getValueAt(jTabelaInternosEvadido.getSelectedRow(), 0);
            jIdLanc.setText(IdLanc);
            String nomeInterno = "" + jTabelaInternosEvadido.getValueAt(jTabelaInternosEvadido.getSelectedRow(), 3);
            jPesqNomeInterno.setText(nomeInterno);
            jBtAuditoria.setEnabled(true);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM EVADIDOSIND "
                        + "INNER JOIN PRONTUARIOSCRC "
                        + "ON EVADIDOSIND.IdInternoCrc=PRONTUARIOSCRC.IdInternoCrc "
                        + "WHERE IdLanc='" + IdLanc + "'");
                conecta.rs.first();
                jIdLancEvasao.setText(String.valueOf(conecta.rs.getInt("IdLanc")));
                jDataEvasao.setDate(conecta.rs.getDate("DataLanc"));
                jOperacao.setText(conecta.rs.getString("TipoOp"));
                jIdInternoEvadido.setText(conecta.rs.getString("IdInternoCrc"));
                jNomeInternoEvadido.setText(conecta.rs.getString("NomeInternoCrc"));
                jObservacao.setText(conecta.rs.getString("Observacao"));
                conecta.desconecta();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por DATA " + e);
            }
        }
    }//GEN-LAST:event_jTabelaInternosEvadidoMouseClicked

