    public void pesquisarLancamentos(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Tipo de Operação ", "Nome do Interno"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataLancaMov = conecta.rs.getString("DataLanc");
                String dia = dataLancaMov.substring(8, 10);
                String mes = dataLancaMov.substring(5, 7);
                String ano = dataLancaMov.substring(0, 4);
                dataLancaMov = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count));
                dados.add(new Object[]{conecta.rs.getInt("IdLanc"), dataLancaMov, conecta.rs.getString("TipoOp"), conecta.rs.getString("NomeInternoCrc")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaInternosEvadido.setModel(modelo);
        jTabelaInternosEvadido.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTabelaInternosEvadido.getColumnModel().getColumn(0).setResizable(false);
        jTabelaInternosEvadido.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaInternosEvadido.getColumnModel().getColumn(1).setResizable(false);
        jTabelaInternosEvadido.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTabelaInternosEvadido.getColumnModel().getColumn(2).setResizable(false);
        jTabelaInternosEvadido.getColumnModel().getColumn(3).setPreferredWidth(350);
        jTabelaInternosEvadido.getColumnModel().getColumn(3).setResizable(false);
        jTabelaInternosEvadido.getTableHeader().setReorderingAllowed(false);
        jTabelaInternosEvadido.setAutoResizeMode(jTabelaInternosEvadido.AUTO_RESIZE_OFF);
        jTabelaInternosEvadido.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        aliharCamposTabela();
        conecta.desconecta();
    }

