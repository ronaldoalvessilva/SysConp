    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdCodigoProf"), conecta.rs.getString("DescricaoProf")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não existe dados a ser exibido!!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaProfissoes.setModel(modelo);
        jTabelaProfissoes.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaProfissoes.getColumnModel().getColumn(0).setResizable(false);
        jTabelaProfissoes.getColumnModel().getColumn(1).setPreferredWidth(320);
        jTabelaProfissoes.getColumnModel().getColumn(1).setResizable(false);
        jTabelaProfissoes.getTableHeader().setReorderingAllowed(false);
        jTabelaProfissoes.setAutoResizeMode(jTabelaProfissoes.AUTO_RESIZE_OFF);
        jTabelaProfissoes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

