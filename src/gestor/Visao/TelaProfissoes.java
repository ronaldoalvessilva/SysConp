    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaProfissoesManuTO);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || nomeGrupoTO.equals("ADMINISTRADORES") || codigoUserTO == codUserAcessoTO && nomeTelaTO.equals(telaProfissoesManuTO) && codGravarTO == 1) {
            buscarProfissao();
            if (jDescricaoProf.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Descrição da profissão não podem ser em branco");
                jDescricaoProf.requestFocus();
            } else {
                objProf.setStatusProf((String) jComboBoxStatusProf.getSelectedItem());
                objProf.setDescricaoProf(jDescricaoProf.getText());
                if (acao == 1) {
                    if (jDescricaoProf.getText().trim().equals(nomeProfissao)) {
                        JOptionPane.showMessageDialog(rootPane, "Profissão já cadastrada.");
                    } else {
                        objProf.setUsuarioInsert(nameUser);
                        objProf.setDataInsert(dataModFinal);
                        objProf.setHorarioInsert(horaMov);
                        //
                        control.incluirProfissao(objProf);
                        buscarID();
                        objLog();
                        controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                        JOptionPane.showMessageDialog(null, "Registro GRAVADO com sucesso!!!");
                        Salvar();
                    }
                }
                if (acao == 2) {
                    objProf.setUsuarioUp(nameUser);
                    objProf.setDataUp(dataModFinal);
                    objProf.setHorarioUp(horaMov);
                    //
                    objProf.setIdCodigo(Integer.parseInt(jIdCodigo.getText()));
                    control.alterarProfissao(objProf);
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    JOptionPane.showMessageDialog(null, "Registro GRAVADO com sucesso!!!");
                    Salvar();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acesso não autorizado, solicite liberação ao administrador.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

